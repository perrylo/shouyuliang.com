						<p>Grandmaster Shou-Yu Liang is one of the recipients of the Coach of Excellence title, given only to the most outstanding Chinese martial arts instructors by the People's Republic of China.</p>
						<p>Grandmaster Liang's martial arts ancestry originated five generations ago. He started his traditional Emei kungfu and Qigong training with his grandfather in 1948. Through the introduction of his grandfather, Grandmaster Liang then sought out other renowned masters and other styles from Shaolin and Wudang. In the early sixties, Grandmaster Liang began his study and research in the few major styles of Taiji such as Yang, Chen, Sun, and Wu style, Buddhist Esoteric Qigong, and Taoist Qigong. Grandmaster Liang had many times been a gold medalist in Wushu and Taiji competitions held in Sichuan province. Representing Sichuan province, he very often entered into highly competitive national and international competitions and won many gold medals. He was also a many-time gold medalist in Chinese wrestling, weight lifting and gymnastics competitions. In Sanshou and Taiji Push Hands competitions organized by non government sponsored communities, he was undefeated in all events he competed in. In the early sixties, he began his career as a university Wushu coach. His skill coupled with his kindness, generosity, sense of humour, and a ready smile had earned him the friendship and the heart of countless numbers of people.</p>
						<p>Grandmaster Liang had been a many-time judge for the Chinese provincial and national professional Wushu competitions. Upon immigrating to Canada, Grandmaster Liang was elected head coach of the first Canadian National Wushu Team in 1985. Competing in the first world Wushu tournament in China that year, the Canadian team finished third. In 1986, in a tournament involving 28 nations, his Canadian team placed second, next only to the powerful China team. The Canadian success story shocked the Chinese nation, and news of their outstanding accomplishment spread throughout China.</p>
						<p>Grandmaster Liang has many times been the head judge or the chairman of the judging team for national and international Wushu tournaments. He formerly held the positions of Chairman of Wushu, Taiji, Qigong instruction at the University of British Columbia Physical Education and Recreation Department. In 1987, Grandmaster Liang formally established the Shouyu Liang Wushu, Taiji and Qigong Institute in Vancouver,Canada. A year later he went on to found the International Wushu Sanshou Dao Association (IWSD) and was selected as the Chairperson. Today, still chaired by Grandmaster Liang, the IWSD has created branches in more than 20 countries around the world.</p>
						<p>In 1994, Grandmaster Liang led the North American Marital Arts Exhibition Team for a friendship performance tour to ten major cities in China. His team received a warm welcome by the people and government of China. While in China, the team also competed in the international Wushu Competition held in Shanghai, for which 32 nations were represented. Grandmaster Liang's team received 42 out of 56 gold medals awarded to the competitors. The Canadian Prime Minister at the time, Jean Chretien, also wrote a letter of encouragement to the team. Many Chinese television stations, radio stations, and newspapers spread the news of the Exhibition Team all over China.</p>
						<p>Since 1985, most of the top ranked competitors in the Canadian and U.S. National and International Competitions are or have been students of Grandmaster Liang. Many of Grandmaster Liang's students in China, the United States, and Canada have become distinguished instructors and coaches themselves.</p>
						<p>Grandmaster Liang is well-known and respected internationally. He was selected by the China Wushu Magazine in the "Biography of Today's Extraordinary Martial Artists". He has been awarded the "World's Top 100 Outstanding Martial Art Professional Award", "World's Greatest Contribution Award", and "World's Outstanding Accomplishment Award". He has also been selected to be included in the "Current List of Famous Martial Artists" and in the Chinese "Who's who in the world" as well as the "international Who's Who of Professionals Historical Society" based in the United States-having demonstrated exemplary achievements and distinguished contributions to the business community.</p>
						<p>Grandmaster Liang has written and produced numerous books and video tapes. Many of these books and video tapes had been translated into other languages such as French, Polish, Greek, Chinese, and Russian. He has been featured a great number of occasions on international television networks including Chinese Central Television(CCTV), Cable News Network(CNN), Discovery Channel and many other Canadian, Mexican, Greek, British and various European networks. He has also been featured on the covers of many prestigious Chinese, American, and Canadian newspapers and magazines. In 2002, Grandmaster Liang was bestowed the title of Life Time Honorary Chairman and ranked a 10th degree by the Confederation of Canadian Wushu Organizations - the highest level of Wushu achievement by that organization.</p>
