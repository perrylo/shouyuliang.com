<script>
(function() {
  const nav = document.querySelector('ul.menu');
  const mainMenuItems = nav.querySelectorAll('ul.menu > li');
  const background = document.querySelector('.dropdownBackground');

  function handleEnter() {
    const dropdown = this.querySelector('.headerMenu');
    if (!dropdown) return;

    this.classList.add('trigger-enter');
    setTimeout(() => this.classList.contains('trigger-enter') && this.classList.add('trigger-enter-active'), 150);
    background.classList.add('open');

    const dropdownCoords = dropdown.getBoundingClientRect();
    const navCoords = nav.getBoundingClientRect();


    const coords = {
      height: dropdownCoords.height,
      width: dropdownCoords.width,
      top: dropdownCoords.top - navCoords.top,
      left: dropdownCoords.left - navCoords.left
    };

    background.style.setProperty('width', `${coords.width}px`);
    background.style.setProperty('height', `${coords.height}px`);
    background.style.setProperty('transform', `translate(${coords.left}px, ${coords.top}px)`);
  }

  function handleLeave() {
    this.classList.remove('trigger-enter', 'trigger-enter-active');
    background.classList.remove('open');
    background.style.setProperty('width', `0px`);
    background.style.setProperty('height', `0px`);
  }

  mainMenuItems.forEach((m) => {
    m.addEventListener('mouseenter', handleEnter);
    m.addEventListener('mouseleave', handleLeave);
  });
})();
</script>
