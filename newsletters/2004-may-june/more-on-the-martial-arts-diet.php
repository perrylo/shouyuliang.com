	        <h2>By Ayron Howey</h2>
	        <br><br>
			<p>As a Martial Artist it is important to remember that you are an athlete, whether you practice Tai chi, Qigong or Sanshou. As an athlete you should be aware of the fuel you are providing for you body as it has an effect on your performance.</p>
			<p>The key point as a Chinese Martial Arts practitioner is to absorb the eating philosophy of the art and culture you have become a part of. This doesn't mean a complete change in your diet from what you are used to eating, to Chinese food everyday. But you should make an effort to eat more sensibly.</p>
			<p>There is a vast tradition in Chinese cooking and diet beyond won tons and egg rolls. There is philosophy; elements of cooking and proper balance of the senses that are all deeply rooted in Chinese Cuisine. My diet is a fusion of what I grew up eating here in Canada and what I have learned from teachers or classmates in my years of study in the Martial Arts. I have also studied the nutrition of many athletes and sports over the years for personal benefit and knowledge. That said, what I am giving here is my own advice and is based on generalizations, not a personal, individual assessment for the reader.</p>
			<p>In a previous article I spoke about some quick steps on getting started on eating healthier. I want to talk now about carbohydrates and the fad being spoken of everywhere you turn. There are tons of commercials out there exclaiming, Low Carbs or No Carbs in various foods clearly saying that this is a benefit. Did you know that vegetables are Carbs? Did you assume that Carb (short for carbohydrate) meant fat? Or starch? This is an important distinction. Does the average person eat too much starch? I would say yes. Does the average person eat too much fat? Again, I would answer yes. Does the average person eat too many carbs? No, as it is important what kind of carbs you are consuming.</p>
			<p>Carbohydrates are the food that provides energy for your body, why would this be a bad thing? It can be very misleading. For example, white rice is the staple of many cultures diets yet today we are told to eat brown rice instead of white. Have you ever asked why this is? What's the difference between brown and white rice? From my research, the only difference is that brown rice is white rice with the husk left on. So the only real difference is that brown rice has more fiber. If you are eating 4-6 servings of fruits and vegetables a day chances are you are covered fiber-wise.  Incidentally when I am talking about white rice I am not talking about instant rice-in-a-box type stuff.</p>
			<p>Don't be fooled by the Low Carb fad out there. Watch what you eat, cut back in fat and sugar, watch your starchy foods like pasta, potatoes, rice not eating too much, eat lots of fresh vegetables and fruits and get plenty of exercise. Listen and look at your body if are wondering if what you are eating is good. Then you can begin to make the necessary changes on your own personal goals and ideals.</p>
