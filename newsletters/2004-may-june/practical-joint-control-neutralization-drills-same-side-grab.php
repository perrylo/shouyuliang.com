			<h3>Neutralizing a Grab from the Top - Same Side Grab</h3>
			<img src="../../images/newsletter/v4n3/a5p1.jpg" width="233" height="250" border="0" alt="Practical Joint Control - Neutralization Drills II" align="left">
			<p><b>Step 1.</b> Your partner grabs a hold of your right wrist with his left hand (Figure 5-6). Drop your right elbow slightly, rotate your palm to face you, then circle your forearm up counterclockwise (Figure 5-7). Rotate your right forearm and press the edge of your right palm on his wrist and cut down to neutralize the grab (Figure 5-8). Repeat 10 times, then grab your partner's wrist and let your partner practice this exercise.</p>
			<br clear="all">
