			<p><b>Step 2.</b> Your partner grabs a hold of your right wrist with his left hand (Figure 5-9). Bend your elbow and press your forearm into his hand, while pulling your arm away, to neutralize the grab (Figure 5-10). Repeat 10 times, then grab your partner's wrist and let your partner practice this exercise.</p>
			<p><img src="../../images/newsletter/v4n3/a5p3.jpg" width="500" height="535" border="0" alt="Practical Joint Control - Neutralization Drills II"></p>
			<p><b>Step 3.</b> Your partner grabs a hold of your right wrist with his left hand (Figure 5-11). Circle your arm up counterclockwise up (Figure 5-12). Open your hand and press the space between your index finger and thumb into his forearm as you turn your body to your left, to neutralize the grab (Figure 5-13). Repeat 10 times, then grab your partner's wrist and let your partner practice this exercise.</p>
			<p><b>Step 4.</b></p> Repeat Steps 1 to 3. Instead of your partner grabbing your right wrist with his left hand, your partner grabs a hold of your left wrist with his right hand.</p>
