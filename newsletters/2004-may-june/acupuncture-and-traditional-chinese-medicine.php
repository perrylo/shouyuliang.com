			<p>It should be noted that the poor circulation of Qi and Blood does not always lead to pain as it can manifest withother symptoms and cause different problems. I will discuss these problems in future articles.</p>
			<p><b>How can Acupuncture help restore the flow of Qi and Blood?</b><br />
			After a blockage or stagnation of Qi and/or Blood has occurred, an acupuncture treatment can help restore circulation and remove the pain and discomfort. In a typical acupuncture treatment session, very fine (as fine as a strain of hair) stainless steel needles are inserted into acupuncture points along the affected meridian on the body. Stimulation of these needles will restore the flow of Qi and subsequently of Blood as well. Hence the pain and discomfort will no longer be a problem.</p>
			<p><b>Things that aid the flow of Qi and Blood</b><br />
			Tai chi and Qi gong exercises are great for maintaining the smooth circulation of Qi and Blood. Walking and swimmingare also good exercises. High impact exercises on the other hand are not as good as they themselves may cause Qi to
			stagnate due to injury. Doing these exercises on a daily basis is the best way to maintain good health.</p>
			<p>Being happy and laughing a lot also circulates Qi in the body.  As the saying goes: prevention isbetter than cure. So, exercise, be happy and stay healthy.</p>
