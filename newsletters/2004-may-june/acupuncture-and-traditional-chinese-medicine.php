			<p><b>What is Qi and Blood?</b><br />
			According to traditional Chinese Medicine (TCM), Qi is the energy that flows in our body along the meridians and collaterals. When Qi is flowing smoothly in our body, it helps the Blood to flow smoothly as well. As long as there is no
