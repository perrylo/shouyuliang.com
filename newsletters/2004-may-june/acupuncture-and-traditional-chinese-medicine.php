			<p><b>What causes diseases and pain?</b><br />
			According to TCM theory, one of the main causes of diseases and pain are the poor circulation of Qi and Blood. This will be the primary focus of this article.</p>
			<p><b>Qi and Blood Stagnation</b><br />
			Poor circulation will first lead to the stagnation of Qi and eventually the stagnation of Blood as well.WhenQi and Blood are not flowing freely, and has come to a stop, there will be pain in the area where they accumulate.</p>
			<p>For example, in the case of a sport injury, Qi and Blood will stop flowing and accumulate at the trauma site.Other causes include improper diet, the lack of exercise and emotional disturbances (i.e., stress, anxiety, sadness or
