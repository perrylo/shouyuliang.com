	        <br><br>
			<p>If you practice Xing Yi (Shape-Intent Fist), here are some tips for training.  For simplicity, let's classifytraining into 3 levels.</p>
			<p>At the first level, you train <i>Ming Jin</i> - your Obvious Power, your Physical Power.  Basic 5-Elementdrills repeated correctly again and again and again, help you to achieve this.  Xing Yi should look/be clean, crisp, neat, precise, exact - as Sifu says: "Exactly like Xing Yi".  Body movement initiates from the dantien, hands & feet arrive simultaneously.  Your whole body works as one concentrated unit.  Qi/Mind/Intent continues, project your energy on through - project far.</p>
			<p>At the second level, you train <i>An Jin</i> - your Hidden Power, your Secret Power, your Inner Power. Standing meditation, sensitivity drills, energy projection and proper postural alignment with body awareness to "feel" your energy running when repeating the basic 5-Element drills help you to achieve this.</p>
			<p>At the third level, you train <i>Hua Jin</i> - Spontaneous Power, Neutralizing Power, Mysterious Power, Very Freedom Power - a whatever is required type of energy.  More practice, more time, more experience help accumulate this power.  Meditation is key in this stage.  The heart/mind/shape become unified - alignment of body, mind and spirit. Understanding nature. Understanding the essence.</p>
