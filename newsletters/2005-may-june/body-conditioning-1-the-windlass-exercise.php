			<h3>Single Hand Windlass</h3>
			<p><b>Equipment:</b> A two to six pound weight suspended by a cord from the center of a short wooden bar.</p>
			<p><b>Training Method:</b> Stand up with both feet shoulder width apart. With one hand grab an end of the bar (Figure 34), and turn your wrist so that the other end of the bar will turn counterclockwise in a circular motion (Figure 3-5). Every time you turn the bar downward, it will scoop up the cord and lift the weight (Figure 3-6). Wind the cord onto the bar until the weight is all the way up (Figure 3-7). You must turn your wrist quickly so the cord does not unwind. Repeat the same exercise with the other hand, and gradually increase the number of repetitions and the amount of weight.</p>
