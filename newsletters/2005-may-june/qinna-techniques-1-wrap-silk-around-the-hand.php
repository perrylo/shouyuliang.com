			<p>Notes:</p>
			<ol type="A">
				<li>When you rotate your forearm up, let the pivoting point of the clockwise circle be as close to the location of the grab as possible. This will allow you to use minimal force to neutralize the grab. Next, while keeping your elbow pointing down, extend your arm forward and down towards your opponent.</li>
				<li>In executing this technique, you are creating a Z angle with your opponent's arm, and twisting while bending your opponent's forearm and wrist. When you extend your arms forward, extend at a 90 degree angle towards the plane of your opponent's body. When executing the technique, maintain constant pressure on your opponent's wrist. By holding onto your opponent's fingers, you prevent your opponent from escaping from your Qinna.</li>
				<li>When you step back you lower your opponent to the floor and move your body away from your opponent's reach. The technique is completed when you move back into a Half-Squat Stance. However, the movement of your arms should be continuously forward and slightly to the side on the way down. This will maintain the pressure on your opponent's arm.</li>
				<li>There are three common mistakes in executing this technique. First, if your left hand fails to hold onto your opponent's hand when you extend your arms. In this case, you will only be neutralizing and not be able to control your opponent. Second, if your right elbow is raised up to the side instead of maintaining it on the same plane. When this happens your opponent can rotate his body around and hit you with his left arm. Third, if your opponent's arm is not in a Z angle with the edge of his palm facing up. To create the Z angle in this situation, hook the fingers of your right hand back as you extend your arms perpendicular to the plane of your opponent's body.<img src="../../images/icons/10x10_yinyang.gif" width="10" height="10" border="0" alt="" style="margin-left:10px;"></li>
