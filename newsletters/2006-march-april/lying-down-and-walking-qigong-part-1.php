			<h3>Walking Qigong 1</h3>
			<p><b>Movement:</b><br />
			When you walk, start by lifting your back heel up and push down gently with your big toe (Figure 1-32).Whenyou step, touch down with your heel first and your toes pointing up, then roll down to the inside edge of your foot, and keep your knees slightly bent (Figure 1-33).</p>
			<p align="center"><img src="../../images/newsletter/v6n3/a2p1.jpg" width="400" height="309" border="0" alt="Walking Qigong"></p>
			<p>When walking, use your shoulders movements to move your neck, chest, waist, hips, and arms. Allow your head to sway in the direction of your step. Fingers should be naturally coiled in, rotate your wrist slightly, but keep your laogong points facing each other (Figure 1-34).</p>
			<p><b>Intention:</b><br />
			Step 1. Inhale naturally without intention. Exhale, focus on the word song, and use your mind to followyourbreath down to your dantian. Once you are able to control your intention easily, proceed to Step 2, otherwise skip Step 2 and proceed to the Closing.</p>
			<p>Step 2. Inhale, bring your mind from your huiyin, past your mingmen, to your dantian. Exhale, bring yourmindbackto your huiyin.</p>
			<p align="center"><img src="../../images/newsletter/v6n3/a2p2.jpg" width="400" height="317" border="0" alt="Walking Qigong"></p>
			<p><b>Closing:</b><br />
			Step 1. After you have walked for at least 10 minutes, begin doing deep inhalations and exhalations, 24times. When you exhale, use your mind to lead the qi in your body out of your skin to the farthest reaches of the cosmos. Inhale naturally. Then walk for 4-5 minutes without thinking of breathing or intention.</p>
			<p>Step 2. After coming to a stop, stand with your feet about shoulder width apart. Then circle both hands out to your sides and in to your Yintang (Figure 1-35). Repeat the arm movements, 3 times.</p>
			<p>Step 3. Next rub your hands together until you feel warmth and gently massage your face for about 10seconds.</p>
			<p><b>Key Points:</b><br />
			During training, the mind and body should be free from worry and comfortable. Stay away from fatty foods, excessivesweets, excessive oily foods, and stale food. This way, the benefits of this training can be more effectively attained.</p>
			<h3>Walking Qigong 2</h3>
			<p><b>Movement:</b><br />
			When you walk, sway your arms from one side of your body to the other, coordinating with your step.Swayyourarmsto your left when you step forward with your left foot. Sway your arms to your right when you step forward with your right foot. Your palms should face down naturally at an angle. This exercise should be done outdoors where the air is fresh. Dress warm.</p>
			<p><b>Intention:</b><br />
			Step 1. Left side: Take two small inhalations as you step forward with your left foot. Exhale once as you step forward with your right foot. Walk for 20 minutes.</p>
			<p>Step 2. Right side: Take two small inhalations as you step forward with your right foot. Exhale onceas you step forward with your left foot. Walk for 20 minutes.</p>
