	        <h1 class="contentheading">Using Wushu & Tai Chi Principles to Resource Yourself in Dealing with Shock and Trauma</h1>
	        <h2>By David Wu, R.Ac.</h2>
	        <br><br>
	        <p>Shock and trauma is-unfortunately-a part of life. The recent horror of the terrorist attacks on the United Stateson September 11, 2001 testifies to this unfortunate truth.  As practitioners of taiji, qigong, and wushu, what can we do? Fortunately, if we look at the principles behind our wushu training, we will see that that it encapsulates a lot of body wisdom. Furthermore, those principles can be applied in daily life to maintain and enhance our physical, emotional, social, and spiritual well-being.</p>
			<p>Wushu emphasizes the importance of physical posture. Proper execution of the bow stance, the horse stance, the empty stance, and so on, gives the practitioner a sense of power, alignment, and grace. The stances have the ability to invoke psychological qualities of courage, equanimity, and poise. Most stances entail keeping the spine straight. By doing so, we enable the lungs to expand to their full capacity, thereby fully oxygenating our brain and body, empowering the blood and qi to circulate properly. The very act of straightening the spine from poor posture heightens our awareness. Notice how kids instinctively sit up straight when they are (or think they are about to be) caught goofing off in class?</p>
			<p>Harmonized breathing in qigong or internal arts such as Taijiquan helps us to clear the mind and increases our ability to focus, relax, and manage our emotions. The visualization exercise from the preparation stance of the Yang Style 24-form<sup>1</sup> is an excellent way to relieve stress: stand with knees slightly bent, imagine a waterfall over your head, gently pouring down your body. As the water passes through the body, feel the tension released from head, neck, shoulders, chest, spine, feet, etc., and then into the ground. Repeat the process until you feel completely relaxed and calm. After this process, "touch your tongue to the roof of your mouth. As saliva is being generated, swallow it and use your mind to follow the saliva down your throat to your stomach. Then bring your attention to your Dantian-your body's center of gravity."<sup>2</sup> Awareness of the dantian (solar plexus) helps you be aware of your center of gravity in your body. It also has the psychological effect of centering your mind so your attention and emotions are not scattered in the normal flow of restless thoughts.</p>
			<p>The body awareness and sensitivity you develop from these practices help you gauge your inner barometer: Am Igrounded and centered? Where am I still tense? Do I need to drink some water? What does the body need right now? Drinking warm (or room temperature) water when you are in a state of emotional shock helps calm the body down. Taking a shower or going swimming is also very beneficial because it takes advantage of water's ability to soothe and redistribute the currents throughout the body.&nbsp;&nbsp;<img src="../../images/icons/10x10_yinyang.gif" width="10" height="10" border="0" alt=""></p></div>
