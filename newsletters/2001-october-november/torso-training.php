	        <h1 class="contentheading">Torso Training</h1>
	        <h2>By Perry Lo and Rena Huang</h2>
	        <br><br>
	        <p>Having strong abdominal muscles is very important for Wushu, especially for northern style forms where torso movements are important.  The abs are fundamental for that extra inch of reach in punching and for that extra half turn in the butterfly twist jump.  They give you greater control in your Fan Yao, aid in stabilizing your lower back, and are essential in kip-ups.  And beyond all that, having well trained abs makes you look good too!</p>
			<p>Here are two exercises that can be done in ten minutes each day.  They are simple, but effective.Seniors should use their judgement in carry out these movements.  If you have any health related problems, be sure to consult your doctor before starting any exercise routine, as these exercises may be too strenuous for some.</p>
			<h3>Leg Raises</h3>
			<a href="javascript:createWindow(540,460,'Torso Training','RenaHuang - Torso Training','/images/newsletter/v1n6/a4p1b.jpg')" class="picture" onclick="this.blur()"><img src="../../images/newsletter/v1n6/a4p1s.jpg" ALT="Rena Huang - Torso Training" border="0"> </a>
			<a href="javascript:createWindow(540,460,'Torso Training','RenaHuang - Torso Training','/images/newsletter/v1n6/a4p2b.jpg')" class="picture" onclick="this.blur()"><img src="../../images/newsletter/v1n6/a4p2s.jpg" ALT="Rena Huang - Torso Training" border="0"> </a>
			<p>Begin by lying flat on the floor with your hands resting comfortably at your sides.  With your feet together,raise your legs one inch off the floor.  This is the starting position.  Pay attention to the small of your back, if you feel any discomfort at any time you should discontinue.</p>
			<p>Focus on using your lower abdominals, and raise your legs slowly with your toes pointed to theverticalposition.  Hold for one second and bring your legs slowly back to the starting position.  Remember that your feet never touch the floor.</p>
			<p>To start, try three sets of 10 repetitions, pausing for a minute between each set.  As you becomemoreadept, try adding increments of 5 reps to each set.</p>
			<h3>Power Crunches</h3>
			<a href="javascript:createWindow(540,460,'Torso Training','Perry Lo- Torso Training','/images/newsletter/v1n6/a4p3b.jpg')" class="picture" onclick="this.blur()"><img src="../../images/newsletter/v1n6/a4p3s.jpg" ALT="Perry Lo - Torso Training" border="0"> </a>
			<a href="javascript:createWindow(540,460,'Torso Training','Perry Lo- Torso Training','/images/newsletter/v1n6/a4p4b.jpg')" class="picture" onclick="this.blur()"><img src="../../images/newsletter/v1n6/a4p4s.jpg" ALT="Perry Lo - Torso Training" border="0"> </a>
			<p>Begin as before with your feet together one inch off the floor, but with your arms extended straightoverhead.</p>
			<p>With one smooth motion, bring both your feet and hands together.  Be aware to control your speed,using both your lower and upper abdominals.  At the "top" position, hold for half a second and return to the starting position, again controlling your descent.</p>
			<p>To start, try three sets of 5 repetitions, pausing for a minute between each set.  As youimprove,add 1 repetitionto each set for difficulty.</p>
			<p>Dedication and consistent training at the cost of only several minutes each day will yield you a stronger, fitter, and happier self!  Train hard!</p>
