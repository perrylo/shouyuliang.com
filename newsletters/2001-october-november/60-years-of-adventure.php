			<p>Once everyone had satisfied their appetites, a video presentation in honour of Sifu was shown.  It was a moving photographic collage of Sifu's extraordinary life from the time he was child to when he started his family and came Canada.  Afterwards, Sifu was presented with his many gifts which he opened one by one and humbly thanked his students.  A large birthday cake was brought in at which Sifu ceremoniously and humorously cut with a large Pu Dao, showing the great control he had in his hands.</p>
			<p>As the day drew to a close, there was laughter and tears at the celebration of someone who had touched so many lives.  The friends, family, and students of Grandmaster Liang were successful in touching the heart of their Sifu in return for the wisdom, patience, generosity, and kindness that he had given so much of over so many years.  It would be a day that no one would soon forget.</p>
