			<p>"Okay everyone, shhhhh!"  The room had already been dark for the last five minutes in anticipation of its next guest.  And ten minutes just before that, the room was bustling with the commotion of guests and performers, all preparing for a great celebration.  Just outside the main set of double doors into the room there was a stirring of voices.  Everyone settled quickly, in spite of the excitement.</p>
			<p>The doors moved, slowly at first, and a beam of daylight flashed across several dozen anxious faces just inside.  "Not yet," someone whispered.  The doors opened more, and a man, small and curious, appeared in the doorway.</p>
			<p>All at once, the lights in the large ballroom illuminated, showing the man 300 smiling eager faces.  "Surprise!  Happy birthday, Sifu!"</p>
			<a href="javascript:createWindow(540,460,'60 Years of Adventure','Grandmaster Shou Yu Liang\'s 60th Birthday Celebration','/images/newsletter/v1n6/a6p3b.jpg')" class="picture" onclick="this.blur()"><img src="../../images/newsletter/v1n6/a6p3s.jpg" ALT="Grandmaster Shou Yu Liang's 60th Birthday Celebration" class="left"></a>
			<p>Grandmaster Shou Yu Liang blinked his wide-open eyes in shock and disbelief.  Here, on this sunny Sunday morning of Sept 2nd were all of his closest friends and family applauding.  He looked around, trying to absorb the shock, as another 40 of his students, all colourfully dressed in their performance uniforms, parted way in front of him.  As he watched, they took practiced steps, moving purposefully into an aisle leading to an elegantly decorated table at the opposite end of the room.  His daughters, Helen and Maria, and his wife entered behind him, and laughed at his reaction.</p>
			<p>As Grandmaster Liang sat in his chair, Tony Bujas and Rena Huang, the MCs for the day, greeted the crowd.  "On behalf of the Shou-Yu Liang Wushu, Taiji, Qigong Institute, I bid you welcome to Master Shou Yu Liang's 60th birthday celebration!"</p>
			<p>The ceremony proceeded with a performance of a well-practiced Wushu presentation that Grandmaster Liang had been mislead to believe was for senior coach Xiao Hong's up coming wedding.  "I knew there was something [strange] when Helen and Maria brought me to the hotel.  They said that they were taking me to a nice lunch for my birthday so I had to dress very well."  After the flawless demonstration, the crowd feasted on the wonderful buffet lunch that included several different salads, soups, roasted meat, steamed fish, and large selections of fresh fruit.</p>
