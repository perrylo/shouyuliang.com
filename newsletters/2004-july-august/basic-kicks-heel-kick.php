			<h4>Heel Kick (dengtui):</h4>
			<p>A Heel Kick is accomplished in a similar manner as a Spring Kick, by first lifting the kicking knee up over your waist, then extending the kicking foot forward. When you kick, point the foot up and kick with the heel.</p>
			<p><b>Drill:</b> Start with your feet together and fists at your waist. Take a small step forward with your right foot and extend your left fist in front of you (Figure 2-23).</p>
			<div align="center"><img src="../../images/newsletter/v4n4/a4p1.jpg" border="0" width="500" height="264" alt="Heel Kick"></div>
			<p>Shift all your weight onto your right foot, lift your left knee up over your waist and extend your left foot forward. At the same time, punch forward with your right fist, and pull your left fist back to your waist (Figure 2-24).</p>
			<p>After completing the kick, bring your leg back down by bending your left knee, keeping your knee above your waist, and pointing your foot down. This completes a left Heel Kick.</p>
			<p>To do the other side, step down and forward with your left foot, shift your weight forward onto your left leg and kick with your right foot. At the same time, punch forward with your left fist and pull your right fist back to your waist. Alternate from side to side across the length of the training area.</p>
