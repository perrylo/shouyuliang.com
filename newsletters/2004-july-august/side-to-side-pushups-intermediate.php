			<p>These are fun push-ups to do to add some variety into your routine. I see that other websites are linking to my previous push up articles and as I haven't seen these done elsewhere, maybe they will garnish some special attention.</p>
			<p>This particular routine is one of my favorites and they help to strengthen your body for one-arm push-ups.</p>
			<p>Start by laying flat on the ground on your front with both arms out to the side 90 degrees from the body. Move your outstretched fingertips in about one foot. Your fingertips should point away from your body, and looking forward lift yourself up straight. That's the easy part.</p>
			<div align="center"><img src="../../images/newsletter/v4n4/a7p1.jpg" width="500" height="250" alt="Side-to-Side Pushups"></div>
			<p>You are now going to lower your body down to the right side, bending your right arm and completely straightening your left arm, resting on your left palm heel. Then, keeping your spine straight and still looking forward, push up back to the start position and repeat this on the left side. Do 5 repetitions on each side to start, and progress to 10 or more as you improve your strength.</p>
			<p>Keep in mind to push all the way up so the arms are straight between each repetition.  Don't simply shift your weight from side to side.</p>
