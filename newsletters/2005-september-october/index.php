				<p><b style="font-size:13px;">"Skill Comes from Practice"</b><br>
				<span class="small">Practice Makes Perfect</span></p>
				<p>CHEN Yaozi was an excellent archer.  One day when he practiced at the range, every one of his arrows hit the bull's eye.  The on-lookers all applauded for him.  Only an old oil-vendor did not appear to be moved very much.  He only nodded his head.</p>
				<p>Seeing that he had been held in contempt, Chen Yaozi asked the old oil-vendor, "Do you also shoot arrows?"  The old man said, "I don't know archery.  But, in my opinion, there's nothing special in your shooting good arrows.  It is only because you are skilled."  Chen became somewhat angry.  He said, "You don't know how to shoot arrows.  And yet you look down on me.  This is really preposterous."  The old man said, "I'm an oil-vendor.  I've learned something from ladling oil.  I can demonstrate it for you, if you like."</p>
				<p>The old oil-vender put the gourd which he used for holding oil on the ground.  He placed a copper coin over the opening of the gourd and began to pout oil into the gourd with a ladle through the hole in the middle of the coin.  He poured a lot of oil into the gourd without allowing any oil touching the hole of the coin.  Smiling, the old man said to Chen Yaozi, "You see.  There is nothing special.  It's only because I'm skilled."</p>
				<p>After that, Chen Yaozi never showed pride again over his archery.</p>
