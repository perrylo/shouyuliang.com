			<p>In Hsing Yi Chuan, the five basic patterns are related to the Five Phases of Chinese philosophy and medicine, and have the same inter-relationships.  These Five Phases are Metal, Water, Wood, Fire, and Earth.  Therefore, these five basic patterns are also commonly called the "Wuu Hsing" (Five Phases) or "Wuu Chuan" (Five Fists).  It is believed that the movements of each of these five patterns benefits the health of the related internal organ.</p>
			<p>There is a Tao of mutual production among the Five Phases.  Metal generates Water, Water generates Wood, Wood generates Fire, Fire generates Earth (ashes), and Earth generates Metal.  (They) also have the meaning (i.e.), (relationships) of mutual conquest.  Metal conquers Wood, Wood conquers Earth, Earth conquers Water, Water conquers Fire, and Fire conquers Metal.</p>
