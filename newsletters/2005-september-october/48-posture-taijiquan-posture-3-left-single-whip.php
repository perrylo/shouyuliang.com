			<h3>Posture 3: Left Single Whip</h3>
			<p><b>Movements:</b><br />Figure 5-12: Shift your weight back to your right leg, while rotating your right palm to face right and pull back. At the same time, lift your left palm up to shoulder level.</p>
			<p>Figure 5-13: Continue the previous movements. Fold your left forearm in next to your chest, while making a big clockwise circle with your right palm until it is under your left palm. As you move your arms, shift all your weight to your left leg and bring the ball of your right foot next to your left.</p>
			<p>Figure 5-14: Step to your right with your right foot into Bow stance, while extending both arms forward. (Step West)</p>
			<p>Figure 5-15: Shift your weight back to your left leg and lift your right foot up. At the same time, begin rotating your right forearm to your right, by pivoting on your elbow with your palm facing up.</p>
			<p>Figure 5-16: Turn your right foot in until it is facing forward, shift your weight on it and bring your left foot next to your right. At the same time, continue the rotation of your right forearm by bringing your palm up to head level, then extend it out to your right and form a hook.</p>
			<p>Figure 5-17: Complete the Single Whip posture by stepping to your left the same as the Single Whip, posture 11, in the Simplified Taijiquan sequence. (Step East)</p>
