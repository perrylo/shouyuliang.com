			<p><b>B. WHEELING THE WEIGHT:</b></p>
			<p><b>Left Side:</b><br />
			Stand up with both feet shoulder width apart and bend slightly at the knees. Hold the weight in front of you with your right palm turned outward and left palm turned inward (Figure 3-25). Back view (Figure 3-26).</p>
			<p>Step with your left foot behind and across your right foot (Figure 3-27) and swing the weight upward in a circular motion. Lead with the left hand and pivot 180 degrees, holding the weight above your head (Figure 3-28).</p>
			<p>Continuing from Figure 3-28 above, step with your left foot behind and across the right foot and swing the weight downward in a circular motion as you pivot from your waist 180 degrees and back to the original form (Figures 3-29, 3-30, and 3-31). Repeat the exercise and gradually increase the weight and number of repetitions. Be sure that the dumbbells are securely fastened to the bar, and do not swing wildly.</p>
			<p align="center"><img src="../../images/newsletter/v5n5/a6p3.jpg" width="500" height="685" border="0" alt="Body Conditioning - Weight Training"></p>
			<p><b>Right Side:</b><br />
			Repeat the same exercises in the opposite direction.</p>
