			<p><b>A. BOTH HANDS PUSH FORWARD:</b></p>
			<p>Stand in Horse Stance, lift the weight up and push it out in front of your chest at shoulder height (Figures 3-22 and 3-23). Then pull the weight in and repeat as many times as you want (Figure 3-24).</p>
