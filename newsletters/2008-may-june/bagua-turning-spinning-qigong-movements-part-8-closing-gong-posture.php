			<h3>Closing Gong Posture</h3>
			<p><b>Verbal Secrets: (Practice) turning-spinning Bagua to return to Pre-Heaven (condition).</b><br />One of the main purposes of Baguazhang practice is to transmute your body's Qi pattern from the Post-Heaven to the Pre-Heaven state. It is believed in Chinese Qigong society that the PreHeaven Qi which is converted from the Original Essence is pure, clean, and healthy - bringing the practitioner inner peace and longevity. Post-Heaven Qi, which is converted from air and food, is potentially more contaminated and harmful to health. Therefore, as do many other Chinese Qigong practices, Baguazhang Qigong emphasizes methods for returning the body to a PreHeaven state.</p>
			<p>Movements:</p>
			<p>From Figure 3-54, bring your right foot next to your left, both feet turned in (Figure 3-55). Lower and extend your right hand to your right, and turn both palms up (Figure 3-56). Raise both palms over your head, inhale, and lower them in front of your while straightening your legs, exhale (Figure 3-57). Continue lowering your palms until they are at your sides. Then turn your right foot until it is pointing forward, and bring your left foot beside your right foot; complete your inhalation (Figure 3-58).</p>
			<div class="small" align="center"><img src="../../images/newsletter/v8n2/bagua-qigong-closing-gong-posture.jpg" width="650" height="188" border="0" alt="Bagua Turning-Spinning Qigong - Closing Gong Posture"><br /><b>Bagua Turning-Spinning Qigong - Closing Gong Posture</b></div>
			<p>Key Notes:</p>
			<p>Pause slightly when you are in the Turn Palm Posture (Figure 3-54). Then continue, and finish the posture. Stand still for a few minutes, allowing your Qi to sink back to your Lower Dan Tian.</p>
