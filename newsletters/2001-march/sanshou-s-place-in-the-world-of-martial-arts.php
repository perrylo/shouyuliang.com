	        <h1 class="contentheading">Sanshou's Place in the World of Martial Arts</h1>
	        <h2>By Ayron Howey</h2>
	        <br><br>
	        <p>I like to think of Sanshou as freestyle application of all martial arts.  You take what you have learned, be it Tai Chi, competition Wushu, Hsing Yi, Chin Na, etc., and apply it to a combat situation.  No matter why you are learning what you are learning, you should know how to actually use what you have been taught, otherwise you have not learned the full lesson.</p>
			<p><img src="../../images/newsletter/v1n2/a5p1.jpg" ALT="Sanshou Sparring - Ayron vs Derek" WIDTH="175" HEIGHT="175" style="float:left;margin-right:20px;">Sanshou is a necessary ingredient to being a true martial artist, although not everyone needs to train to fight.   Few people actually enter combat situations on a regular basis outside of their training, and fewer still fight on a formal basis in national and international competitions.  However, a working martial foundation is where everyone must begin.  Internal styles, such as Qigong and Taiji push hand, external styles, such as Wushu and Sanshou, and cultural attributes including history and philosophy, are all aspects of a martial artist. It is essential, in order to become a better martial artist, to have a working knowledge of all of these, even if your focus remains on one area alone.</p>
			<p><img src="../../images/newsletter/v1n2/a5p6.jpg" ALT="Sanshou Sparring - Tjong vs Howie" WIDTH="180" HEIGHT="120" style="float:right;margin-left:10px;">Sanshou is but one component of martial arts, a necessary piece of the learning puzzle.  For me, Sanshou is a large and important piece.  It is currently my focus, as a serious competitor, and I must fight while I am still young and strong.  Regardless of the martial arts you practice, I urge everyone to share this ideal while training, so you can recover better and push beyond your physical limits.  My age, health, and skill are all factors that I share in common with other fighters, though I believe I have only two more years of competition left.  I do not want any permanent injuries that may affect my learning of martial arts, yet I do not want my growth to be stagnant either.  And for me to grow personally, I must take my training to the next level and compete against those at my level.</p>
			<p>Sanshou, free fighting, is about will.  A strong mind builds a strong body, and thus builds a better person. Bearing this in mind, Sanshou is not about competition, or winning a fight.  As piece of the learning puzzle, it is about learning more about martial arts, finding the other connecting pieces of the puzzle.  It is about developing your skills and becoming a better student, teacher, and person.&nbsp;&nbsp;<img src="../../images/icons/10x10_yinyang.gif" width="10" height="10" border="0" alt=""></p>
