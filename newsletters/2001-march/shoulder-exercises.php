			<p>Rotate the shoulders 90 degrees forward, so that the fingers point forward. Keep the shoulders down, elbows straight, hands tight, and chest open.</p>
			<p>Return to the start position. Be sure not to over-rotate; fingers should not point backward. Try 50 repetitions, and without resting, go on to the second exercise.</p>
