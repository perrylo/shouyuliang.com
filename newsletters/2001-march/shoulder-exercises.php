	        <p>For Wushu, both shoulder strength and flexibility are required.  Increasing these attributes can help give you more control  in your forms and weapons.</p>
			<p><b>Strength</b><br />
			This is a series of three exercises I learned from Master Nick Gracenin, a well-known teacher, athlete, and promoterof Chinese martial arts in PennShou-Yu Liangvania.  Try fifty repetitions for each exercise, without resting in between.  As far as the speed is concerned, approximately one repetition per second is about right.</p>
