			<p><b>Stretch 1</b><br />
			Start with your palms against the wall at approximately shoulder height and less than shoulder width apart. Leanforward,keeping the arms and legs straight, until your arms are behind your ears.  You should feel the stretch in the shoulders.  See if you can touch your head to the wall.  Avoid arching the back.</p>
