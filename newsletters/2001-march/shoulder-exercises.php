			<p><b>Exercise 2</b><br />
			Remaining in your stance, begin with your fingers pointing forward.  Keep the arms 180 degrees out to the sides,at shoulder level, with the elbows straight and the chest open.  With the fingers pointing forward, pull your arms back as far as possible, keeping the elbows straight, shoulders down, and hands tight.  Do not arch your back.</p>
			<p>Return your arms to 180 degrees apart. Repeat fifty times, and without resting, go on to the third part.</p>
