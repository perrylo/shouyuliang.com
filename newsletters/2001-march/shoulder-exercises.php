			<p><b>Exercise 3</b><BR>
			Remain in your stance and begin again with your fingers pointing forward.  Bend the elbows slightly and pullthemback, sothat the chest stays open.  Lift the elbows as high as you can, without lifting the shoulders themselves.</p>
			<p>Return the elbows to level with the shoulders.  Resist dropping the elbows below the shoulders.  Try to do fifty.After this final exercise, you will probably be ready for a stretch!</p>
			<p><b>Flexibility</b><br />
			Here are some basic stretches to help keep the shoulders limber.  All you need is a wall.  Try holding each stretch for 30 seconds to a minute.</p>
