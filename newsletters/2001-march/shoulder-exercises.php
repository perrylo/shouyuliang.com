			<p><b>Exercise 1</b><br />
			Start by standing with the feet at a comfortable distance apart.  Hold the arms 180 degrees out to the sides, atshoulder level, with the elbows straight, and the fingers straight, together, and pointing upward.  Keep the chest open.</p>
			<br clear="all">
