	        <br><br>
	        <p>There are four major approaches in Qigong training: training the body, the breath, the voice, and the mind; with the training of the mind being of the highest importance.  During training, the mind is placed on regulating the body, breathing, and vocalization.  When you have gained control of your body, breathing, and vocalization, you will also have gained control of your mind through disciplining the other three.  In this qigong volume, all four approaches will be presented.</p>
			<p><b>Body</b><br />
			Training the body includes sitting, standing still, and moving.  In the still or meditation training, the body is positioned in a way that is best suited for energy circulation.  In sitting meditation, for example, people often sit with their legs naturally crossed, Half Lotus, or Full Lotus.  The hands are also placed in certain positions.  The proper positioning of the body allows the energy in the body to flow smoother, as well as, becomes a better receiver to the energy of the cosmos.  The moving aspect of Qigong training is easier to comprehend.  Each movement is designed to directly or indirectly simulated specific areas of the body to increase Qi circulation.</p>
			<p><b>Breathing</b><br />
			Most Qigong exercises are done very slowly.  The slow movements allow the mind to register the movementsthroughout the body and to allow the body to be in a more relaxed state, than with fast paced exercise.  This way the breathing can be slower and deeper; and the efficiency of the lungs to take in oxygen can be greatly increased.  Many of the breathing exercises in Qigong include body movements to assist with breathing.  Breathing can also help with your concentration.  By paying attention to your breathing, you will be able to reduce your scattered thoughts.  When your mind is less busy, it will be easier for the body to perform its proper internal functions to keep you healthy - regulating Qi flow and maintaining balance.</p>
			<p><b>Sound</b><br />
			All material substances are in constant vibration.  Molecules in our body are also constantly vibrating.Sound vibrations, for example, are capable of traveling through space and causing an avalanche.  Sound can help you relax or give you a headache depending on the type of sound.  In Qigong training, specific sounds are used for specific purposes.  For example, a song sound can help you relax, and a gentle xu sound can help calm the liver.  Some sounds, chants, and mantras also have mystical connections with the cosmos.</p>
			<p><b>Mind</b><br />
			he mind is the most important part of Qigong training.  The mind's potential is unlimited.  In the trainingof the body, breathing, and voice, the mind is used to regulate them.  When you are able to regulate your body, breathing, and voice, you will have also gained better control of your mind.  When this conscious regulation becomes automatic, you will have attained a state of regulation becomes automatic, you will have attained a state of regulation without consciously regulating - a natural and automatic response to attain energetic harmony.  Your mind will then be able to sense any potential problems and correct them before any damage occurs.  Our mind has amazing mental power.  There is a saying in Buddhism, "All things are the result of the heart (mind)."  The higher goals in Qigong cultivation are to attain Great Wisdom, and realize the Truth of the cosmos.</p>
