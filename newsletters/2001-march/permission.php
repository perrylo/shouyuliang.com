	        <p>Training in the Internal martial arts is always a process of discovering.  Solo work, partner work, exercises, forms, applications, energy sensing-emitting, healing, qigong, and meditation are all methods that can provide you with your opportunity for discovery.  Please note that this is <I>your</I> opportunity, something that you take ownership of.  It belongs to you, and ultimately you are responsible for your progress.</p>
			<p>When beginning, you first learn a technique, a method, a sequence, a form, a type of qigong, a type of meditation, or etc.  You essentially start a process in which you have a procedure to follow, memorizing all the details and duplicating the pattern.  You develop and perfect this tool of exploration as needed to get the most out of it, assimilating and evolving as you progress from basic to advanced levels of skill and knowledge.  Your intellectual mind is needed at first to learn the pattern and then you let it go, tapping into the sub-conscious, and continuing from instinct.  In trying to quiet the mind, disregard detail and let thoughts pass.  Your mind may tend to analyze too much.  How should this feel? Exactly how far do I visualize?  What should I see?  Taste?  Smell?  Feel?  Free yourself of this clutter and allow your sub-conscious to play.  Forgo the analyzing, as the doing is more important.  Do, and experience what flows by!  Gain insights, but do not dwell on any one thing.  Like standing by a river, just watch what comes floating down go by.</p>
			<p>The teacher leads you to the door.  He provides you with the tools you need to open the door and enter.  However,are you willing and brave enough to explore the room, the house, the block, the city, the realm, or the new universe?  Are you willing and brave enough to stand behind your discoveries?</p>
			<p>You have permission to do so!  Sifu, your teacher, gives you permission! Your classmates give you permission!Do you give yourself permission?  You must make your own discoveries in exploring the universe of Qigong. The lessons you need to learn may not be the same for others.  Your path of discovery will require you to make your own way.  Be a trailblazer!  Do not expect to keep the exact same procedure you were taught.  Do expect to evolve along your spiritual path, adding bit by bit your personal input to the original methodology learned.  Have faith in your teacher and in the method that you train.  Keep with the principles to steer clear of any difficulties that may arise as you train.  Meet any obstacles you encounter bravely. Assistance to work through and overcome them can be found with your fundamental support group - your teacher and fellow Qigong classmates.</p>
			<p>Transition from your teacher to independence involves taking ownership of your progress.  Your evolution is important.  Stagnate is not a way to be.  Life is change.  Embrace the changes that naturally develop.  Liken it to painting a picture, everyone may have similar brushes, paints, and subjects, but individual paintings will be unique.  Each picture will have its own distinctive artistic flavor.  Martial science transcends into martial art.</p>
			<p>Remember, you have permission to do so.   Permit yourself to experience!  Permit your experience!  Do it!</p>
