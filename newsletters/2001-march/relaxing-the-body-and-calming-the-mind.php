	        <h2>By Grandmaster Shou-Yu Liang and Master Wen-Ching Wu</h2>
	        <br><br>
	        <p>Relaxing the Body and Claming the Mind Technique (<I>Song Jing Jianshen Gong</I>) is a more advanced relaxation technique that relaxes the body by mentally relaxing the acupuncture points.  It is especially beneficial if practiced before the Stomach and Spleen Qigong methods.  This technique is also a way to calm the mind by lowering unsteady and scattered energy.</p>
			<h3>Preparation</h3>
			<p><b>Posture:</b><br>
			This technique can be practiced while sitting, standing, or lying down.  In a sitting position, place your feet flaton the floor with your palms resting on the inside corner of your knees without touching.  The <I>laogong</I> points on your palms should be on top of your <I>xuehai</I> point.</p>
			<p>In a standing position, allow your arms to hang naturally at your sides.  In a reclined position, layon your backwith your arms at your sides and palms facing down.</p>
			<h3>Visualization</h3>
			<a href="javascript:createWindow(450,580,'Relaxing the Body and Calming the Mind','Relaxing the Bodyand Calming the Mind','/images/newsletter/v1n2/a4p1b.gif')" class="picture" onclick="this.blur()"><img src="../../images/newsletter/v1n2/a4p1s.gif" ALT="Relaxing the Body and Calming the Mind" WIDTH="105" HEIGHT="140" style="float:left;margin-right:10px;"></a>
			<p><b>Step 1:</b><BR>
			Mentally relax the points and areas by focusing on the word <I><B>song</B></I> (pronouced ss-oooo-ng), starting fromyour<I>baihui</I>, to <I>Yintang</I>, to both <I>jianjing</I>, to <I>shanzhong</I>, to <I>dantian</I>, to <I>huiyin</I>, to <I>yinlingquan</I>, to <I>sanyinjiao</I>, to <I>yongquan</I>, to your big toe, to your second toe, to your third toe, to your fourth toe, and to your small toe.</p>
			<p>Then continue in the same direction around your feet and bring your mind back to your <I>yongquan</I>points. Imagine that you are standing on top of two wells, one foot on each well, with your <I>yongquan</I> points touching the top of the water.</p>
			<p><b>Step 2:</b><BR>
			If it is hard for you to imagine your feet on top of two wells, bring your mind up from your yongquan pointsalong the inside of your legs, up to <I>yinlingquan</I>, to <I>huantiao</I>, to <I>huiyin</I>, to <I>mingmen</I>, to your <I>dantian</I>.  Then follow the previous path down to your <I>huiyin</I>, to <I>yinlingquan</I>, to <I>sanyinjiao</I>, to <I>yongquan</I>, to your big toe, to your second toe, to your third toe, to your fourth toe, and to your small toe.  Then circle along the edges of your feet and bring your mind back to your <I>yongquan</I> points.</p>
			<p>Then imagine that you are standing on top of two wells, one foot on each well, with your <I>yongquan</I> points touching the top of the water.  If you are still unable to focus your mind on this imagery, repeat step 2 a few more times.</p>
			<p>You may remain imagining the coolness of the wells for as long as you like.  This relaxation technique prepares you to practice medical Qigong, though can be used as an easy vocal meditation.</p>
