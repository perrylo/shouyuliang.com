			<h3>Part 1.  Cleansing the Lungs</h3>
			<p>This Qigong technique is used for healing emphysema, asthma, gastroptosis, and a prolapse of the uterus.</p>
			<p align="center"><img src="../../images/newsletter/v1n7/a1p1.jpg" border="0" alt="Lung Qigong"></p>
			<h3>Movement and Intention:</h3>
			<p> Step1.  Stand with your feet shoulder width apart, head and body upright, eyes lookstraightahead, and arms placed naturally at your sides. Then overlap both hands on top of your <i>dantian</i> (Figure 1-6).</p>
			<p> Men, keep your left hand on <i>dantian</i>, with your right hand on top of your left hand. Women, keep your right hand on your <i>dantian</i>, with your left hand on top of your right hand.</p>
			<p> Step 2.  Press gently on your <i>dantian</i> as you exhale and draw your abdomen in.  As you exhale, bend your upper body forward.  Use your mind to imagine that 	you are touching your abdomen toyourback(Figure 1-7).  Then straighten your body and breathe in naturally.  Repeat 3 times.</p>
			<p> Step 3.  From an upright position, lean back lightly as you extend both arms slightly back while inhaling (Figure 1-8).  When extending your arms back, expand your lungs as much as possible, then hold your breath for as long as you can.  Then return to the upright position as you exhale all the impurities from your mouth.</p>
			<h3>Key Points:</h3>
			<ul>
				<li>In Step 2, your intent is placed on exhalation only, inhale naturally.  Physically, the bending movementis directly stimulation the diaphragm to improve its <i>elasticity</i>.  If you have poor balance, you should do this exercise sitting down.</li>
				<li>In Step 3, the abdomen draws in during inhalation, where as, in Step 1, the abdomen draws induring exhalation.</li>
				<li>In Step 3, hold up your <i>huiyin</i> area slightly as you inhale, but do not sink your qi to your <i>dantian</i>.  Push up with your head slightly.  Inhale from your nose and exhale through your mouth.  Physically, this exercise stimulates the lungs to an elevated state.  It trains you to use more of your lungs.</li>
				<li>Don't overextend your body when leaning back.  Only lean to the point that you do not tense up or lose your balance.</li>
			</ul>
