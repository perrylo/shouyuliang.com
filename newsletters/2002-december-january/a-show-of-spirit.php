          <h1 class="contentheading">A Show of Spirit</h1>
          <h2>By Perry Lo</h2>
          <br><br>
          <p>The 2001 North American Wushu/Kung Fu Championships took place on Sunday November 18, 2001 in the beautiful city of Richmond, a close suburb to Vancouver.  Hosted by Sifu Ken Low, the event was a major fundraiser for Wushu BC, an association of British Columbian Chinese martial arts practitioners to promote the sport of Wushu.</p>
          <p>Like so many other competitions, the day started early for the competitors, and even earlier for the administrators.  Registration was scheduled at 8 AM sharp with eliminations starting exactly one hour afterwards.  Though the judges meetings and general protocol took a little longer than expected, the day passed with no major problems.  There were many great performances by the competitors as well great shows of support from their different schools.</p>
          <p>The day concluded with some fabulous shows by the Masters and their advanced students.  The demonstrations, hosted by Gil Tan and Helen Liang, consisted of speeches from the president and vice-president of Wushu BC, special award presentations, and of course, unique martial arts.</p>
