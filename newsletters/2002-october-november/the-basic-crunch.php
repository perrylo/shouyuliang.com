	        <h2>By Ayron Howey</h2>
	        <br><br>
			<p>Strengthening the abdominal area is very important for general health, not to mention for martial arts. I'm not going to delve into all the practical reasons for a strong midsection at this time - every martial artist will have their own.  As an inside tip for those of you out there are already doing sit-ups and have a hard time getting that '6-pack' to show there are two things to remember.  Proper diet is the first key, start by cutting back on sugar, pasta and white bread.  The second key is to use added weight (resistance) with your exercises, as this makes the muscles bigger.  Regardless of appearance, however, we want to focus on increasing your midsection strength.</p>
			<p>We'll start with the basic crunch as it's the easiest on the lower back and if done properly and repeatedly can build up strength rather fast. Remember, consistency is what you are looking for.  The abdominal muscles can be trained every day and you do not need to take a day off to see growth or improvement in strength.</p>
			<br clear="all">
			<img src="../../images/newsletter/v2n6/a5p1.jpg" alt="Ayron Howey - Basic Crunch" align="right">
			<p><b>Step 1)</b><br />Lying flat on your back with your legs outstretched, place your fingertips on the side of your head. If you put your hands behind your head, there is a greater risk of injury, as most people tend to pull with their arms throughout the exercise as they tire.  From this initial position you are going to roll yourself up and forwards looking towards your toes. You should feel the stomach contract as much as possible lifting your shoulder blades and upper back off the floor. The lower back should still be in contact with the floor. From the up position, slowly lower yourself back down to the floor but do not touch the back of your head, instead try to keep your eyes on your toes, maintaining a slight degree of pressure on your abs.</p>
			<br clear="all">
			<img src="../../images/newsletter/v2n6/a5p2.jpg" alt="Ayron Howey - Basic Crunch" align="right">
			<p><b>Step 2)</b><br />Repeat this movement, slowly for 3 sets of ten. Try and keep to a 3 count going up and a 3 count going down.</p>
			<p>With this basic crunch routine you will slowly feel stronger in your abdominal section.  You can then go on to try the following variations.</p>
			<p><b>Step 1)</b><br />Keeping to the 3-count duration per repetition, increase the sets to 5.</p>
			<p><b>Step 2)</b><br />Keeping the 3-count, try to do 2 sets of 50 repetitions.</p>
			<br clear="all">
			<img src="../../images/newsletter/v2n6/a5p3.jpg" alt="Ayron Howey - Basic Crunch" align="right">
			<p><b>Step 3)</b><br />Try decreasing the duration to 1 count and do 3 sets of 25 repetitions.</p>
			<p>For more advanced athletes, try looking straight up instead of at your toes when contracting your muscles, as if trying to touch your chin to the ceiling. This should cause you to lift the upper back off the floor instead of rolling it. Try this at the basic 3 sets of 10 then move on. Combine the 2 methods to vary your workout.</p>
			<p>Good luck and train hard!</p>
