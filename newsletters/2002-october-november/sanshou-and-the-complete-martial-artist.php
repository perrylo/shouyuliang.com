	        <h1 class="contentheading">Sanshou and the Complete Martial Artist</h1>
	        <h2>By Ayron Howey</h2>
	        <br><br>
			<p>I feel that it's important for me to introduce my philosophy behind Sanshou (aka free fighting) in martial arts. In Wushu, free fighting is an important aspect to develop - it can and will improve your understanding of Wushu. Yet, free fighting is not a martial art in itself. It is merely application. On the flip side, if you do not have any free-fighting skills or lapse in its practice, you are also incomplete.  Some seem to see the path of fighting as a one-way street and those that follow it end up with the negative connotation of simply being a 'fighter' rather than a 'martial artist'. This is not unfounded.</p>
			<p>If you base your martial training on simply fighting and leave bare the other branches of developmentsuchas Tai Chi, Qigong, fist forms, and weapons you cannot become a true martial artist. There is simply too much you are missing. To be complete you need all these components and more to reach a level where you can be called a martial artist. Those that stay on the one path they have chosen tend to stagnate and never really begin to understand the vast knowledge and enlightenment that is available through Wushu.</p>
			<p>Being a student of martial arts and a competitive fighter, I feel that it is important to remain humble. Fightingdoes not make one great, nor does it gain you respect as a martial artist. It is one component necessary to continue learning and understanding Wushu.  Too many times arrogance is found hand-in-hand with being a fighter. This is not what being a martial artist is about, and those that become accomplished fighters through advanced skill understand why. A fighter knows better than his opponents his own weaknesses - it is the acceptance of your faults and the desire to improve and learn that one can transcend arrogance and start down the path to becoming a martial artist. </p>
			<p>Likewise, you cannot simply practice Tai Chi forms without other training in push-hands and otherphysicalapplications and call your self a martial artist.  One that exclusively practices Tai Chi forms is no different than one who exclusively practices sparring, they are opposite sides of the same coin and neither adds up to being a complete martial artist. </p>
			<p>If you just do Tai Chi for health or free fighting for a good workout, there is nothing wrong with that and Iam not trying to discourage that in the least.  I am concerning myself with the students wishing to teach and advance and those that wish to one day be called a martial artist, and for that title to respectfully mean something.</p>
			<p>In the pursuit and training of sanshou, I'd have to say the two influences that I feel have made me improve as well as learn more is Hsing Yi (external style) and Dapeng Qiqong (internal style).  Sure, drills help me improve my speed and push-ups strengthen my punches, but the internal routines help my mind and spirit to understand fighting, and that is more important if I am to develop and improve.  Keeping humble, continuing to learn, listening to everything Sifu says (and doesn't say) is what is important to me. It is what I feel will make me a better student, teacher, person, and martial artist.</p>
