	        <br><br>
			<p><img src="../../images/newsletter/v2n6/a2p4.jpg" alt="Qigong - Easing the Liver" align="right">According to Traditional Chinese Medicine (TCM), the liver is not only responsible for storing blood and regulating the distribution of blood, it also controls the function of the tendons.  The bluish discoloration of one's complexion is usually caused by the stagnation and obstruction of blood and qi, indicating the presence of coldness, pain, blood stasis, convulsion or illness of the liver.  "The eyes are the specific opening of the liver."  Normal eyesight relies on the proper functioning of the liver.  "The liver stores one's soul."  Patients with liver diseases tend to have dreadful dreams, restlessness, and irritability.</p>
			<p>Premature whiteningof the hair is also allegedly to be due to insufficient blood stored in the liver and the decreased function of the kidneys.  Since blood is made up of food essence and nutrients produced in the spleen and stomach, combined with the <i>innate-jing</i> stored in the kidneys, this links the liver and kidneys together.  Therefore, the deficiency of either one can affect the other.</p>
			<h3>Part 1.  Easing the Liver</h3>
			<h4>Movements and Intention:</h4>
			<p><b>Step 1.</b><br />Stand (or sit) with your feet shoulder width apart, knees slightly bent, and your handsat your sides.  Eyes gently closed, focus your mind, and regulate your breathing.  Then overlap your palms on top of you <i>dantian</i>.  Inhale from your nose and exhale from your mouth, while letting out the <i>xu</i>[link to sounds article] sound as your mind concentrates on your <i>dantian</i> (Figure 1-21).</p>
			<p align="center"><img src="../../images/newsletter/v2n6/a2p1.jpg" alt="Qigong - Easing the Liver"></p>
			<p><b>Step 2.</b><br />Use Drawing 1-11 as a reference.  Hold your hands as if you were holding a ballin frontof your abdomen (Figure 1-22).  Raise your palms up until they are over your <i>baihui</i> and imagine that pure energy is entering your body from your <i>baihui</i> (Figure 1-23).</p>
			<p>Lower your palms in front of your body, passing your eyes, passing your <i>quepen</i> point on the Stomach Channel, until your <i>qimen</i> point on the Liver Channel (Figure 1-24).<p>
			<p align="center"><img src="../../images/newsletter/v2n6/a2p2.jpg" alt="Qigong - Easing the Liver"></p>
			<p><b>Step 3.</b><br />Next with your palms on top of your <i>qimen</i> points, massage 6 circles in each direction.  Press your palms on top of your <i>qimen</i> points and bend your body from left to right, 4 times on each side (Figure 1-25).</p>
			<p><b>Step 4.</b><br />Slide your palms down to your <i>zhangmen</i> point and massage 6 circles in each direction(Figure 1-26).  Overlap both palms on top of your <i>dantian</i>.  Then bend your body forward and back, 4 times (Figures 1-27 and 1-28).</p>
			<p align="center"><img src="../../images/newsletter/v2n6/a2p3.jpg" alt="Qigong - Easing the Liver"></p>
			<p><b>Step 5.</b><br />Then separate your palms and begin moving down along the inside of your legs, on the Liver Channel. Exhale as you bend your body and lower your palms.  Your palms should pass your <i>ququan</i>, your <i>taichong</i>, and your <i>xingjian</i> points, and end with your middle finger pointing at your <i>dadun</i> point without touching (Figures 1-29 and 1-30).  Stay there for a few seconds, then bend your knees slightly, stand up.</p>
			<h4>Key Points:</h4>
			<ul>
				<li>During training, imagine that your qi is flowing smoothly up and down, your liver is very relaxed, and all discomfort is disappearing.</li>
				<li>Stay away from frightening or infuriating situations, keep away from alcohol, drink a lot ofwater, and balance work with rest.</li>
