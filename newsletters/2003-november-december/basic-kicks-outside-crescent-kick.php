			<h4>Outside Crescent Kick (waibaitui)</h4>
			<p>This is a circular kick with the top of the foot sweeping across sideways.  The kicking leg starts by circling in across the stationary leg, up, across, and down making a complete circle with your foot.</p>
			<p>Drill: Start with your feet together and extend your arms to your sides (Figure 2-11). Take a small step at an angle towards your left with your left foot, turn your foot out about 30 degrees, and shift your weight onto your left leg, and bring your arms up with your palms overlapping over your head (Figure 2-12).</p>
			<p>Swing your right foot next to your left leg, up, and down towards your right in a  clockwise circle. As your leg moves in front of your face, pull your hands in the opposite direction of the kick, hitting the top of your right foot with the inside of both palms (Figure 2-13).</p>
			<div align="center"><img src="../../images/newsletter/v3n6/a6p1.jpg" border="0" width="500" height="200" alt="Basic Kicks - Outside Crescent Kick"></div>
			<p>Complete the kick by placing your right foot down, gently touching the floor with the ball of your foot and your arms back to the starting position (Figure 2-14).</p>
			<p>You may repeat the kick in place 10 to 20 times; then practice kicking with the other leg. You may also alternate kicking with one leg then the next across the training area, by stepping forward with the foot that has just completed the kick.</p>
