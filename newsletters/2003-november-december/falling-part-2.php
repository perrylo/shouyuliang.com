			<h4>3. Falling Sideways</h4>
			<p>Start in a squatting position and fall sideways to your right.  Before your body makes contact with the ground, keep your right arm bent with fingers pointing inward and use the arm to absorb the shock. Keep your chin tucked in, coil your body, and pull up your knees. Land on your right side (Figures 2-65 and 2-66).  Change to the other side and repeat.</p>
			<h4>4. Falling Forward</h4>
			<p><b>A). Kneeling Position</b><br />
			Start in a kneeling position with both arms in front of your body and slightly bent (Figure 2-67).  Keep your head up and start to fall forward. The palms should make contact first, followed by the forearms to absorb the shock of the fall (Figure 2-68).</p>
			<p><b>B). Standing Position</b><br />
			The degree of difficulty in this position is much greater than the Kneeling Position. You may want to start with the Kneeling Position first, then practice from a Squatting Position and then Standing.</p>
