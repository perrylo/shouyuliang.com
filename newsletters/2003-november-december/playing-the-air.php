	        <h2>By Tony Bujas</h2>
	        <br><br>
			<p>Most particularly in our Chen style Taijiquan classes, you can hear Sifu say: "Play The Air" or, "You Need To Be Playing The Air"!  A most interesting remark!!! There is much depth to this statement.  Let's see if I can help you step into the door of exploring this practice and making it your own.</p>
			<p>First, an analogy with water:  If you've ever tried swimming, then you know how the water feels against your bodya nd the resistance feeling it gives you.  (You can also try practicing forms and sparring in varying depths of water for an experience.)  If you haven't been swimming lately or never tried then you can take a bath and play in the water basking in the sensations, or even just half fill your kitchen sink with water and move your hands around in it.  The water is all around your immersed body parts - everywhere! Engulfing you! Surrounding you! Just like air! And, just like Qi!</p>
			<p>Air and Qi surround you constantly.  When it's breezy you become aware of the air.  If an electrical storm is brewing, you become aware of the energy (qi) in the atmosphere.  Now all you need do is tune the awareness to normal everyday conditions.  You can liken it to moving/swimming in water - just the medium is different. Your movement, your form, is able to "Play The Air".  Of course, your basics, posture, principles, etc. still need to be on the ball, be it Taiji, Bagua, or Hsing-Yi.  Your internal qi also gets "played" as your awareness expands.  The atmosphere along your skin, the internal energy along its routes, can cultivate a powerful feeling within you, changing your practice.</p>
			<p>Secondly, a qigong exercise:  This can be done in any position, sitting, standing up, or lying down. Place your tongue on the roof of your mouth. Concentrate on your dantian area - 2 to 3 inches below your navel in the centre of your body.  Initially, breathe naturally, later on if you wish, you can experiment with abdominal breathing. {See what may work best for you, either, 1.) on the inhale you expand your dantian area and on the exhale you allow it to naturally contract (inhale belly goes out, exhale, belly goes in) or, 2.) on the inhale you contract your dantian area and then on the exhale you allow it to naturally expand (inhale belly goes in, exhale, belly goes out)}.</p>
			<p>When you breathe in, visualize the good, positive, pure, Qi of the universe coming into your body through your skin, through all your body's pores, hair, tissue, blood, muscles, bones, organs and collecting in the dantian.  It permeates your whole body and centrally collects and compacts in the dantian elixir field.</p>
			<p>When you exhale, in the reverse order it came in, exchange the collected qi along with your own qi back into the universe.  From the dantian, the energy goes back out through the organs, bones, muscles, blood, tissue, hair, skin, pores and into the universe.</p>
			<p>Repeat.  Gradually, work up to about 20 minutes of this type of breathing/visualization.</p>
			<p>Then go and have fun when you work on "Playing The Air" in your forms practice.</p>
