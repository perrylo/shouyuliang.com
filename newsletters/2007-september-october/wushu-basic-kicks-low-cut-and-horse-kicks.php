			<h3>Low Cut Kick (xia chuai tui)</h3>
			<p>A Low Cut Kick is a kick with the outside edge of the foot at an angle down.</p>
			<p>Drill: Start in a ready position with your left leg forward. Take a step forward with your right foot and turn your foot out (Figure 261). Shift your weight onto your right leg and lift your left leg up (Figure 2-62). Kick forward and down with the outside edge of your left foot (Figure 2-63). Put your left foot down and turn your foot out. Then lift and kick with your right foot. Alternate from side to side across the training area.</p>
			<h3>Horse Kick (jue zi tui)</h3>
			<p>A Horse Kick is a kick with the heel or the bottom of the foot with the body turned sideways and leaning in the opposite direction of the kick.</p>
			<p>Drill: Start in a ready position with your right leg forward (Figure 264). Hop forward and shift your weight back to your left leg and hook your right foot up (Figure 2-65). Stomp your right foot down contacting the floor with the ball of your right foot (Figure 2-66). Turn to face forward as in the starting position, and repeat the kick across the training area. Then do the kick with the other leg.</p>
