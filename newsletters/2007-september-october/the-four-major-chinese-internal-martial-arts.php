			<div class="articleImage"><img src="../../images/newsletter/v7n4/Helen-Liang-Chen-Taiji.jpg" width="300" height="225" border="0" alt="Helen Liang - Chen Style Taiji"><br />Helen Liang - Chen Style Taiji</div>
			<p>Because all Chinese martial styles utilize some Qigong training, it is difficult to distinguish the external styles from the internal. Traditionally, almost all of the Chinese martial styles were taught in secret, and it was not until the last 100 years that these secrets were gradually exposed to the general public. There are many styles that are still taught secretly. Because of this conservatism, most people (including many Chinese martial artists) do not have enough information to distinguish the styles clearly. There are four generally known styles which emphasize Qi development more seriously than other styles, and are therefore considered internal. These four styles are Taiji, Xing Yi Quan, Bagua Zhang, and Liuhe Bafa. We would like to briefly introduce the major differences between these four styles.</p>
			<p>Before we discuss these differences, we would first like to point out the similarities among these four styles. First, they all concentrate on training the circulation of Qi and building it up to a higher level. Second, they all emphasize a calm and peaceful mind. And finally, all four styles are very effective in improving health.</p>
			<ol style="list-style:1">
				<li><b>Taiji Quan</b>:
					<ol type="A">
						<li>In order for the Qi to move freely and smoothly in the physical body, the body must be relaxed from the skin to the bone marrow and the internal organs. In order to lead the Qi to any part of the body without stagnation, in addition to the body being relaxed, the movements must be as soft as a baby's.</li>
						<li>When Jing is emitted for an attack, it is like a whip. Though soft, the power is strong and penetrating.</li>
						<li>The fighting strategy is more defensive than offensive. This means that defense is often treated as the preparation for an attack. Because of this, training focuses on yielding, neutralizing, sticking, adhering, and coiling, and movements are always rounded. "Pushing hands" practice leads the practitioner to this goal.</li>
						<li>Strategy and techniques indicate that Taiji specializes in fighting mostly in the short and middle ranges. Almost all of the kicks trained in the Taiji sequences focus within these ranges.</li>
					</ol>
				</li>
				<li><b>Xing Yi Quan</b>:
					<ol type="A">
						<li>In order to enable the Qi to move freely and smoothly in the physical body, the body must be natural and comfortable. In the beginning of both attacking and defensive movements, the body remains soft so that Qi can be led to the limbs. The body is then stiffened for an instant upon striking to manifest the Jing. Xing Yi Jing is like rattan, soft at the beginning and hard at the end. Jing manifests like a cannonball exploding.</li>
						<li>The fighting strategy is more active than passive. Offensive movement is usually used as a defense. Although techniques such as yielding, neutralizing, sticking, adhering, and coiling are used, the attacking mind and movement remain paramount. In order to keep up momentum, straight forward and backward movements are emphasized, although some dodging and sideward movements are used.</li>
						<li>Because of the strategy and techniques emphasized, Xing Yi can be very effective within the short fighting range. Though some kicks are trained, almost all of them are directed at targets below the groin.</li>
					</ol>
				</li>
				<li><b>Bagua Zhang</b>:
					<ol type="A">
						<li>The movements of Bagua Zhang are not as soft as Taiji Quan, yet they are not as hard as Xing Yi. The internal Qi is the main focus of the training.</li>
						<li>The fighting strategy emphasizes circular movements. Both the stepping and the techniques are circular. Although many techniques such as yielding, neutralizing, sticking, adhering, and coiling are used, they are mainly adopted to coordinate with the round movements. Attack and defense are equally important. Rounded defensive movements are usually used first, followed by rounded attacking movements to uproot the opponent and make him fall.</li>
						<li>Because of its strategy and techniques, Bagua can be effective at all ranges. Because round stepping movements are constantly used in coordination with the techniques, kicks are seldom used. The training focuses instead on firm and rapid walking.</li>
					</ol>
				</li>
				<li><b>Liuhe Bafa</b>:<br />
					Liuhe Bafa is a combination of the strategy and techniques of Taiji, Xing Yi, and Bagua. Therefore, the training contains soft within the hard and hard within the soft. Its strategy contains straight line forward and backward, as well as circular movements. It utilizes all three fighting ranges. It does not emphasize kicking techniques. It is normally taught to people who have already learned the three styles, because they are most likely to be able to understand the essence of the three and mix the techniques skillfully and apply them effectively.</li>
			</ol>
