			<h3>Bagua Zhuan Xuan Gong</h3>
			<br />
			<h3>Spiral and Rotate Posture</h3>
			<p><b>Verbal Secrets: Waist like an axle turning, arm swings around.</b><br />
			In Chinese martial arts, it is said that Jin originates from the legs, is directed by the waist, and manifested by the fingers. In a sense, the waist is like a steering wheel which directs the Jin generated from the legs to the extremities. When this steering wheel is controlled skillfully, you will be centered, balanced, and naturally rooted. From the waist's turning, the arms' swinging movements are generated. For each movement - from the legs, to the waist, and to the arms - the body is acting as one unit.</p>
			<div class="small" align="center"><img src="../../images/newsletter/v7n4/Bagua-Qigong-Spiral-Rotate.jpg" width="600" height="876" border="0" alt="Bagua Turning-Spinning Qigong - Spiral and Rotate Posture"><br />Bagua Turning-Spinning Qigong - Spiral and Rotate Posture</div>
			<p>Movements:</p>
			<p><b>Left Spiral and Rotate:</b><br/>
			Shift your weight to your right leg and step to your left with your left foot, one shoulder width apart; begin to inhale. Next, shift your weight to your left leg, while extending your arms up to your left; palms face up; inhale (Figure 3-19). Shift your weight to your right leg and swing both arms horizontally to your right. The edge of your left palm strikes the Girdle Vessel (Waist). Your eyes focus in the direction of your right hand; complete exhale (Figure 3-20).</p>
			<p>Grip the floor with your feet, distribute your weight evenly between both feet, and turn your body to your left as far as possible. At the same time, your left palm moves along the Girdle Vessel to your back, while maintaining the palm face up. The right palm moves down next to the left side of your waist. Your eyes look in the direction of your left hand; inhale (Figure 3-21). Shift your weight to your left leg and turn your waist to your left, while extending your arms up to your left. The right palm touches your left elbow; exhale (Figure 3-22).</p>
			<p><b>Right spiral and Rotate:</b><br/>
			Shift your weight to your right leg and swing both arms horizontally to your right; inhale (Figure 3-23). Shift your weight to your left leg and swing both arms horizontally to your left. The edge of your right palm strikes the Girdle Vessel (Waist). Your eyes focus in the direction of your right hand; exhale (Figure 3-24).</p>
			<p>Grip the floor with your feet, distribute your weight evenly between both feet, and turn your body to your right as far as possible. At the same time, your right palm moves along the Girdle Vessel to your back, while maintaining the palm face up. The left palm moves down next to the right side of your waist. Your eyes look to your right; inhale (Figure 3-25). Shift your weight to your right leg and turn your waist to your right, while extending your arms up to your right. The left palm touches your right elbow; exhale (Figure 3-26).</p>
			<p>Alternate between the Left and Right Spiral and Rotate Postures, a total of eight times each side. To finish this posture, turn your body until you are facing forward and your weight is evenly distributed on both feet, and extend both arms straight in front of you; inhale (Figure 3-27).</p>
			<p>Then bring your left foot next to your right foot and bring both palms down next to your abdomen; exhale (Figure 3-28).</p>
			<p>Key Notes:</p>
			<p>When you do the posture, keep the movements continuous. Use your waist to swing the arms around. When striking your waist with the edge of your palm, speed up your exhalation, increasing the amount of air exchange in your lungs. During the other movements, breathe evenly and smoothly.</p>
