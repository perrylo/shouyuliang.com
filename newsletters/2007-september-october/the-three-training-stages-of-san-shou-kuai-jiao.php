			<p>The training and development of San Shou Kuai Jiao skills generally goes in three stages. The first stage is learning the basic skills of San Shou Kuai Jiao. The second stage is to master those basic skills. The third stage is to be able to use those skills naturally and creatively in real situations.</p>
			<h3>1. The Fundamental Stage:</h3>
			<p>San Shou Kuai Jiao training must start with basic training for body conditioning. Through basic training you improve your speed, power, confidence and body coordination.</p>
			<p>In this stage of training, you will practice single San Shou Kuai Jiao techniques until they are smooth and accurate. This stage emphasizes techniques, footwork, body coordination and understanding the application of each technique. Also, solo forms and equipment training will improve endurance and power. Proper development of one technique requires long hard training. Above all, the most important element is the development of your Will, Bravery and Alertness.</p>
			<h3>2. Mastering and Specializing in Techniques Stage:</h3>
			<p>According to your own body potential and condition, select the San Shou Kuai Jiao techniques that suit you most. Each individual's body height, weight, shape, strength and condition is different. Therefore, you should choose techniques that suit your own condition and specialize in them. The benefit of selecting favorite techniques is that it boosts enthusiasm and speeds up the learning process and understanding of the techniques.</p>
			<p>At this point you should have a very solid foundation of San Shou Kuai Jiao techniques, body conditioning and other important requirements. Also in this stage, you should further explore the concepts of speed and fighting distance to further improve your attacking and defending abilities. Continue to polish your San Shou Kuai Jiao techniques to make them natural, so that your body will react automatically and correctly. At this stage of training, you will start to form your own specialized San Shou Kuai Jiao techniques and fighting strategy.</p>
			<h3>3. Refining Stage:</h3>
			<p>With the combination of solid basic skills and specialized skills, this is the time to test the abilities you have acquired and apply them in more competitive environments, like sparring and competitions. At this stage you will hone your basic skills and train your ingenuity and fighting strategies. To reach this level requires a strong will, a commitment to hard work, thoughtful analysis, and the clarity of mind to know your goals and see how best to achieve them.</p>
