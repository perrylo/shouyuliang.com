			<p>As the times change so do our training methods.  Oftentimes new trends or methods replace sound training methods that have been used by martial artists of the past. In this article I'll be discussing some traditional training methods that are an excellent way to add a challenge to your regular training.</p>
			<div class="small" align="center"><img src="../../images/newsletter/v7n4/Windlass-Strength-Training.jpg" width="227" height="300" border="0" alt="Tjong Tjhie - Traditional Arm Strength Training - Windlass"><br />Tjong Tjhie - Traditional Arm Strength Training - Windlass</div>
			<h3>The Windlass</h3>
			<p>To increase strength in the arms and forearms.</p>
			<p>Equipment:  A short round stick with a 10-20lb weight attached to it by a rope.</p>
			<ul style="list-style:disc">
				<li>While in a horstance, hold the stick in front of you at shoulder height.  You may want to do on a bench or chair so that the hanging weight will not touch the floor.</li>
				<li>Keeping the proper horsestance and your arms horizontally straight at shoulder height, wind the stick in one direction to raise the weight until it touches the stick.   Wind the stick slowly in the other direction to lower weight.   Repeat as many times as possible while keeping your arms horizontal.</li>
			</ul>
			<div class="small" align="center"><img src="../../images/newsletter/v7n4/Pushing-Bricks-Strength.jpg" width="300" height="225" border="0" alt="Tjong Tjhie - Traditional Arm Strength Training - Pushing Bricks"><br />Tjong Tjhie - Traditional Arm Strength Training - Pushing Bricks</div>
			<h3>Pushing bricks</h3>
			<p>To increase arm and grappling strength.</p>
			<p>Equipment: Two bricks of about 10-15lbs in weight.</p>
			<ul style="list-style:disc">
				<li>While in a horse stance position, hold 1 brick in each hand.</li>
				<li>While keeping the proper horsestance (spine straight and your thighs flat), alternate pushing each brick out until your arms are straight.  Repeat 100 times each arm.</li>
			</ul>
			<div class="small" align="center"><img src="../../images/newsletter/v7n4/One-Arm-Push-Up.jpg" width="300" height="225" border="0" alt="Tjong Tjhie - Traditional Arm Strength Training - One Arm Push-Ups"><br />Tjong Tjhie - Traditional Arm Strength Training - One Arm Push-Ups</div>
			<h3>Elevated one hand pushups</h3>
			<p>To increase arm and chest strength.</p>
			<p>Equipment: A chair or bench.</p>
			<ul style="list-style:disc">
				<li>Go into the one handed pushup position with your feet up on a bench about 1 foot off the ground.  Spread your feet apart for balance.</li>
				<li>Keeping your spine straight during the whole pushup, slowly bend your arm until your chest touches the ground.  Don't let your body rest or lie on the ground.</li>
				<li>Push your arm straight to complete the movement and return to your starting position.</li>
				<li>Repeat 30 times.</li>
			</ul>
