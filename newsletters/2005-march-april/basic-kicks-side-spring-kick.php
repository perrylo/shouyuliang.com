			<h4>Side Spring Kick:</h4>
			<p>This is a kick with the top of the foot or the shin. As the name implies, it is a sideways Spring Kick. Instead of kicking vertically upward as in a Spring Kick, you spring kick sideways towards your target.</p>
			<p>Drill: Start with your left leg forward in a ready position. Turn your left foot out, push off your right foot, lift your right knee up, then spring kick sideways in front of you (Figures 2-32 and 2-33). Put your right leg down and stand in a ready position with your right foot in front. Then do the kick with your left leg. Alternate legs across the length of the training area.</p>
