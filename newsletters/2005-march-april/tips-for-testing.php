			<img src="../../images/newsletter/v5n2/a4p1.jpg" width="170" height="128" border="0" alt="Tips for Testing" style="margin-left:10px" align="right">
			<p>At the SYL Wushu Taiji Qigong Institute, new Wushu students generally begin their form training by learning Liu He Quan (Six Combinations Fist) and Wu Bu Quan (Five Stances Fist) followed by either Yan Qing Quan (Yan Qing Fist) or Kun Wu Jian (Kun Wu Sword).  When students have learned a form and can demonstrate it well, they get tested.  A test consists of performing a form solo in front of Sifu.  If the student passes, they move on to the next form.  Students who do not pass continue working on their form and test again at a later date.</p>
			<p>Here are some of my suggestions for students preparing to test.</p>
			<ol type="1">
				<li><b>Practice a lot.</b> Your training is the foundation for you success. If you want to improve you need to practice regularly.  Try to find some time at home to train and always train hard when you are in the school.  In the club, please don't wait for your instructor to stand beside you before you start to practice.  I admire those students who work hard when instructors are not staring directly at them.</li>
				<li><b>Be patient.</b>  Take the time to learn the form to the best of your abilities.  Many students learn a form and two weeks later they are asking to test.  Your goal should be mastery, especially if you are eager to learn higher-level forms.  Without a solid Wushu foundation it will be difficult to progress to later forms.</li>
				<li><b>Ask questions.</b>  Talk to your instructors if you have any questions about what you are learning.  Ask your classmates what they think.  Ask them to watch you and make suggestions.  Try to get as much out of your practice as possible.</li>
				<li><b>Be gracious.</b> Some people have a hard time listening to criticism.  Then there are other people who don't take compliments very well.  After you've passed your test and your classmates are showering you with congratulations, remember to say "thank you."  If Sifu offers you praise, rather than contradicting him, try to just say "thank you."</li>
				<p>A very important last bit of advice:</p>
				<li><b>Bow to Sifu before you start and after you have finished your form.</b>  Show respect to the man who is your teacher and your instructors' teacher.  Forgetting to do so can result in a fail.</li>
			</ol>
			<p>As far as the test itself, do your best and believe in yourself.  With enough practice your movements should feel natural no matter how tense you might feel inside.  Best of luck.</p>
