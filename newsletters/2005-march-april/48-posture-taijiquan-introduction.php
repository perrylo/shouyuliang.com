			<p>In 1976, another standardized Taijiquan (Tai Chi Chuan) sequence was compiled by the Chinese National Athletic Association. The new sequence was called 48 Posture Taijiquan. It broadened the content of the Simplified Taijiquan sequence by adding additional postures from the Yang, Chen, Wu, and Sun styles of Taijiquan. The new postures were modified and performed with the characteristic "flavor" of the Yang Style. Therefore, the same guidelines and principles of Yang Style Taijiquan applies to the 48 Posture Taijiquan. The only exception is that the Simplified Taijiquan sequence is performed at an even pace, and the 48 Posture Taijiquan includes some fast movements. The fast movements are a characteristic of martial arts power emission called Fajin. These fast transitional movements are placed within * *, followed by the phrase, (*Quick Tempo*), placed in parentheses at the end of the description.</p>
