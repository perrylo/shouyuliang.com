	        <br><br>
			<p>Are you learning a Martial Art currently? Are you really? Here is where I might make some enemies.</p>
			<p>If you are not at the very least learning applications to your martial arts practice, then you might as well be doing modern dance, or interpretive ballet. Most people miss out on the 'martial ' aspect and use some flimsy excuse about them just doing martial arts for health or recreation. This is more of the salad bar mentality, a Westernized approach to picking and choosing what lessons we want to learn from our teachers. Grandmaster Shou-Yu Liang teaches applications for everything, he is known for it, yet some people shy away from contact.</p>
			<p>I should clarify that contact doesn't have to mean putting on gear and fighting full contact. It could be push hands, practicing techniques, hitting the heavy bag (am I the only one who uses it?), hitting the pads etc. If you are a student and have been learning martial arts for years and haven't trained in application you are missing the point. You are learning a MARTIAL art.</p>
			<p>I have met a lot of people over the years, teachers and students, and there are those out there who do not train martially. Without being contemptuous of this, I need to simply ask why?  Why don't you train martially? Even a little bit? If you meet someone and they have just started a martial arts class and you tell them you have been learning for 3 or 5 years and they ask you to show them an application, or even spar and you can't, isn't this embarrassing? I'm embarrassed for you.</p>
			<p>My teacher told me years ago to learn Tai Chi. I told him I didn't want to because it was slow and boring. But really I lacked the patience as it was difficult in a way I wasn't used to. Now I practice a few times tai chi every week and have found it useful with many benefits. What I really learned was to listen to my teacher. He teaches Tai Chi and says it will be good for me, so I do it. He says Xing Yi will make me a better fighter, so I do it. He says Da Peng Qigong will make me strong, so I do it. He says a true martial artist doesn't just do forms, but applications too, so I do it. If you don't, why don't you?</p>
