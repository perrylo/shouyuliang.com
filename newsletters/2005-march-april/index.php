				<p><b style="font-size:14px;">"Listen to Many Sides and You Will Be Enlightened; Heed Only a Few and You Will Be Benighted"</b></p>
				<p>Wei Zheng was a minister during the reign of Emperor Taizong of the Tang Dynasty.  He was a learned man, who dared to speak out and point out where the Emperor was wrong.  He therefore enjoyed very high prestige at court.</p>
				<p>"What makes the ruler of a country wise and what makes him foolish?" Emperor Taizong once asked him.</p>
				<p>"Listen to many sides and you will be enlightened; heed only a few and you will be benighted," answered Wei Zheng.  He named a few enlightened emperors in history as examples. Wei Zheng also wrote "Advice to the Emperor to Think About Ten Things", reminding Taizong to examine himself, work conscientiously and concern himself with the hardships of the people.</p>
				<p>Later, when Wei Zheng died, the Emperor was very sad.  With emotion, he said, "Wei Zheng was like a mirror.  I could see my success and failure in it.  Now Wei Zheng is dead, I can no longer see myself in the mirror."</p>
