			<p>Figure 5-68: Bring your left palm next to your body and lift your right palm up to your eye level with your elbow bent.</p>
			<p>Figure 5-69: Take a small step to your left with your left foot and shift most of your weight on it. Right foot follows up with a small step, touching down on the ball of your foot.</p>
			<p>At the same time, thrust your right palm forward and bring your left palm next to your waist. (Step Northeast)</p>
			<p>Figure 5-70: Turn to your back, by pivoting on the ball of your right foot and the heel of your left foot. At the same time, bring your right palm next to your body and lift your left palm up to eye level with your elbow bent.</p>
			<p>Figure 5-71: Take a small step forward with your right leg and shift most of your weight on it. Left foot follows up with a small step, touching down on the ball of your foot. At the same time, thrust your left palm forward and bring your right palm next to your waist. (Step Southwest)</p>
