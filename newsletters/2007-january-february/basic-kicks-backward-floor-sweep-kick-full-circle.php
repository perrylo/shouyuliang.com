			<p><b>Drill:</b> Start in a left Bow Stance and push forward with both palms (Figure 2-50). Squat down on your left leg and place your hands on the floor and sweep your right leg 360 degrees around (Figures 2-51 to 2-53). Stand up, hook your right hand back and extend your left hand forward (Figure 2-54). Bring your back foot forward next to your front foot, stand up, and bring your fists next to your waist. Repeat the kick across the training area. Then do the kick with the other leg.</p>
