			<h3>1. Preparation Posture (Yu Bei Shi)</h3>
			<p><b>Verbal Secrets:</b><br />
			Calm down (your) emotional mind (Xin) and harmonize your Qi to lock (i.e, control) the monkey heart (emotional mind).</p>
			<p>The first step of Baguazhang Qigong, like many other Qigong practices, is to calm down your emotional mind. When your emotional mind is calm, your Qi can be peaceful and harmonized. The purpose of this training is to learn how to use your wisdom mind (Y) to control or govern the emotional mind (Xin). In Chinese Qigong society, the emotional mind (Xin) is commonly compared to a monkey, since it is always jumping around and hard to calm down. Conversely, the wisdom mind (Y) is compared to a horse, since the horse can be calm and its power can be strong and enduring.</p>
			<p><b>Movements:</b><br />
			Stand upright with your feet together, and place the arms naturally at your sides (Figure 3-1). Next, shift your weight to your right leg, and step your left leg to your left about a shoulder's width apart.</p>
			<p><b>Key Notes:</b><br />
			Relax your entire body. Breathe naturally. Concentrate your mind on training and avoid scattering thoughts.</p>
			<h3>2. Nourish Elixir Posture (Yang Dan Shi)</h3>
			<p><b>Verbal Secrets:</b><br />
			Sink the top and gather the internal (Qi), breathe slenderly and smoothly.</p>
			<p>After you calm down your mind, you should then coordinate it with your breathing, and lead the Qi from the top of your head down to the Lower Dan Tian. You should breathe naturally, slenderly, and smoothly. Only when your entire body is be relaxed and natural can the Qi sink easily to the Lower Dan Tian. Once you lead your Qi to the Lower Dan Tian, you can condense it in your center</p>
			<p><b>Movements:</b><br />
			Move your left foot to your left, a shoulder's width apart from your right. Raise both hands up to waist level, fingers pointing down at an angle and palms facing each other. Relax your shoulders, drop your elbows, settle your wrists, and hold your hands in an embrace, fingertips about one foot apart. At the same time, arc your chest in slightly to relax it further. Hold this posture and do 16 deep abdominal breathing cycles. (Figure 3-2).</p>
			<p><b>Key Notes:</b><br />
			Your head should have a slightly suspended feeling. When "embracing" with your hands, shift your weight back slightly. Position your knees directly over your Bubbling Well (Yongquan) cavity on the bottom of your feet, and grip the floor with your toes (Figure 3-3). Breathe slowly, evenly, and deeply. During inhalation, bring your attention to your Lower Dan Tian, and lead all the Qi from your extremities to your center of gravity - the Lower Dan Tian area. During exhalation, lead your Qi out to the extremities, loosen and expand each joint in your arm and shoulder, and use your mind to lead the Qi from the Lower Dan Tian upward, following the spine to the shoulders, elbows, and finally your fingers. Simultaneously perform a similar expansion through your legs. When you exhale, loosen and expand each joint, leading your Qi from your hips to your knees, ankles, and toes.</p>
