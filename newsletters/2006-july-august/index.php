					<p>When one has a great purpose, he will be ready to go anywhere, even the remotest corners of the earth.</p>
					<p>WHEN Zi Gao of the Warring States Period was touring the state of Zhao, he made the acquaintance of Zou Wen and Ji Jie. On the day he was to leave for the state of Lu, many friends came to bid him farewell. Zou Wen and Ji Jie were so reluctant to part with Zi Gao that they traveled with him for three days before they said good-bye to their friend, tears welling up in their eyes. But Zi Gao left determinedly after waving to them and making a gesture of thanks with both hands clasped before his chest.  Zi Gao's attendants asked their master why he had acted like that. "At first, I thought Zou Wen and Ji Jie were all men with lofty ideals. But now I can say that they are both of them mediocre, weak-willed persons. One should have high aspirations, ready to go wherever he is needed. How can one afford to be like a mountain deer that feels content only when it stays with the other animals of the herd?"</p>
