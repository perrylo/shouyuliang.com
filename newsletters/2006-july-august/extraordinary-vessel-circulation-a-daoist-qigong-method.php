			<p>After achieving proficiency in basic Daoist (Taoist) Qigong, Reverse Breathing, Microcosmic Circulation and Macrocosmic Circulation our Daoist Level II Qigong Class proceeds on to the Exraordinary Vessel Circulation.  Documented here is the procedure for the 5 Inhale/Exhale Breathing Cycle.  You repeat steps 1 to 5 - one cycle, for as many cycles as comfortable, working up to 20 minutes and longer.</p>
			<ol type="1">
				<li>On the Inhale, visualize & draw the qi from the HuiYin up the Governing Vessel to the BaiHui. On the Exhale, visualize & direct the qi from the BaiHui down the Conception Vessel to the HuiYin.</li>
				<li>On the Inhale, visualize & draw the qi from the HuiYin up the Governing Vessel where it splits into two paths at the Dorsal Gate to the JianJing, the left and right Shoulder Well points. On the Exhale, visualize & direct the qi from the JianJing, Shoulder Well points, down the outside of your arms to the tips of your fingers, specifically the ZhongChong points of your middle fingers.</li>
				<li>On the Inhale, visualize & draw the qi from the ZhongChong points of your middle fingers along the inside of your arms to the center of your chest, the ShanZhong point (midpoint between the nipples). On the Exhale, visualize & direct the qi from the center chest ShanZhong point down the Conception Vessel to the HuiYin.</li>
				<li>On the Inhale, visualize & draw the qi from the HuiYin back up along the Conception Vessel to the ShanZhong center chest point. On the Exhale, visualize & direct the qi from the ShanZhong down the Conception Vessel to the HuiYin and down the inside of the legs to the center of each foot's YongQuan (bubbling well) points.</li>
				<li>On the Inhale, visualize & draw the qi from the YongQuan points along the outside of the legs, up to the HuiYin, and up the spine along the Governing Vessel to the BaiHui. On the Exhale, visualize & direct the qi from the BaiHui down the Conception Vessel to the HuiYin.</li>
			</ol>
			<p>These 5 inhale/exhale breaths constitute 1 cycle. Repeat.</p>
