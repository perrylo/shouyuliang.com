			<p><img src="../../images/newsletter/v4n6/a4p2.jpg" width="200" height="134" border="0" alt="Helen Liang- Wushu EmeiSnake" align="left" style="margin-top:5px; margin-right:10px;">The philosophy of harmony incorporates an elegant artistic expression, as well as, proper body mechanics. The rhythm of Wushu movements requires that to extend, one must first condense. To go left, first go right. To open, first close. To rise, first sink. To tighten, first loosen. This method of attaining the objective by first beginning with the opposite can be traced back to the philosophy of Laozi. Wushu movements also require that hard and soft compliment each other; offensive implies defense; and defense implies offense. This understanding of the opposing, yet interdependent, aspects are adapted from the philosophy and harmonious integration of yin-yang Proper body mechanics are necessary to utilize the body's potential. In Wushu, natural scenery and animals are often used to describe movements and proper body mechanics. The Twelve Models describe the postures and movements as, "Move like a tidal wave, still as a great mountain, jump like an ape, land like a magpie, balance like a rooster, stand like a pine tree, spin like a wheel, bend like a bow, light as a leaf, heavy as iron, suspend like an eagle, and.fast like the wind."</p>
			<p>"Move like a tidal wave" describes the powerful, rhythmic, and controlled force of your movement.</p>
			<p>"Still as a great mountain" describes the strength of a still posture with an immovable foundation like thatof agreat mountain; and expresses a majestic presence.</p>
			<p>"Jump like an ape" describes the alertness and agility of your movements, like that of an ape ina movement involving jumping.</p>
			<P>"Land like a magpie"	describes the lightness and stability of a magpie when landing on a tree branch.</p>
			<p>"Balance like a rooster"	describes a one legged standing posture that moves, then stops; similarto the stillnessand alertness of a rooster in motion, then coming to an abrupt stop.</p>
			<p>"Stand like a pine tree"	describes a standing posture with both feet on the floor. Even though you are standing still, your posture emanates the strength of a pine tree.</p>
			<p>"Spin like a wheel" describes a rotational movement, like that of a spinning car wheel with the rotationaxlewelldefined and controlled.</p>
			<p>"Bend like a bow" describes the storage of potential power in a bending or twisting posture, resembling a strungbow ready to release an arrow.</p>
			<p>"Light as a leaf" describes the lightness of the body gliding effortlessly from one location to the next.</p>
			<p>"Heavy as iron" describes an expression of seriousness, but not vicious and out of control.</p>
			<p>"Suspend like an eagle" describes an animated state like that of an eagle flying and searching for food on the ground. It is alert and concentrated, not simply moving aimlessly.</p>
			<p>"Fast like the wind"	describes fast, sharp, and swiftly accomplished movements.</p>
