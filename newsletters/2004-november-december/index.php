				<p style="font-size:13px; font-weight:bold; font-style:italic;"><b style="font-size:16px;">"The Wisdom of Knowing Oneself"</b><br>Used to describe a person who has a correct appraisal of himself.</p>
				<p><b style="font-size: 15px">Z</b>ou Ji was a senior official and a gentleman in the state of Qi during the Warring States period.  One morning he dressed and studied himself in the mirror.  "Who is more handsome," he asked his wife.  "Lord Xu or me?"  "You of course," replied his wife.</p>
				<p>Knowing that Xu was considered the most handsome man in the state, Zou Ji could not quite believe his wife.  So, he asked his concubine.  "How can he compare with you," she answered.</p>
				<p>The next day a friend came to see him, and he asked his friend.  "Would you tell me who is more handsome?  Lord Xu or I?"  "Xu is not nearly as handsome as you," his friend replied.</p>
				<p>On the third day, Lord Xu himself paid Zou Ji a visit.  Comparing his looks to Xu's Zou Ji concluded that he was the plainer of the two.  "The fact is that I am not as handsome as Lord Xu," thought Zou Ji afterwards.  "But my wife, my concubine, and my friend all say that I am more handsome than Lord Xu.  This is because my wife is biased, my concubine dares not offend, and my friend wants me to do him a favor."</p>
