					<h2>By Howie Leung</h2>
					<br><br>
					<p><i><b>Wushu Sanshou</b></i> is set to burst out of the blocks to become part of the 2008 Summer Olympics,especially now that Beijing has been selected as the hosting city.  But what is it? Wushu Sanshou is the practical applications of Wushu (Chinese Martial Arts).  It incorporates the four main categories of fighting: <i><b>Ti</b></i> (kicking), <i><b>Da</b></i> (striking), <i><b>Shuai</b></i> (wrestling), and <i><b>Na</b></i> (grappling).  In Wushu Sanshou fighters develop the ability to defend themselves at both short range and far range attacks in a real combat situation.  However, what sets this martial art apart from others is that individual technique is based upon the applications hidden within the beauty of traditional Wushu forms a fighter knows.  In essence, Wushu Sanshou creates the complete martial artist: agile, powerful, and balanced in forms, yet devastatingly accurate with his/her applications.</p>
