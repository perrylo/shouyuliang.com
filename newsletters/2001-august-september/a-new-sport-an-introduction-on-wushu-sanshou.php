					<h4>So What Are the Rules?</h4>
					<p>Wushu Sanshou is fought on a raised platform called a <i>leitai</i>.  The participants try to score points through the first three categories of form: striking, kicking, wrestling.  The fourth category of shuai is not used for two reasons.  First, the dangers of joint dislocation, tendon shearing, and ligament damage are very common if applied in competition.  Secondly, participants are required to wear closed gloves, preventing the use of fingers during the match. Matches are fought in rounds of two minutes, with the maximum of three rounds being fought in each bout.</p>
					<p>As it stands, Wushu Sanshou may become the next most popular combat sport upon entry to the internation Olympics.Whether training for self-defense or for competition, it will definitely offer another means for martial artist to prove their ability and gain honor for their schools.</p>
