	        <h2>By Howie Leung</h2>
	        <br><br>
	        <p>Wushu is the Mandarin translation for Chinese martial arts, or popularly and improperly known as Kung Fu. But to make things easier to understand I'll refer to Wushu as the fast moving external martial art.  Wushu is set to become part of the summer Olympics in 2008 and has become increasingly popular due to its impressive acrobatic techniques and graceful beauty.  It has also taken a further step in the western world due to action stars such as Jacky Chan and Jet Li bringing their talents to American movies.</p>
			<p>The beginner and intermediate Wushu classes are usually catered to children and to young adults.  That is not to say I've never taught older students before.  If you've maintained somewhat of an active lifestyle then Wushu, is just as easy as any other sport.  Wushu classes emphasize a lot of stretching and coordination exercises.  Ideally classes should be at least one and a half-hour long.  The reason why an hour wouldn't be sufficient is due to the fact that a good twenty minutes of every class is dedicated towards stretching.  The remainder of the class would be focused on kicks, forms and self-defense applications.</p>
			<p><b>Benefits of learning Wushu</b><br />
			Wushu is a very well balanced martial art because it promotes good health, both aerobically and mentally.  All practitioners learn to coordinate the body and its limbs, increase their spiritual energy, and build muscular strength and flexibility.  For children, Wushu is very interesting because of its uniqueness, but more importantly  it teaches discipline through hard work.  Adults on the other hand, will notice that everyday chores become easier because of the exercises used to promote muscular strength, coordination, and flexibility.  Wushu is also very well rounded, meaning it doesn't overemphasize one aspect over another.   For example, Wushu philosophy doesn't believe that the kick is more powerful then the punch, but together they compliment each other and make a martial artist well rounded.  Learning Wushu will give you the ability to defend yourself with combinations of punches, kicks, throws, and grappling techniques, depending on the situation.</p>
			<p>Furthermore, Wushu is truly a work of art in that many of its forms were created from observing animals such as eagles, bears, tigers, cranes, and the preying mantis.  Historically, Wushu is the oldest martial art; many martial arts such as Karate, Judo, and Tae Kwon Do all originated from China at some point in history.  Learning Wushu will fundamentally give you a superior foundation (mentally, physically, and spiritually) for learning other fighting techniques.</p>
