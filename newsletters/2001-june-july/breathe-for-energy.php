		    <p><i>Inside-Dantian:</i><br>
		    The inside-dantian or elixir field is located 4 finger widths below the navel, inside your belly. First, think of it as a point centrally located, equidistant from front to back as well as side to side. Now, realize that it has life, expanding and contracting to your needs and to your intent (mind).</p>
		    <p><b>Basic Method:</b><br>
		    Breathe naturally.  Relax, let go of all tension, just release. Place the tip of your tongue on the roof of your mouth at the upper front teeth where they join the palette.  Visualize the "best-bright-healing-good-pure-positive" Qi energy of the universe.  As you inhale, draw this powerful cleansing Qi into your inside-dantian and store it there. Keep the energy in your dantian.  When you exhale, just let the breath go as you still keep the Qi inside your dantian.  Repeat for 5 to 20 minutes or more.  Just keep 'growing' the dantian Qi.</p>
		    <p>You might feel the dantian Qi changing in size from a pea to a golf ball, to a softball, to a basketball, or even expand to a huge size. Or you may sense the area getting brighter, denser, heavier, healthier, warmer, purer, etc.  Accept whatever naturally comes up for you as correct.  The next time you try this Qigong the sensations may change/evolve and are also correct for you. If initially you're not feeling any sensations in your dantian region, use your mind to form a concept that appeals to you and develop it. For example, on the inhale it's raining Qi into my dantian well and filling up the reservoir. Whatever is comfortable for you.  It's okay to personalize - you have permission.</p>
		    <p><b>Advanced Methods:</b><br>
		    After getting used to the above give this variation a try. On the inhale, visualize the 'best quality' Qi of the universe coming in through your whole body & gathering into your dantian.  As you're breathing in, visualize the Qi not only coming in with your breath but also being drawn in through your skin, your pores, hair follicles, cells, organs, blood, bone marrow, etc. energizing & healing your whole system and then storing itself in your dantian.  When you exhale, still keep the Qi stored in your dantian. Repeat for 5 to 20 minutes or more.</p>
		    <p><i>Variation 1:</i>  When you exhale, send the Qi from your dantian to your whole body and store it internally throughout, from tip to toe, including your body's outside surface area.</p>
		    <p><i>Variation 2:</i> When you exhale, send the Qi from your dantian to any area of your body you may be having problems with and store it there.  Visualize the Qi healing this area.</p>
		    <p><i>Variation 3:</i> When you exhale, store only good Qi in your body system and visualize all the negative, unhealthy, impurities leaving your body/mind/heart and staying away forever.  It's sort of like breathing with the universe - inhale the positive, exhale the negative.</p>
