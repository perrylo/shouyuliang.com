	        <br><br>
			<p>This is a lesson that I try to impart on all classes where the students are going to be sparring or having any physical contact.  It comes down to two criteria to keep in mind for beginners - respect and common sense.  Now, these seem like two things that are fairly easy to understand, yet every class there is someone who doesn't follow these two simple criteria. Of the many various reasons the most dangerous is arrogance.</p>
			<p>When I talk about arrogance in learning I'm talking about entering a lesson with a less than open mind. Thinking that you know about a technique or lesson better than your teacher can be dangerous to yourself and other students in the class. This is a quiet arrogance that usually comes out when you do the technique and you have changed it according to your beliefs.  Keep in mind the adage about 'emptying your cup when you attend a lesson' (See the book Zen Flesh, Zen Bones by J. Hyams).  Respect for your teacher and your classmates means emptying your cup, and opening your mind to the lesson at hand.</p>
			<p>Respect is realizing that Chinese Wushu has been around for a very long time - the methods learned and created by the ancient masters have been passed down to your master, and your master is imparting them to you.  Some students have the arrogance to change this because they feel they know better or that their way feels better. This is utter nonsense. I have been training since I was seven years old and when Sifu shows me something now I treat it like precious gold, to be cherished and stored in a safe place. I don't say, "hmmmm, This would be better if I did it this way."</p>
