			<h3>Six Harmony Routine <i>(Liu He Quan)</i></h3>
			<h4>Salutation</h4>
			<p>Start with your feet together with your hands naturally down at your sides (figure 1-118).  Bringyourhands together in a standard Wushu greeting (Figures 1-119 and 1-119a, front view).</p>
			<p align="center"><img src="../../images/newsletter/v3n2/a5p1.jpg" width="500" height="268" alt="Six (Combinations) Harmony Routine - Liu He Quan)"></p>
			<h4>Routine</h4>
			<p><b>Step 1</b><br />
			Bend your knees slightly.  At the same time, move your hands apart in a clockwise pattern, left handin frontand right hand circles down, up, back, and down to your waist (Figures 1-120 and 1-121).  Step forward with your left foot and follow up with your right foot until both feet are together.  At the same time, punch forward with your right fist and pull your left hand back next to your right forearm (Figures 1-122 and 1-123).</p>
			<p align="center"><img src="../../images/newsletter/v3n2/a5p2.jpg" width="500" height="531" alt="Six (Combinations) Harmony Routine - Liu He Quan)"></p>
			<p><b>Step 2</b><br />
			Step forward with your left foot into a left Bow Stance.  At the same time, bring your right fist back to your waist, and extend your left hand forward with your fingers pointing down and your palm facing to your right (Figure 1-124).  Rotate your left palm to face down and strike upward with your right fist under your left palm (Figure 1-125).</p>
			<p><b>Step 3</b><br />
			Turn your body 180 degrees around into a right Bow Stance.  As you turn, hit the inside of your right thigh withyourright hand and hit the inside of your left thigh with your left hand (Figure 1-126).  Quickly turn your body 180 degrees around again into a left Bow Stance.  At the same time, raise your right fist and hammer down in front of you into your left hand (Figures 1-127 and 1-128).</p>
			<p align="center"><img src="../../images/newsletter/v3n2/a5p3.jpg" width="500" height="529" alt="Six (Combinations) Harmony Routine - Liu He Quan)"></p>
			<p><b>Step 4</b><br />
			Shift your weight back to your right leg and circular step with your left leg to your right.  At thesametime, extendyour left forward and raise your right fist up (Figures 1-129 and 130).</p>
			<p>Step around with your right leg into a right Bow Stance.  At the same time, hammer down with yourright fist into your left hand (Figure 1-131).  Quickly, punch with your left fist over your right fist(Figure 1-132).  Backfist with your right fist and extend your left fist back(Figure 1-133).</p>
			<p align="center"><img src="../../images/newsletter/v3n2/a5p4.jpg" width="500" height="524" alt="Six (Combinations) Harmony Routine - Liu He Quan)"></p>
			<p><b>Step 5</b><br />
			To the mirror image of Step 4 by shifting your weight back to the left leg and circular Step 4 by shifting your weight back to your left leg and circular step with your right leg to your left.  At the same time, extend your right hand forward and raise your left fist up (Figures 1-134 and 135).</p>
			<p>Step around with your left leg into a left Bow Stance.  At the same time, hammer down with your left fistintoyourright hand (Figure 1-136).  Quickly, punch with your right fist over your left fist (Figure 1-137).  Back fist with your left fist and extend your right fist back (Figure 1-138).</p>
			<p align="center"><img src="../../images/newsletter/v3n2/a5p5.jpg" width="500" height="530" alt="Six (Combinations) Harmony Routine - Liu He Quan)"></p>
			<p><b>Step 6</b><br />
			Shift your weight back to your right leg and circular step with your left leg to your right.  At thesametime, extendyour left hand forward and raise your right fist up (Figures 1-139 and 1-140).</p>
			<p>Step around with your right leg and hammer down with your right fist (Figure 1-141).  Continue the circular momentum of the previous movement and spin your body around on your right foot.  As you turn, backfist with your left fist and raise your left keen up (Figure 1-142).</p>
			<p><b>Step 7</b><br />
			Step down and forward with your left foot and bring your right foot next to your left.  At the same time, pull your hands down to your left (Figure 1-143).  Step forward with your right leg into a right Bow Stance.  At the same time, bring your hands back to your waist then push forward (Figures 1-144 and 1-145).</p>
			<p align="center"><img src="../../images/newsletter/v3n2/a5p6.jpg" width="500" height="525" alt="Six (Combinations) Harmony Routine - Liu He Quan)"></p>
			<p><b>Step 8</b><br />
			Turn your body 180 degrees around into a left Bow Stance.  At the same time, bring your right fist back to your waist, and extend your left hand forward with your fingers pointing down and your palm facing to your right (Figure 1-146).</p>
			<p>Repeat Steps 2 through 8.  This will bring you back to the starting position facing the same direction aswhenyou started (Figure 1-147).  You can repeat as many times as you wish or finish.</p>
			<p><b>Step 9</b><br />
			When you are ready to finish, rotate your left palm to face down and strike upward with your right fist under your left palm (Figure 1-148).  Turn your body 180 degrees around into a right Bow Stance.  As you turn, hit the inside of your right thigh with your right hand and hit the inside of your left thigh with your left hand (Figure 1-149).  Quickly turn your body 180 degrees around again into a left Bow Stance.  At the same time, raise your right fist and hammer down in front of you into your left hand (Figures 1-150 and 1-151).</p>
			<p align="center"><img src="../../images/newsletter/v3n2/a5p7.jpg" width="500" height="525" alt="Six (Combinations) Harmony Routine - Liu He Quan)"></p>
			<p><b>Step 10</b><br />
			Shift you weight back slightly to your right.  At the same time, raise your right hand up and down to your waist, and extend your left hand forward (Figures 1-152 and 1-153).  Bring your right foot forward and stomp down next to your left foot.  At the same time, punch forward with your right fist, and pull your left hand back next to your right shoulder (Figure 1-154).</p>
			<p align="center"><img src="../../images/newsletter/v3n2/a5p8.jpg" width="500" height="525" alt="Six (Combinations) Harmony Routine - Liu He Quan)"></p>
			<p><b>Step 11</b><br />
			Step back with your right foot, then bring your left foot next to your right foot.  At the same time, lower bothhands, then bring them together into a standard Wushu greeting (Figures 1-155 and 1-156).</p>
			<p><b>Step 12</b><br />
			Lower both hands down to your sides (Figure 1-157).</p>
