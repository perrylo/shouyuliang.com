	        <h2>By Tony Bujas</h2>
	        <br><br>
			<p>Many times we get e-mail inquiries asking "How Do I Begin Qigong Training?" While there is no real substitute for a qualified instructor, resource material is available.  A wonderful reference that I refer to in this article is Grandmaster Liang's book "Qigong Empowerment", by Master Shou-Yu Liang and Wen-Ching Wu for great explanations and good guidance.  (I also recommend the companion videotape on Qigong entitled "Health Maintenance Qigong").</p>
			<p>First you may want to consider getting an expert medical opinion on your health - just to rule out any and all possibilities.  Also, you may want to consider getting a check up by a qualified acupuncturist and/or herbalist if possible.  Qi blockages and over stimulation of one or two organs do occur and need a helping hand to improve. You may have a blocked point/channel; that with a needle or herb treatment can get cleared up and restored to balance.</p>
			<p>Start Qigong training from step one - this is a good thing!  Many times people jump into Qigong without building a foundation and then get into trouble later on.  Having a good foundation, the basics so to speak, means you can come back and do them when needed - and there are times when that is required.</p>
			<p>Keep your focus simple - to get your health back on track, open up your channels, strengthen your organs and then progress from there. The 3 key items to do daily work on are (from the book):</p>
			<ol type="1">
				<li>Qi Permeating Technique (pg 36 to 40),</li>
				<li>Basic Relaxation Technique (pg 32 to 35),</li>
				<li>Organ (Health Maintenance Qigong) - start with the Kidneys first (pg 47), then Lungs, Liver, etc.</li>
				<li>"Unification of the Cosmos" (175 to 188, not further)</li>
			</ol>
			<p>Generally, caring for the Kidneys is a primary importance for many people who are weak and in poor health, that is why we also recommend doing the practice known as "Plugging The Leak", see pages 108 & 109.  (The Lying Down & Walking Qigong I leave optional - do, if you have time or if you have major health concerns).</p>
			<p>Work on items 1 to 3 for at least two to three months before adding the item 4.  When you are ready to include item 4, practice routinely for 6 months.</p>
			<p>You may want to keep a journal of your experiences, noting the myriad Qigong feelings and emotions that may come up. Many different sensations can be experienced when practicing Qigong, it is best not to dwell on any one aspect, just make note of them and let them flow by, no need to act on them - just observe and let go.  Everybody is unique; experiences can vary greatly and do change as progress is made.  Be natural.  Know and trust, that your inner self will have valid sensations.</p>
			<p>It is best to practice consistently, daily if possible, for best Qigong development.  Remember, the onus is on YOU to DO!  Go for it!  Don't rush - it's a lifelong pursuit, take your time and enjoy.  It is a life art you do - integrate it into your life.  Be patient and keep diligent!  The benefits will come.  Make Qigong part of your healthy lifestyle.</p>
			<p>GuanQiFa cleansing/permeating Qigong helps remove bad Qi from the body and replenishes good Qi.  Relaxation (Fangsong Gong) Qigong helps you release tension and get in touch with your body and it's systems.  Medical Qigong is good for the organs & meridians of the body and increasing Qi. Unifying yourself with the universe helps you to let go and allows natural balancing to occur, this Qigong can effect the emotions quite strongly in some cases.</p>
			<p>Meditation can help quiet the mind, allow insights, and work with the spirit.  Remember, you're training your whole body\mind\emotion\spirit system(s).  A balance throughout is trying to be achieved.  Feedback from your body is what you listen to.  If it tells you some internal practice is not working at this time then let it go and perhaps later you may be able to come back to it - no rush.</p>
			<p>I offer the following piece of wisdom for your contemplation, again from "Qigong Empowerment":</p>
			<p>"Some methods enable you to achieve extraordinary abilities, but don't search for them.  Allow your ability to develop naturally.  Everyone's foundation is different and everyone's development will be different.  Extraordinary abilities should not be the goal of your training.  When you have attained Great Wisdom, you will have it all."</p>
			<p>Keep an open mind and practice Qigong daily - it's definitely worth doing!</p>
