			<p>These are so named due to the hand positioning when performing the exercise. Your open palms cometogethertouchingforefingers and thumbs on the side, palms outward so that a diamond shape can be seen in-between your hands.  Now assume the starting position for push-ups on your hands and knees but instead of placing your hands shoulder width apart, place them together in the diamond pattern and position them in the center of your body on the floor. Lift your self up into a push-up, up position extending your arms and on your toes. Now lower yourself to the floor touching the center of your chest or sternum to the diamond shape between your hands. Keep your head looking straight ahead, or touch your nose for variations. Push up, feeling the focus on the triceps. Repeat 10 times to start. Work up to 25. I find this works best with full extensions and a slow steady pace. Try also lowering your self slowly but when pushing up, do it fast exploding upwards. This will help to do explosive movements such as punching or throwing.</p>
