	        <h1 class="contentheading">Judging in a Sanshou Competition</h1>
	        <h2>By Derek Cheng</h2>
	        <br><br>
			<p>An important factor in organizing a good competition is to have good judges.  Usually, when we watch a Sanshou competition, we focus our attention on the competitors, and ignore the quality of the judging.  As Sanshou has become more and more popular, the overall skill level of the competitors has significantly increased, and so the sport has become a lot more competitive.  As a result, the need of having good and experienced judges has become essential.</p>
			<p>In a well-organized Sanshou competition, there should be seven essential judges.  They include one head judge, five corner judges, and one platform judge. There are also assistants to the judges, such as the time-keeper, the announcer, and the recorder.</p>
			<p>The responsibility of the head judge is to ensure that the competition is running properly, settle disputes if they arise, and coordinate all judging efforts.  Five corner judges are used to avoid ties; their responsibility is to record points won by the competitors according to the rules.</p>
			<p>The platform judge has the most immediate influence on the match.  Contrary to what most people believe, the rolefor a platform judge is NOT primarily to award points, and is certainly NOT to determine who is the winner of the match, but rather, to ensure safety of both competitors throughout the match.  This is achieved by regulating the fight using verbal commands and hand gestures.</p>
			<p>Whether the platform judge understands this determines the quality of the resultant judging.  If the platform judge chooses arbitrarily to focus his attention on one side (at the expense of the other), then his judgements would be biased toward one competitor.  For example, if the red side is significantly stronger than the black side, and if the platform judge chooses to focus on the red side, he might lose sight of the fact that the black side could be injured, or is not strong enough to defend himself/herself, and requires the platform judge to stop the fight.</p>
			<p>On the other hand, if the platform judge only focuses on the weaker side, then he might not notice even if the red side uses illegal techniques on the black side.  The result of both scenarios is the same:  the platform judge is putting the competitors at unnecessary risk.</p>
			<p>The ideal platform judge would, of course, divide his attention equally between the two competitors, and regulatethe fight according to standardized rules.  Therefore, having a clear understanding of the rules is the first step in becoming a good judge.</p>
			<p>The same thing also applies to corner judges.  The corner judge should not judge thinking that one side should bethe winner of the match, otherwise, the score will be biased.  Another important note for the corner judges is that they must follow the decisions given by the platform judge, even if some are given incorrectly, or some are omitted.</p>
			<p>It is the responsibility of the head judge, not the corner judges, to monitor the match and to ensurethatthe platform judge is giving out correct judgments.  If the head judge sees an incorrect decision given by the platform judge, or the omission to give a decision when one should be given, the head judge will signal to stop the fight and advise all the judges.  In order to ensure consistency in the scoring, the corner judges should not alter the points given by the platform judge.</p>
			<p>Being a good judge requires an understanding of the rules as well as cumulated experience; however objectiveness,fairness, and assertiveness are all basic characteristics of a good judge.  If all the judges understand their roles and responsibilities well, then everybody including the organizers, competitors, coaches, as well as spectators, will have a good competition experience.</p>
