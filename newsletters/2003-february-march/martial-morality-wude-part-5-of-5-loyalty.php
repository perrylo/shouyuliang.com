			<h3>Morality of Deed: Loyalty <i>(Zhong Cheng)</i></h3>
			<p>Loyalty is the root of trust.  You should be loyal to your teacher and to your friends, and they should also be loyal to you.  Loyalty lets mutual trust grow.  In the Chinese martial arts, it is especially crucial that there be loyalty between you and your master.  This loyalty is build upon a foundation of obedience to your master. Obedience is the prerequisite for learning.  If you sincerely desire to learn, you should rid yourself of false dignity. You must bow to your teacher both mentally and spiritually.  Only this will open the gates of trust.  A teacher will not teach someone who is always concerned about his own dignity.</p>
			<p>There was a story told to me when I was a child.  A long time ago in Asia there was a king.  Nobody had ever seenthe king's real face, because whenever he met with his ministers and officials, and whenever he appeared in public, he always wore a mask.  The face on the mask had a very stern and solemn expression.  Because nobody could see the real expression on his face, all the officials and people respected him, obeyed him, and feared him.  This made it possible for him to rule the country efficiently and well.</p>
			<p>One day his wife said to him, "If you have to wear the mask in order to rule the country well, then what the people respect and show loyalty to is the mask and not you."  The king wanted to prove to his wife that it was he who really ruled the country, and not the mask, so he decided to the take the mask off and let the officials see his real face.</p>
			<p>Without the mask, the officials were able to see the expression on his face and figure out what he was thinking. It wasn't long before the officials weren't afraid of him anymore.</p>
			<p>A few months passed, and the situation got steadily worse.  He had lost the solemn dignity which madepeople fear him, and even worse, the officials had started to lose respect for him.  Not only did they argue with each other in front of him, they even began to argue with him about his decisions.</p>
			<p>He soon realized that the unity and cooperation among his officials had disintegrated.  His ability to lead the country had gradually disappeared, and the country was falling into disorder.  The king realized that, in order to regain the respect of the people and his ability to rule the country, he had to do something.  He therefore gave the order to behead all the officials who had seen his face, and he then appointed new ones.  He then put the mask back on his face.  Soon afterward, the country was again united and under his control.</p>
			<p>Do you have a mask on your face?  Is it the mask that people are loyal to?  Is what you show people on your face what you really thing?  Do we have to put a mask on this masked society?  How heavy and how thick is your mask?  Have you ever taken your mask off and taken a good look at the real you in the mirror?  If you can do this it will make you humble.  Then, even if you have a mask on your face, your life will not be ruled by your mask.</p>
