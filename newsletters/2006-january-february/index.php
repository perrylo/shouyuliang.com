				<p><b style="font-size:13px;">"Selflessness"</b><br>
				<span class="small">To be impartial and act without thought of self.</span></p>
				<p>It happened during the Spring and Autumn Period. Duke Pinggong of Jin asked Qi Huangyang, "Nanyang County needs a new magistrate. Who do you think is fit to fill the vacancy?" Qi answered, "Xie Hu would make the best choice. He is equal to the post, I'm sure." Surprised, the Duke said, "Isn't Xie Hu an enemy of yours? Why should you recommend him, of all persons?" "Yes, he is," rejoined Qi. "But Your Grace has only asked me who is fit to be the new magistrate. I'm not supposed to say who my enemy is." So Duke Pinggong made Xie Hu magistrate of Nanyang County, where the latter did many good things for the people, winning praises from all quarters.</p>
				<p>Some time later, Duke Pinggong asked Qi Huangyang again, "A new judge is needed in the imperial court. Whom, in your opinion, should be chosen for the post?" Qi replied, "Qi Wu, I think." Again surprised, the Duke said, "But isn't Qi Wu your son?" "Your Grace only asked me who should be made the judge," came the answer. "That's why I've suggested Qi Wu. I don't think Your Grace wants to know who my son is." So Qi Wu was made the new judge, and as such he performed many good deeds, much to the acclaim of all.</p>
				<p>Hearing of how Qi Huangyang had behaved in this two cases, Confucius spoke highly of him. The Master had this to say: "In recommending people for official posts, Qi Huangyang was only guided by the criteria of virtue and abilities. He had no prejudice against anyone or refrained from recommending anyone even though the latter might be his enemy. Nor was he afraid of being gossiped about and so he did not hesitate in recommending his own son. Qi Huangyang can be said to be truly selfless."</p>
				<p class="small"><b>A work written by proteges of Lu Buwei ( ? - 235 B.C.), Prime Minister of the state of Qin towards the end of the Warring States Period.</b></p>
