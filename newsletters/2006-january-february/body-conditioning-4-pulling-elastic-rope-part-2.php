			<p><b>F. PULL AND KICK:</b><br />
			Squat down in Horse Stance and grab both ends of the rope, one in each hand. Pull the rope down to your left. Simultaneously kick with the left leg, keeping the toes locked back (Figure 3-43). Change to right side and repeat in the opposite direction (Figure 3-44). While you are kicking, keep the standing leg bent for balance and rooting training.</p>
			<p>Repeat the exercise and gradually increase the number of repetitions.</p>
			<p align="center"><img src="../../images/newsletter/v6n1/a6p2.jpg" width="500" height="194" alt="Body Conditioning - Pulling Elastic Rope"></p>
			<p><b>G. LEG HOOKING, BLOCKING</b><br />
			Stand with your right foot for ward (Figure 3-45). Grab the ends of the rope with both hands and pull down to your left. Simultaneously, step your left foot behind your right foot (Figure 3-46). Keep pulling the rope and raise both hands in front of your chest (Figure 3-47).</p>
			<p>Then twist your body to your left 180 degrees, and kick back and upward in a circular motion with your right leg as high as you can. Keep pulling the rope to your left with your left hand and with the right hand keep pulling the rope forward then downward in front of you. Your eyes should look back to the left (Figure 3-48). Repeat this exercise many times as you want.</p>
			<p>Change sides and repeat.</p>
			<p align="center"><img src="../../images/newsletter/v6n1/a6p3.jpg" width="400" height="204" alt="Body Conditioning - Pulling Elastic Rope"></p>
			<p><b>H. LOWER LEG BLOCKING, ARMS PULLING AND SHOULDER PRESSING TRAINING:</b><br />
			Face the pole and pull the rope towards you with both hands (Figure 3-49). Step forward with your right foot (Figure 3-50). Step back and around the right foot with your left. Your back should now face the pole (Figure 3-51). Twist and extend your body into a Left Bow Stance and extend your right leg out for a leg block while looking back to your left. Pull the rope upward over your head and then down to your front with your left hand. Simultaneously, pull the rope with your right hand forward to the left (Figure 3-52). Both hands should pull the rope at the same time in a circular motion. Repeat this exercise as many times as you want. Change sides and repeat.</p>
			<p align="center"><img src="../../images/newsletter/v6n1/a6p4.jpg" width="500" height="194" alt="Body Conditioning - Pulling Elastic Rope"></p>
			<p><b>I. FRONT CRASS STEP, CARRYING OVER THE SHOULDER TRAINING:</b><br />
			This is a typical training method for hip throwing techniques. Stand with both legs shoulder width apart and slightly bent. Grab the ends of the rope with both hands (Figure 3-53). Step with your right foot across and in front of your left (Figure 3-54). Keeping the legs bent, pivot to the left on the balls of you feet and pull the rope up to your left shoulder (Figure 3-55). Pull the rope down over the shoulder in a circular motion with both hands. Bend at the waist and bow forward deeply. Simultaneously, spring up with both knees. Remember to keep your head tucked in (Figure 3-56).</p>
			<p>Repeat the exercise many times as you want, and then change sides.</b>
			<img src="../../images/newsletter/v6n1/a6p5.jpg" width="130" height="201" alt="Body Conditioning - Pulling Elastic Rope" align="left" style="margin-right:15px;">
			<p><b>THE SHOULDER TRAINING:</b><br />
			This training is the same as Front Cross Step, Carrying Over the Shoulder Training except for the foot work. Instead of stepping in front of the other foot, step across and behind. Feel free to experiment with the foot work when training this exercise.</p>
