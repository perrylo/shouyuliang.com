	        <h1 class="contentheading">Interview with Rena Huang</h1>
	        <h2>By Tony Bujas</h2>
	        <br><br>
			<p><img src="../../images/newsletter/v6n1/a1p1.jpg" width="225" height="300" border="0" alt="Rena Huang - Chen Taiji" align="right" style="margin-left:15px;margin-bottom:10px">Rena Huang is a former professional dancer and teacher from the highly regarded Beijing Dance Academy in China. She has appeared in countless performances, videos, and commercials. Currently, she practices Wushu to enrich her life with a new art.</p>
			<p><b>What is your Chinese name?</b><br />Huang Xue</p>
			<p><b>Where were you actually born?</b><br />
			I was born in an industrial city called Shen Yang in Liao Ning province in the north part of China.  About 2 hours flight north of Beijing.</p>
			<p><b>Tell me a little bit about your schooling?</b><br />
			Before the age of 10, I went to an elementary school in Shen Yang city. I had a little bit dance training (not by professionals) and used to go to a place after school once a week (more like an after school activity center kind of place) where I danced.  My mom is a Chinese dance teacher so she trained me too, even after I started going to Beijing Dance Academy she used to train me very hard during school holidays and I really hated it since I never really had any holidays!</p>
			<p><b>In Beijing at 10 years old you started professional dance school training?</b><br />
			At 10 years old, I was chosen by the best dance school of China, the Beijing Dance Academy, and started professional dance training. I went to Beijing and lived in the school with my classmates, who came from all over China and only went home twice a year during the school holidays.</p>
			<p>Every year, Beijing Dance Academy only chooses around 20 (10 girls and 10 boys) from tens of thousands of kids who audition.  Back then expenses were all paid by the country (everything including food, a place for us to live, all dancing clothes and shoes and everything else. We were also given a monthly allowance too).  Not anymore, "now" the kids need to pay quite a bit of money.</p>
			<p>The first 6 years were considered like a high school level, and I studied a further 2 years at the university level - 8 years in total.</p>
			<p><b>Sounds like it was very demanding?</b><br />
			Yes! Very demanding, full time, all the time, plus other subjects needed to be studied.</p>
			<p><b>Can you describe a typical high-school-level day please?</b><br />
			Sure! We'd wake up about 6 AM and to build stamina we would run around the lakes located in the park next to our school; this was followed by stretches.  Typically this could take anywhere from 30 to 60 minutes. Then we'd go back and have breakfast.  The next part of the day would vary. At 8 AM we would start class and do 2 hours of basic training. At around 10 AM we would either learn basic dance training or do other dance and presentation training, or on some days we would do subject study.  Lunch was for one hour followed by about 2 hours of nap time.  Then 90 minutes of further dance training followed by 90 minutes of special dance study/training or subject study.  Some days it would be all dance training while on other days it would vary with subject studies.  Generally 2/3 of the time was devoted to practicing dance training while 1/3 of the time was for other subjects study.</p>
			<p><b>Was university-level different?</b><br />
			It was similar, but the training was no longer general. I specialized in Chinese folk dances. So the training was more specific and concentrated with emphasis on the folk dancing.</p>
			<p><b>Can you say a few words about your professional career please?</b><br />
			After graduation, I stayed with the Beijing Dance Academy becoming a part of their elite dance troupe.  We would perform around China and other countries and at times do commercial and media work.  Our day would still start with 2 hours of basics training but then we would focus on all the different dances for the performances.  We would need to meet and maintain a certain standard at the professional performance dance level.  Many days would be like one long continuous rehearsal, performing over and over again - we would start around 8:30 AM and not finish until around 10:30 PM.</p>
			<p><b>Wow!  Sounds very repetitive and very tiring?</b><br />
			Yes! The repetition may seem boring but it becomes a part of you, like muscle memory, once you turn on everything is automatic; the smile is there, the body placement the movement. And yes it is very tiring!!</p>
			<p><b>What did you like about the lifestyle?</b><br />
			The food! The people, meeting other performers and special dignitaries and all types of culture.  The flowers.  Travel. Audience appreciation and feedback.  The fun!</p>
			<p><b>How long have you been doing WuShu at the Shou-Yu Liang (SYL) Wushu Taiji Qigong Institute?</b><br />
			Just over 5 years.</p>
			<p><b>Formally, you are a 'tudi' of Helen Liang as well as very good friends?</b><br />
			Yes!</p>
			<p><b>How did you get interested in WuShu?</b><br />
			Friends, and I did a dance for SYL one year, plus I was in a transition period at the time and decided to give WuShu a go.</p>
			<p><b>Do you find any similarities between WuShu training compared to dance training?</b><br />
			Yes!  I find a lot of similarities.  You need focus, concentration, flexibility, stamina, coordination and body control in both arts.  Both arts make you strong.  I really enjoy WuShu a lot.  I've learned so much and met many good people and made many friends.</p>
			<p><b>I know you do weapons in WuShu; did your Dance training ever use weapons for props?</b><br />
			In dance we used different props at different times depending on the dance; we used swords and "long sleeves" and fans and handkerchiefs but for dance.  In WuShu I really like weapons, especially the sword and I really like learning forms, it's so much fun.  I find the basics different from dance basics.  In dance it's more like light, soft, float, feather type emphasis, while in WuShu it's more explosive, martial and application type emphasis.</p>
			<p><b>Your Chen Taiji has improved greatly and I see that you enjoy practicing it.</b><br />
			Thank you! I personally enjoy the movement and flavor and how you use the body.</p>
			<p><b>Thank you very much for taking the time to do this interview.</b><br />
			My pleasure!</p>
