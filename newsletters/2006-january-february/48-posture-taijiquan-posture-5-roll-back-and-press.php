			<p align="center"><img src="../../images/newsletter/v6n1/a3p1.jpg" width="500" height="506" alt="Taiji 48 Form - Posture 5: Roll Back and Press"></p>
			<p><b>Movements:</b></p>
			<p><b>Roll Back - left side</b><br />
			Figure 5-20: Step forward to your left corner, while extending and circling your right palm to your right, over your left palm. At the same time, turn your left palm up and bring it closer to your body. Both palms move in a clockwise arc. (Step Northeast)</p>
			<p>Figure 5-21: Shift your weight to your left leg, while lowering both palms in the direction of your left knee.</p>
			<p><b>Press - right side</b><br />
			Figure 5-22: Shift a11 your weight to your left leg and bring the ball of your right foot next to it. At the same time, raise both palms up - your right palm next to your chest with fingers pointing to your left and your left palm facing your head with fingers pointing up. (See Figure 5-26 for the mirror image of this figure)</p>
			<p>Figure 5-23: Bring your left palm next to your right wrist, step to your front right corner with your right foot and extend both palms forward. (Step Southeast)</p>
			<p><b>Roll Back - right side</b><br />
			Figure 5-24: Shift your weight back to your left leg, while circling both palms counter clockwise.</p>
			<p>Figure 5-25: Shift your weight back to your right leg while lowering both palms in the direction of your right knee.</p>
			<p align="center"><img src="../../images/newsletter/v6n1/a3p2.jpg" width="500" height="496" alt="Taiji 48 Form - Posture 5: Roll Back and Press"></p>
			<p><b>Press - left side</b><br />
			Figure 5-26: Shift all your weight to your right leg and bring your left foot next to it. At the same time, raise both palms up - your left palm next to your chest with fingers pointing to your right and your right palm facing your head with fingers pointing up.</p>
			<p>Figure 5-27: Bring your right palm next to your left wrist, step to your front left corner with your left foot and extend both palms forward. (Step Northeast)</p>
			<p><b>Roll Back - left side</b><br />
			Figures 5-28 and 5-29: Shift your weight back to your right leg then repeat Figures 5-20 and 5-21.</p>
			<p><b>Press - right side</b><br />
			Figures 5-30 and 5-31: Repeat Figures 5-22 and 5-23.</p>
