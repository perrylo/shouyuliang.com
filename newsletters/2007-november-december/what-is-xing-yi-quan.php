			What is Xing Yi Quan?	</h2>




<div class="article-content">
<h2>By Grandmaster Shou-Yu Liang and Master Wen-Qing Wu</h2>

<div class="articleImage"><img src="../../images/newsletter/v7n5/Helen-Liang-Xing-Yi-Sword-Yao.jpg" width="300" height="225" border="0" alt="Helen Liang - Xing Yi 12 Animals Sword - Yao"><br />Helen Liang - Xing Yi 12 Animals Sword - Yao</div>

<p>In order to survive in what is oftentimes a violent and cruel world, animals must very quickly develop the ability to protect themselves from natural dangers and other animals. All wild animals still have the instincts and senses for this. They all still know how to hunt and fight to survive. These abilities require physical strength, a natural, instinctively perceptive mind, and a highly alert spirit. For example, an eagle must have sharp eyes and claws. A tiger doesn't just need a strong body and claws, it must also have the spirit which makes other animals afraid of it.</p>

<p>We humans have lost most of the natural instincts, senses, and fighting abilities that all other animals possess. In order to regain these abilities and use them to fight against other men, the martial artists in ancient times imitated the fighting techniques and spirits of animals, modifying them with human rational analysis. This is how many different styles were created.  Xing Yi was created in imitation of the fighting techniques and spirit of twelve animals. One of the documents which are available to us contains a section which explains what Xing Yi is. We would like to translate it here and make some comments. Hopefully this will give you a clear concept of Xing Yi Quan.</p>

<h3>About Xing Yi</h3>

<p><b>(What is) the Xing (the shape)? The shape of imitating. (What is) the Yi (the mind)? The mind (generated) from the Xin (heart).</b></p>

<p>"Shape" here means the external appearance or the movements used in fighting, and Yi (pronounced <i>yee</i>) means the mind which is generated internally from the heart (Xin, the emotional, feeling mind). Therefore, Xing Yi is an internal style which imitates the shapes of twelve animals and their inner characteristics when they fight.</p>

<p>According to Chinese Qigong, Yi is the wisdom mind, which is generated from the Xin (emotional mind). The emotional mind makes you excited, quick, and agile, while the wisdom mind makes you calm and gives you clear and accurate judgement. When you are able to combine your Xin and Yi, your movements will be fast and agile, yet accurate and calm.</p>

<p>Therefore, Xing Yi is an internal martial art which utilizes the techniques of the twelve animals, and also requires an internal cultivation by which the Xin and Yi can be combined.</p>

<p><b>(Only) man of the million living things has a "Ling" (supernatural spirit). (He) is able to feel and respond to everything. This is because (he has a) Xin (emotional mind) internally to comprehend the surrounding objects, objects are external, but the "comprehending" is internal. Objects are external, but the "comprehending" is internal.</b></p>

<p>"Ling" means supernatural spirit. The Chinese people believe that men are more civilized and wiser than other animals simply because they have a "Ling" which allows them to feel, understand, and communicate with nature. All of this is because we have stronger emotional feelings than other animals. The outside of your body and everything external to you is comprehended by your mind, which is within you. The control of your body's posture and movement also comes from your internal awareness and understanding.</p>

<p><b>The Yi (wise judgement) originates with the Xin (emotional mind). Therefore, (when) Xin and Yi sincerely exist within, then millions of objects will be shaped externally. The internal and external correspondence (depends on) nothing but the circulation and transport of the one Qi.</b></p>

<p>Your Yi (wisdom mind) originates with your Xin (heart, or emotional mind). This is because it is your emotional mind which first responds to stimuli. Your wisdom mind evaluates what the emotional mind has perceived. If your wisdom mind is in control of your emotional mind, and they both sincerely work together, then you will have great control of everything external, both your body's posture and movements, and your interactions with people and things around you ("millions of objects"). This control is the result of having strong, healthy Qi circulation, and also of being able to effectively move the Qi wherever you wish.</p>

<p><b>Therefore Da Mo, the ancestral teacher, used this (theory) and created this fist (i.e., style). His objective was Yang Qi (i.e., cultivating Qi) and increasing the Li (muscular strength). The movements are simple, but its aQievements and applications are unlimited. Therefore, it is named Xing Yi (Shape-Mind) Quan.</b></p>

<p>It is believed that Marshal Yeuh Fei was the creator of Xing Yi. However, since his art originated at Shaolin, many martial artists credit Da Mo as the original ancestor.</p>

<p>Many people think that Shaolin Kung Fu is limited to the external techniques, but they are wrong. All of the Chinese martial styles require internal training to build up the Qi in order to reach a high level of skill. Xing Yi Quan is a typical example. It emphasizes Yang Qi. Yeang means to nourish, to increase, to raise, and to cultivate. Only after you have built up your Qi to a higher level will you be able to use it to effectively energize the muscles and increase your power. Muscle power is called "Li." Li is the demonstration of muscular power, but it must be energized by Qi (refer to Dr. Yang's book: "Advanced Yang Style Tai Qi Quan, Vol. 1").<img src="../../images/icons/10x10_yinyang.gif" class="articleYinyang" alt=""></p>


