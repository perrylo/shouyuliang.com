			48 Posture Taijiquan Posture 15 - Slap Foot and Tame the Tiger 	</h2>




<div class="article-content">
<H2>By Grandmaster Shou-Yu Liang and Master Wen-Ching Wu</H2>
<p class="small" style="border-top:solid 1px #cccccc;border-bottom:solid 1px #cccccc;"><b>48 Posture Taijiquan requires that students have previously studied and completed 24 Posture Taijiquan.  These instructions assume SOUTH as the starting direction.</b></p>

<h2>Posture 15: Slap Foot and Tame the Tiger </h2>

<div class="small" align="center"><img src="../../images/newsletter/v7n5/Taiji-48-Form-Slap-Foot-Tame-Tiger-1.jpg" width="600" height="588" border="0" alt="Slap Foot and Tame the Tiger "><br /><b>Taiji 48 Form - Slap Foot and Tame the Tiger</b></div>

<p>Movements:</p>

<p><b>Slap Foot and Tame the Tiger - left side</b></p>

<p>Figure 5-82: Step forward with your left foot, while circling your right palm down and back up to head level.</p>

<p>Figure 5-83: Shift your weight to your left leg.* Then kick your right foot up and slap it with your right palm*. (*Quick Tempo*)</p>

<p>Figure 5-84: *Bring your right foot down next to your left and shift all your weight on it. Left foot touches the floor lightly. At the same time, rotate both palms clockwise to your right.* (*Quick Tempo*)</p>

<p>Figure 5-85: Step to your left with your left foot into Bow stance. At the same time, lower both hands in fists down over your knee. Continue the movement of your left fist up to head level and bring your right fist next to your chest. (Step North)</p>

<p>Figure 5-86: Bend both elbows, and pull both fists closer to your body and turn your head to your right. (See Figure 5-86R for the posture taken from a different angle)</p>

<div class="small" align="center"><img src="../../images/newsletter/v7n5/Taiji-48-Form-Slap-Foot-Tame-Tiger-2.jpg" width="600" height="594" border="0" alt="Slap Foot and Tame the Tiger "><br /><b>Taiji 48 Form - Slap Foot and Tame the Tiger</b></div>


<p><b>Slap Foot and Tame the Tiger - right side</b></p>

<p>Figure 5-87: Shift your weight back to your right leg, while lowering your left arm and extending your right palm over your left.</p>

<p>Figure 5-88: Turn your left foot in and shift your weight forward to your left leg while extending your right palm in an arc to your right. At the same time, bring your left palm down next to your waist, palm up.</p>

<p>Figure 5-89: Bring your right foot next to your left foot as you step to your right. At the same time, lower your right palm next to your waist; and circle your left palm back and up until it is in front of you. (Step East)</p>

<p>Figure 5-90: Shift all your weight to your right leg then *kick up with your left foot, and slap it with your left palm, while circling your right palm back and up.* (*Quick Tempo*)</p>

<p>Figure 5-91: *Bring your left foot down next to your right and shift all your weight on it. Right foot touches the floor lightly. At the same time, rotate both palms counter-clockwise to your left.* (*Quick Tempo*)</p>

<p>Figure 5-92: Step to your right with your right foot into Bow stance. At the same time, lower both hands in fists down over your knee. Continue the movement of your right fist up to head level and bring your left fist next to your chest. (Step South)</p>

<p>Figure 5-93: Bend both elbows, pulling both fists closer to your body and turn your head to your left.<img src="../../images/icons/10x10_yinyang.gif" class="articleYinyang" alt=""></p>




