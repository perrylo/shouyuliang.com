<h3>Holding Leg Throw #1 -  Holding Two Legs, Pressing Forward</h3>

<ol type="a">
	<li>Gray steps with right foot forward and attacks with a right-handed punch. White blocks outward with his left arm. Then White strikes Gray with both hands to the face or chest (or White will initiate the attack) (Figure 4-1).</li>
	<li>Gray moves his head to dodge White's attack or raises his arms to block. Whites moves his hands quickly downward to grab and hold both of Gray's legs. White steps forward with either the right or left foot deep in between Gray's legs (Figure 4-2).</li>
	<li>White pulls with both hands back and upward with force and drives the body forward, using his shoulder to strike Gray's thigh or abdomen and forcing Gray to fall backward (Figures 4-3 and 4-4).</li>
</ol>

<p><b>Key Points</b></p>
<ul style="list-style:disc">
	<li>Drop low as quickly as possible.</li>
	<li>Your body weight should be slightly more on the rear leg to create pushing power for the drive. Push forward from the rear leg and lift the hands upward simultaneously.<img src="../../images/icons/10x10_yinyang.gif" class="articleYinyang" alt=""></li>
</ul></div>


