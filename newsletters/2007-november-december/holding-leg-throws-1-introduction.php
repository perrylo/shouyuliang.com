<h2>By Grandmaster Shou-Yu Liang and Tao D. Ngo</h2>

<h3>Introduction</h3>

<p>Holding leg(s) techniques are often used in San Shou Kuai Jiao fighting. They are very effective when defending against an opponent's kicks or for controlling an opponent's leg(s) at close range. These kinds of throws are used when you have the opportunity to hold an opponent's leg(s). Generally, these techniques are used for long range and close range fighting.</p>

<p>In long range fighting these techniques are used when defending against an opponent's kick. Catch or trap the kicking leg and then apply the throw. When applying this type of technique, keep your body as far away from your opponent as possible so that he cannot strike you while you are holding his leg. You need to unbalance your opponent first so that even if he does strike, there will little power behind it.</p>

<p>For close range fighting, the holding leg(s) techniques require you to get very close to your opponent, and are normally coupled with fakes, attacks, or defenses to disguise your intention. While your opponent is reacting to your first move, you must enter quickly, get hold of his leg(s), unbalance him, then throw him down. Keep in mind that when you are close enough to your opponent to hold his leg(s) you are vulnerable to attack. Therefore, move in and apply the throw as quickly as possible. Avoid struggling with your opponent, and give him no time to react and strike back.</p>

<p>In this series of articles, we will introduce San Shou Kuai Jiao techniques for holding an opponent's leg(s) to throw him. Your hold must be tight and strong and the catching, lifting, and pulling movements quick and powerful. The throw itself should also be fast, smooth, and strong. A half-hearted throw is a waste of energy and can put you in a dangerous position.</p>

