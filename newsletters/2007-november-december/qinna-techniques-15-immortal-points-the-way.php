<p>Step 1. Your opponent grabs a hold of your right wrist from the bottom with his left hand (Figure 5-73). Circle your right arm clockwise to your right; at the same time, hook your left arm under his left arm (Figure 5-74).</p>

<p>Step 2. Place your left hand on top of your right wrist and push both hands down, while raising your left elbow up (Figure 5-75).</p>

<p>Note:</p>
<p>When you circle your right arm, rotate and point your fist towards your opponent before moving your arm to your right. This will set it up for your left arm to hook under his arm. When you complete the circle to your right, rotate your right arm until your arm is facing down.<img src="../../images/icons/10x10_yinyang.gif" class="articleYinyang" alt=""></p>
