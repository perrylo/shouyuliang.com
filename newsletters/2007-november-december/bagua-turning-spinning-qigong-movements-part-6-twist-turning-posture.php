<H2>By Grandmaster Shou-Yu Liang, Dr. Jwing-Ming Yang, and Master Wen-Ching Wu</H2>
<h3>Bagua Zhuan Xuan Gong</h3>
<br />
<h3>Twist Turning Posture</h3>
<p><b>Verbal Secrets: Body is twisted like a rope and the head is suspended from above.</b><br />In Baguazhang, you walk in a circular pattern. Your face, body, and arms are all twisted toward the center of the circle. It is therefore said that your body should be twisted like a rope. However, even though your body is twisted, your head should remain upright, as though suspended from above.
</p>
<div class="small" align="center"><img src="../../images/newsletter/v7n5/Bagua-Qigong-Twist-Turning-Posture-1.jpg" width="600" height="592" border="0" alt="Bagua Turning-Spinning Qigong - Twist Turning Posture"><br /><b>Bagua Turning-Spinning Qigong - Twist Turning Posture</b></div>
<p>Movements:</p>
<p><b>Left Wrench and Turn:</b><br/>
Move your left foot to your left, about 12 inches from your right foot and hold your arms as though you were holding a ball in front of your chest, right arm on top; inhale (Figure 3-29). Then turn your right foot in about 45 degrees as you turn your body to your left (Figure 3-30). Next turn your left foot out until it is pointing to your rear, and turn your body as far to your left as possible. At the same time, extend and rotate your left palm up over your head, and push your right palm back next to your left armpit; exhale (Figure 3-31 and 3-31R). Keep this posture and do eight abdominal breathing cycles.</p>

<div class="small" align="center"><img src="../../images/newsletter/v7n5/Bagua-Qigong-Twist-Turning-Posture-2.jpg" width="486" height="591" border="0" alt="Bagua Turning-Spinning Qigong - Twist Turning Posture"><br /><b>Bagua Turning-Spinning Qigong - Twist Turning Posture</b></div>

<p><b>Right Wrench and Turn:</b><br/>
Turn your body back until you are facing forward, and hold your arms as though you were holding a ball in front of your chest, left arm on top, inhale (Figure 3-32). Then turn your left foot in until it is pointing inward at about 45 degrees as you turn your body to your right (Figure 3-33). Next turn your right foot out until it is pointing to your rear, and turn your body to your right. At the same time, extend and rotate your right arm up over your head, and push your left palm back next to your left armpit; exhale (Figure 3-34). Keep this posture and do eight abdominal breathing cycles. To complete this posture, turn your feet back until pointing forward. As you turn your body forward, lower both palms next to your abdomen Then bring your left foot next to your right foot; inhale (Figure 3-35).</p>
<p>Key Notes:</p>
<p>Keep your head upright and don't raise your shoulders. While turning your body, rotate your torso muscles gradually. When holding the posture, your body should feel like a piece of rope with its strands intertwined. During exhalation, push your lower palm back, and continue rotating your extended arm slightly - creating a stronger wrenching feeling of your torso. Eyes look past your shoulders.<img src="../../images/icons/10x10_yinyang.gif" class="articleYinyang" alt=""></p></div>


