<h4>Movement and Intention:</h4>

<p>Step 1. Stand naturally, overlap your hands on top of your abdomen. Men place your left LaoGong point on top of your DanTian, women with right hand on top of your DanTian, as in Figure 1-21.</p>

<p>Step 2. Use Drawing 1-11 as a reference. Inhale, use your mind to lead qi from the dadun point, on your Liver Channel, along the inside of your thighs, past the reproductive organs, and up to ZhangMen, then to QiMen.</p>

<p>Step 3. Exhale, open your eyes wide, but mentally look into your liver, and let out the <b>Xu</b> (<i>Shuuu...yiii...</i>) sound as you lean forward slightly (Figure 1-31). At the same time, hold up your huiyin area and draw your abdomen in to squeeze out the impurities from your liver. Repeat 6 inhalations and exhalations.<img src="../../images/icons/10x10_yinyang.gif" class="articleYinyang" alt=""></p>

