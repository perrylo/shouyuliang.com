<div class="small" align="center"><img src="../../images/newsletter/v7n5/Wushu-Low-Hook-Kick.jpg" width="355" height="281" alt="Wushu Basic Kicks - Low Hook Kick"><br /><b>Wushu Basic Kicks - Low Hook Kick</b></div>

<h3>Low Hook Kick (gou ti tui)</h3>
<p>A Low Hook Kick utilizes the angle created by hooking the foot up. The kicking surface is the area between the foot and the shin.</p.
<p>Drill: Start in a ready position with your left leg forward (Figure 267). Turn your left foot out, shift your weight forward and hook kick forward with your right foot. At the same time, pull your right hand down in a hook and extend your left arm up (Figure 2-68). Put your right foot down and turn your foot out. Then repeat the kick with your left leg. Alternate from side to side across the training area.</p>

<div class="small" align="center"><img src="../../images/newsletter/v7n5/Wushu-Squat-Kick.jpg" width="619" height="285" alt="Wushu Basic Kicks - Squat Kick"><br /><b>Wushu Basic Kicks - Squat Kick</b></div>

<h3>Squat Kick (di chuai tui)</h3>
<p>This is a kick with the outside edge of the foot while squatting down.</p>
<p>Drill: Start in a ready position with your right leg forward (Figure 269). Squat down quickly and shift all your weight onto your left leg. At the same time, kick forward with your right foot (Figures 2-70 and 2-71). Stand up and repeat the kick 10 to 20 times. Then do the kick with the other leg.<img src="../../images/icons/10x10_yinyang.gif" class="articleYinyang" alt=""></p>


