			<p>Questions naturally arise when a new Beginners Qigong Class starts up. One of the top questions has to do with visualization.  Some people have difficulties visualizing and ask what they can do about it.   I offer indirect and direct approaches that may help.</p>
			<p>First, have faith and trust in the teacher and the teachings.  Believe in the Qigong Method you are practicing, and know that you will give it your best try; you will faithfully "do it" as consistently as possible, step by step.  Initially follow the procedure as close as you can.  You are getting in touch with your body and its systems, connecting and opening up your channels.  Physically smile and internally smile from the top of your head to the bottom of your toes, smiling within all your internal organs and into your bone marrow. Think: I am happy, I am relaxed.  Release tension as best as possible.  Once comfortable with what you are doing, make it your own.</p>
			<p>Individual mind-speak and terminology can greatly vary from individual to individual.  How you talk 'inside your own mind', with 'your own vocabulary meanings' differ from person to person.  To help with visualization you may need to modify terms to suit your personal inner voice.  This is good.  Allow yourself permission to do this; know and trust your inner self to have valid sensations.  I offer a listing of terms and expressions that can be used in various points of Qigong Training that you may find useful. You are encouraged to add and use your own - what works best for you.</p>
