			<p>To help strengthen your mind's-eye, try the following:</p>
			<p>Go out and watch a tree, see it as clearly as possible, close your eyes and still see it, practice until you can do so.  Go home, close your eyes and see the tree in your mind as clearly as if it were in front of you.  You can do this with the full moon or even a candle.   Any object of your choosing - practice looking at it, closing your eyes, and seeing it in your minds-eye; bringing the "picture" back whenever you like.</p>
