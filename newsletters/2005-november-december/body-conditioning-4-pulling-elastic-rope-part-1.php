	        <h2>By Grandmaster Shou-Yu Liang and Master Wen-Ching Wu</h2>
	        <br><br>
			<p>Pulling elastic rope is another basic of Chinese Wrestling training. It is convenient because you can bring the rope with you to practice where and when you want. This training will help to increase the strength and mobility of the whole body. Elastic rope pulling is often practiced in combination with basic wrestling moves to improve the accuracy and fluency of the techniques.</p>
			<p><b>Equipment:</b><br />
			Traditionally, the equipment consists of pulleys with weights tied at one end of the rope. The weight should vary according to your ability. Elastic rope can also now be used. Tie the rope so that it has one long section joined by two shorter sections. The long section should be two or three times longer. The two shorter sections are for both hands to pull. When practicing, tie the longer end of the rope to a pole or tree. Make sure the rope is secure before using it (Figure 3-32).</p>
			<p align="center"><img src="../../images/newsletter/v5n6/a6p1.jpg" width="500" height="233" alt="Body Conditioning - Pulling Elastic Rope"></p>
			<h3>Training Method:</h3>
			<p><b>A. PULLING SIDEWAYS:</b><br />
			Stand with both feet shoulder width apart. Grab both short ends of the rope in your left hand and stand sideways with your right shoulder facing the pole (Figure 3-33). Pull the rope in front of you and past your body to your left. Follow the pulling hand with your eyes. Repeat the exercise and gradually increase the number of repetitions. (Figure 3-34).</p>
			<p>Change sides and repeat with the right hand.</p>
			<p><b>B. PULLING DOWNWARD SIDEWAYS:</b><br />
			Grab the short ends of the rope with both hands and face the pole with right leg forward (Figure 3-35). Step the right foot across the left foot while pulling the rope down to the left with both hands. Repeat the exercise and gradually increase the number of repetitions. (Figure 3-36).</p>
			<p>Change sides and repeat.</p>
			<p align="center"><img src="../../images/newsletter/v5n6/a6p2.jpg" width="400" height="562" alt="Body Conditioning - Pulling Elastic Rope"></p>
			<p><b>C. PULL AND THRUST FORWARD:</b><br />
			Stand with both feet shoulder width apart, grab both ends of the rope with the right hand and face the pole sideways (Figure 3-37).</p>
			<p>Pull the rope to your left in front of you. When it passes your body, twist and push into a Left Bow Stance. The right hand thrusts forward and extends as far as possible while you look back to the left. Repeat the exercise and gradually increase the number of repetitions (Figure 3-38).</p>
			<p>Change sides and repeat.</p>
			<p><b>D. EXTENDING THE BODY:</b><br />
			Stand with both feet shoulder width apart with the right side of your body facing the pole. Grab both ends of the rope with your left hand and pull it around your back. Hook the rope with your right arm at the inside crook of your elbow (Figure 3-39). Twist your whole body to the left into Left Bow Stance. The right arm pulls forward to the left. Repeat the exercise and gradually increase the number of repetitions (Figure 3-40).</p>
			<p>Change sides and repeat.</p>
			<p align="center"><img src="../../images/newsletter/v5n6/a6p3.jpg" width="400" height="546" alt="Body Conditioning - Pulling Elastic Rope"></p>
			<p><b>E. PULLING IN BOTH DIRECTIONS:</b><br />
			Squat down in Horse Stance. Each hand grabs an end of the rope and pulls it in front of you (Figure 3-41). Open the arms out to the sides and then return to the original position (Figures 3-42).</p>
			<p>Repeat the exercise and gradually increase the number of repetitions.</p>
