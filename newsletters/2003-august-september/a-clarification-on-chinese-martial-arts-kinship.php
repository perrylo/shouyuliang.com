			<p>We had an unintentional "misname" recently at the club, and to help sort things out and for future reference; I decided to put together a little table of common terms used.</p>
			<p>What started as something initially thought of as simple became complicated, then very complicated, and then (for peace of mind at least) streamlined.  The following table and chart are the end result.  Any and all mistakes and omissions are mine to take responsibility for  - I apologize in advance if anything appears odd and improper.  The final say in your club rests on your ShiFu's shoulders and his/her preferences.  I have found out that distinctions can become very detailed. The spelling of the anglicized Mandarin is meant to be Pinyin, and the anglicized Cantonese is my own understanding of the spelling.  The chart is from YOUR point of reference.  Therefore, the terms are what you would call someone else in relation to you.  Finally, thanks to all who helped me with this - sorry for the headaches.  Thank you ShiFu (Grandmaster Liang), Helen Liang, Rena Huang, and Derek Cheng.</p>
