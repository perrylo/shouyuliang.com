	        <h2>By Grandmaster Shou-Yu Liang and Tai D. Ngo</h2>
	        <br><br>
			<p>Training in San Shou involves a lot of throwing or being thrown by a partner.  In same circumstances,you may needto fall to the ground purposely to execute certain throwing techniques.  Therefore, one of the very first skills you need to learn is how to fall.  It is of utmost importance that you know how to fall correctly and safely.  Being able to fall correctly in any position will help you to prevent injuries.  It is also an effective way to train bodily awareness, coordination, and quickness.  Another benefit of having good falling skills is that once you have learned and mastered the skill of falling, you will be more relaxed and confident during practice or in a fight.</p>
			<p><b><i>For all falling training a safety mat is recommended.</i></b></p>
			<h4>1) On Ground Body Position</h4>
			<p>This is a very typical body position used for self protection when you fall.  Before you land on the ground, tuckin your chin, contract your body so that the torso is rounded, bend your arms, and use your hands to cover both sides of your head.  Land on your side to protect the spine.  At the moment of impact, tense up your body by holding your breath to prevent internal organ injury from the force of the landing (Figure 2-61).</p>
			<div align="center"><img src="../../images/newsletter/v3n5/a4p1.jpg" border="0" height="352" width="500" alt="Falling- Part 1"></div>
			<h4>2) Rolling Forward</h4>
			<p>Start with the right foot as the leading foot.  Please your right arm at the side of your foot with fingers pointed inward, and shoulder tucked in.  Place your left arm in front of your body, keep your chin tucked in, and the right arm slightly bent with the elbow pointing outwards (Figure 2-62).  Roll forward over your right arm, then your shoulder, and then diagonally across the back (Figures 2-63 and 2-64).  Repeat the same movement for the left side.</p>
