	        <h2>By Tony Bujas</h2>
	        <br><br>
			<p>We were in class the other day practicing the Xiao Yao Shuai form. This is primarily a Chinese Wrestling grappling/throwdown based form.  You have the freedom to do the form internally, externally, fast, slow, in-between, etc.  Normally we put an internal flavour to the form and then stress the applications with a partner(s).  I was concentrating on putting more body movements and flair into my form.  Sifu was watching us do the solo form and then his face lit up with a smile.</p>
			<p>"Tony" he said to me, "I was watching you enjoying yourself, doing the form, good, BUT I saw your head and your shoulders moving too much."</p>
			<p>"Even though you have freedom with the form, you still need to follow internal principles when performing internal arts.  For example, your head needs to remain upright, the Baihui feeling as if a string is connecting it to heaven.  And your chin should not stick out; it needs to be gently pulled in.  Remember to not let it wobble sideways or up and down.  The eyes - not too intense.  Shoulders relaxed and down, chest humble.  It's good to work on your body movements, but, you must keep the principles!"</p>
			<p>I too smiled and thanked him for the input.  In my zeal to improve my body movements I neglected to keep to the internal basics.  Two days later in one of the Chen Taiji classes, the lesson was reinforced for me. Sifu, was teaching us the form and telling us about being diligent to the principles and staying open to input.</p>
			<p>"Sometimes people think they are doing everything correctly but they are not.  If someone tells them they're sticking out their chin, they might get angry or just ignore you.  But if they get feedback from a mirror, or a videotape of themselves, or their Sifu, then they need to face the truth and work even harder to improve.  You need to follow and be good in the basic principles, remaining ever vigilant..."</p>
			<p>It seems to me that at times in your training you do need to focus on a particular aspect and improve in it.  Then you can move on to another aspect of training and so on.  Now as you're developing and integrating these aspects into your self, you have to look at the big picture and ask yourself: "Am I still in tune with the basic principles?"  I think you can ask this for any and all martial arts as well as Qigong and meditation.</p>
			<p>Is my posture correct?  My head & neck on straight?  My focus proper?  My eyes, ears, nose, mouth, & tongue sensing? Chest humble? Shoulders, arms, elbows, wrists, fingers, spine, waist, legs, knees, feet, toes, and all the other things I haven't mentioned in line with the principles?  Remember the achievements you've made to get this far in your progress! Don't get overwhelmed by the climb the ahead.  Step by step!</p>
			<p>At some point, it will be time for you to do some in-depth self-study on the principles and also to studyup "the classics" - and then apply.  You will need to be open to getting feedback from your Sifu, your friends, your mirror, your videotape and adjusting your self accordingly.  Some effort required, I'd say!  AND I'd also say that you can stay lit up inside smiling, ENJOYING every step of the way!  Happy Trails!</p>
