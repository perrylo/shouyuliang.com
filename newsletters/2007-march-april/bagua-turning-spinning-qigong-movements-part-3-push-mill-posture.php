			<br />
			<h3>Push Mill Posture (Tui Mo Shi)</h3>
			<p><b>Verbal Secrets:</b><br />
			Exhale to push, inhale to pull; turn the mill with round motion.</p>
			<p>In Chinese martial arts, in order to efficiently manifest the Qi into physical Jin, a practitioner must learn how to use reverse breathing (or Daoist Breathing). In reverse breathing, when you inhale, you withdraw your abdomen, and when you exhale, you expand your abdomen. Reverse breathing is part of our natural breathing cycle. Usually, we use normal abdominal breathing (or Buddhist Breathing). Normal abdominal breathing expands the abdomen on the inhale, and contracts it on the exhale. However, when you are emotionally aroused, your breathing can change automatically into reverse breathing. For example, when you are happy and laugh, the laughter itself is accompanied by abdominal expansion. Conversely, when you are sad and cry, inhalation is accompanied by abdominal contraction. Other than emotional disturbance, you will also change to reverse breathing automatically whenever you intend to manifest physical power. For example, when you are blowing a balloon, or when you are lifting a weight, your abdomen will expand as you exhale.</p>
			<p>From this premise, we can infer that, since exhalation in reverse breathing facilitates the manifestation of physical power, then inhalation in reverse breathing can allow the storage of energy for later manifestation as such physical power. It is extremely important in such practice to maintain breathing in a natural, comfortable, continuous and easy manner. You must never force either your breath or energy in any way.</p>
			<div class="small" align="center"><img src="../../images/newsletter/v7n2/a1p1.jpg" width="500" height="240" border="0" alt="Bagua Turning-Spinning Qigong - Bagua Zhuan Xuan Gong"><br />Bagua Turning-Spinning Qigong - Left Push Mill</div>
			<p><b>Movements:</b><br />
			<i>Left Push Mill:</i><br />
			Bring your left foot next to the right foot, turning your palms down; inhale (Figure 3-4). Step to your front left corner with your left foot. Shift your weight forward into Bow stance while circling your palms horizontally and counterclockwise in an arc; exhale (Figure 3-5). Shift about 60% of your weight back to your rear leg, while circling your palms back next to your abdomen; inhale (Figure 3-6). Continue shifting your weight forward and back, coordinating your movements with exhalations and inhalations. Do eight complete revolutions. To complete the Left Push Mill, shift all your weight back to your right leg, bring your left foot next to your right foot and stand up; inhale (Figure 3-7).</p>
			<div class="small" align="center"><img src="../../images/newsletter/v7n2/a1p2.jpg" width="500" height="239" border="0" alt="Bagua Turning-Spinning Qigong - Bagua Zhuan Xuan Gong"><br />Bagua Turning-Spinning Qigong - Right Push Mill</div>
			<p><i>Right Push Mill:</i><br />
			From Figure 3-7, step to your front right corner with your right foot. Shift your weight forward into Bow stance while circling your palm horizontally and clockwise arc; exhale (Figure 3-8). Shift about 60% of your weight back to your rear leg, while circling your palms back next to your abdomen; inhale (Figure 3-9). Continue shifting your weight forward and back, coordinating the movements with exhalations and inhalations. Do eight complete revolutions. To complete the Right Push Mill, shift all your weight back to your left leg, bring your right foot next to your left foot and stand up; inhale.</p>
			<p><b>Key Notes:</b><br />
			Imagine that you are holding the handle of a mill, rotating it around and around. You can also imagine that your palms are connected to the ground by a column of Qi, leading it around in circles. As you circle your palms, shift your weight slowly and evenly. Exhale and inhale as your complete each circle. Be careful not to extend your knees over your toes as you shift your weight forward, and not to extend your lower back past your heel as you shift your weight backward.</p>
