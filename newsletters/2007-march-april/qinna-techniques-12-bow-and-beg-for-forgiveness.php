			A. The pressure is on your opponent's left elbow. By placing your arm pit directly on his elbow, you make his elbow the pivoting point of the arm, and make it bend in an unnatural direction. As you lower your body to exert pressure on his arm, make certain that his elbow continues to be the pivoting point by pulling your hands up and pushing his body down, creating pressure in opposite directions.</p>
