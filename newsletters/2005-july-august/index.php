				<h1 class="idiom" style="font-size:18px;"><img src="../../images/newsletter/v5n4/idiom.gif" width="130" height="32" border="0" alt="yi gu zuo qi - The soldier's morale is at its highest with the first beat of the battle drum." style="margin-top:5px;margin-bottom:5px;"><br>   y&#299; g&#365; zuò qì</h1>

				<p><b style="font-size:13px;">"The soldier's morale is at its highest with the first beat of the battle drum."</b></p>
				<p>This idiom is used to encourage people to press on and finish something without let-up while their spirits are rising.</p>
				<p>During the Spring and Autumn Period (770-476 B.C.), the state of Qi invaded the state of Lu. Duke Zhuanggong of Lu, accompanied by Cao Gui, personally commanded his troops in resistance against the enemy. When the battle was joined, Qi's army was the first to beat the drum for attack. The Duke was about to sound the drum and throw his men into action when Cao advised him not. Not until the other side had drummed thrice did Cao advise the Duke to begin the counter-attack during which the Qi army was routed.</p>
				<p>After the victory, the Duke asked Cao why he had given such advice. "It is the morale of men that counts in battle," replied Cao. "At the first drum, their courage is roused and is at its highest. With the second drum, it begins to flag, and with the third it runs out. When the enemy's courage ran out with the third beat of the battle drum, ours was rising ever higher with the first beat for charging forward. So we won at one go."</p>
				<p class="small"><b>Zuo Qiuming's Chronicles</b> - A classical history of the Spring and Autumn Period (770-476 B.C.). Its author, Zuo Qiuming of the state of Lu, lived during the closing years of that period.</p>
