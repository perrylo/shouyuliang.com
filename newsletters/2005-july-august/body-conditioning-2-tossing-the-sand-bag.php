			<h3>Training Method:</h3>
			<p><b>Solo Practice</b></p>
			<p>Tossing Left and Right:</p>
			<p>Stand with both feet shoulder width apart and knees slightly bent. With your left hand grab the sand bag at its center (Figure 3-8) and throw it up above your head to your right (Figure 3-9). Then grab the center of the bag with your right hand while it is still in the air. Then throw it up to your left (Figure 3-10). Repeat as many times as you want. When you are comfortable with the exercise, you can add footwork like Front Cross Step and Back Cross Step while you throw and grab the bag. Make sure to grab the bag's center.</p>
			<p>Tossing Around the Back:</p>
			<p>Stand up with both feet shoulder width apart and your knees slightly bent. With your right hand grab the bag and toss it behind your back and over your head so it will be in front of you to your left. Grab the bag in the air with your left hand and toss it to the right over your head so it will be in front of you to your right. Grab the bag with the right hand and repeat the exercise (Figures 3-11 and 3-12). When you are comfortable with this exercise, you can add footwork while you toss and grab the bag.</p>
			<p>Tossing Between the Legs:</p>
			<p>Stand in Horse Stance. Grab the bag with your right hand and toss it up between both legs from outside of your right leg (Figures 3-13 and 3-14). Grab the bag in the air with the left hand (Figure 3-15) and then toss it up between both legs from the outside of the left leg. Grab the bag with the right hand and repeat the exercise.</p>
			<p align="center"><img src="../../images/newsletter/v5n4/a6p3.jpg" width="500" height="235" border="0" alt="Body Conditioning - Tossing the Sand Bag"></p>
			<p><b>Two Person Exercise</b></p>
			<p>Two persons face each other. Your partner tosses the bag and you catch it with your right hand. Then toss it up above your head to your left (Figures 3-16 and 3-17). Grab the bag at its center with your left hand while it is still in the air (Figure 3-18). Toss the bag back to your right hand and throw it back to your partner (Figures 3-19 and 3-20). Adjust your distance accordingly. You can throw directly to each other, from behind your back to each other, or between the legs to each other. Be creative and have fun with this exercise.</p>
