	        <br><br>
			<p><b>Goal:</b> To increase your squeezing, grabbing, and pulling power. Also to increase body movement coordination.</p>
			<p><b>Equipment:</b> Use fine canvas to make a bag about 16x16 inches. Fill the bag about one third of the way with sand. Gradually add more sand as your strength increases.</p>
			<p align="center">
				<img src="../../images/newsletter/v5n4/a6p1.jpg" width="500" height="193" border="0" alt="Body Conditioning - Tossing the Sand Bag"><br />
