				<p>During the Spring and Autumn period, Duke Daogong of Jin acted on the proposal of his minister Wei Jiang and became leader of an alliance of the ducal states on the Central Planes.  Soon afterwards, the state of Zheng surrendered to the southern state of Chu in violation of the oath of alliance.  The resentful Duke Daogong allied with other states in preparation for an attack on Zheng.  Too weak to resist, Zheng sued for peace and Jin agreed to the specific terms.</p>
				<p>To express its thanks on Jin, the state of Zheng sent to it an envoy bearing quantities of gifts.  Duke Daogong gave some of the gifts to Wei Jiang, who refused them and took the opportunity to tell his sovereign, "It's the outstanding capabilities of Your Grace that have made you the acknowledged leader of the multi-state alliance.  I've done nothing to deserve Your Grace's reward.  I would like, however, to offer a piece of advice.  When circumstances are favorable, never forget that difficulties and dangers might crop up any time.  One should always be prepared to cope with any situation and all eventualities."</p>
