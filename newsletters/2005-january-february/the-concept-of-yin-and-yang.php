			<h3>Balance of Yin and Yang = Health</h3>
			<p>There's yin and yang energy in our bodies.  The level of yin and yang is constantly changing.  Sometimesthere ismore yin and at other times, more yang.  However, if for some reason, the level of yin super exceeds that of the yang, there will be an imbalance, hence symptoms of disease will arise.  For example, there will be feelings of cold, lack of energy, diarrhea, and so forth.  If this imbalance is not addressed, the yang could be further consumed by the yin.</p>
			<p>An external invasion of cold into the body can also alter the level of yin and cause an imbalance ofyin and yang.  When this happens there may be symptoms of pain and so forth.  For example, if one consumes too much food with cold energy, one could experience abdominal pains.</p>
			<h3>Achieving and Maintaining the Balance of Yin and Yang</h3>
			<p>The normal physiological activities of the body is the result of a state of perfect harmony between yin and yang.  The disruption of the equilibrium of yin and yang in the body results in disease.</p>
			<p>The goal of Traditional Chinese Medicine (TCM) is to address this disequilibrium, hence, achieving and maintaining the balance of yin and yang to bring about health.  Acupuncture and moxibustion, Chinese herbal remedies and Qi-gong can be used to adjust the level of yin and yang, thus bringing them into a state of harmony.</p>
			<h3>The Chinese word "ping"</h3>
			<p>The Chinese word "ping" means balance.  It can also be translated into English as peace.  Interestingly,the Hebrew word for peace is "shalom" which carries the connotation of health.</p>
			<p>Shalom refers to health, prosperity, harmony or balance, and well being (Strong's, 1990).  Thereis safety from troubles, terror or fears, oppressions, bondages, diseases and accidents.  In other words, when you have shalom or are in shalom, you will be far from anything evil or harmful to your well being.</p>
