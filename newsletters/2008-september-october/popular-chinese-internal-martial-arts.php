			<p>Popular Chinese Internal Martial Arts</p>
			Because all Chinese martial styles utilize some Qigong training, it is difficult to distinguish the external styles from the internal ones. Traditionally, almost all of the Chinese martial styles were taught in secret, and it was not until the last hundred years that these secrets were gradually revealed to the general public. Even today, there are many styles that are still taught secretly. Because of this conservatism, most people (including many Chinese martial artists) simply don't have enough information to clearly distinguish between styles.
			There are four generally known styles which emphasize Qi development more seriously than other styles, and are therefore considered internal. These four styles are Taijiquan, Xingyiquan, Baguazhang, and Liu He Ba Fa. Before we discuss the differences between these styles, we would first like to point out their similarities. First, they all concentrate on training the circulation of Qi and building it up to a higher level. Second, they all emphasize a calm and peaceful mind. And finally, all four styles are very effective in improving health.
			<br>
			<h3>Taijiquan:</h3>
			<ol type="A">
				<li>In order for the Qi to move freely and smoothly in the physical body, the body must be relaxed from the skin to the bone marrow and the internal organs. In order to lead the Qi to any part of the body without stagnation, the body must be relaxed, and the movements soft, like a baby's.</li>
				<li>When Jin is emitted for an attack, it is like a whip. Though soft, the power is strong and penetrating.</li>
				<li>The fighting strategy is more defensive than offensive. This means that defense is often treated as the preparation for an attack. Because of this, training focuses on yielding, neutralizing, sticking, adhering, and coiling. The movements are always rounded. "Pushing hands" practice leads the practitioner towards this goal.</li>
				<li>Strategy and techniques indicate that Taiji specializes in fighting mostly within the short and middle ranges. Almost all of the kicks trained in the Taiji sequences focus within these ranges.</li>
			</ol>
			<h3>Xingyiquan:</h3>
			<ol type="A">
				<li>In order to enable the Qi to move freely and smoothly in the physical body, the body must be natural and comfortable. In the beginning of both attacking and defensive movements, the body remains soft so that Qi can be led to the limbs. The body is then stiffened for an instant upon striking, in order to manifest the Jin. Xingyi Jin is like rattan, soft at the beginning and hard at the end. Jin manifests like a cannonball exploding.</li>
				<li>The fighting strategy is more active than passive. Offensive movement is usually used as a defense. Although techniques such as yielding, neutralizing, sticking, adhering, and coiling are used, the attacking mind and movement remain paramount. In order to keep up momentum, straight forward and backward movements are emphasized, although some dodging and sideward movements are used.</li>
				<li>Because of the strategy and techniques emphasized, Xingyi can be very effective when fighting at short range. Though some kicks are trained, almost all of them are directed at targets below the groin.</li>
			</ol>
			<h3>Baguazhang:</h3>
			<ol type="A">
				<li>The movements of Baguazhang are not as soft as Taijiquan, yet they are not as hard as Xingyi. The internal Qi is the main focus of the training.
				<li>The fighting strategy emphasizes circular movements. Generally, both the stepping and the techniques are circular. Although many techniques such as yielding, neutralizing, sticking, adhering, and coiling are used, they are mainly adopted to coordinate with the round movements. Attack and defense are equally important. Rounded defensive movements are usually used first, followed by rounded attacking movements, to uproot the opponent and make him fall.</li>
				<li>Because of its strategy and techniques, Baguazhang can be effective at all ranges. Round stepping movements are constantly used in coordination with the techniques, but high kicks are seldom used. Instead, the training focuses on low kicks and firm rapid walking.</li>
			</ol>
			<h3>LiuHe BaFa:</h3>
			<ol type="A">
				<li>Liu He Ba Fa is a combination of the strategy and techniques of Taijiquan, Xingyiquan, and Baguazhang. The training contains soft within the hard and hard within the soft. Its strategy contains straight line forward and backward, as well as circular movements. It utilizes all three fighting ranges, but does not emphasize high kicking techniques. It is normally taught to people who have already learned the three previous styles, because they are more likely to understand the essence of the three and mix and apply the techniques skillfully and effectively.</li>
			</ol>
