			<h3>Part 1. Xi Sound to Regulate the Triple Burner</h3>
			<h4>Movement and Intention:</h4>
			<p>Step 1. Use Drawing 1-14 as a reference. Stand with your feet shoulder width apart, and arms naturally down at your sides. Inhale, bring both arms up along the front of your body with your hands facing up and the fingers of both hands pointing toward each other (Figure 1-53). As your hands reach your shanzhong point, rotate your hands until they are facing up.</p>
			<p>Use your mind to lead the qi up from your qiaoyin point on the Gall Bladder Channel, passing quixu, xiyangguan, huantiao, and into the Triple Burner.</p>
			<p>Step 2. Exhale, and make the Xi (Seeeeee) sound. The sound is made with your upper and lower teeth close, but not touching, and your tongue gently pointing down. As you make the sound, extend your arms up above your head, hold up your huiyin area, and pull your abdomen in, as you exhale all the impurities from your body (Figure 1-54).</p>
			<p>Use your mind to lead the impurities out of your Triple Burner, passing quepen on the Stomach Channel and jianjing on the Gall Bladder Channel. Continue along the Triple Burner Channel, passing jianliao, tianjing, waiguan, and guanchong on the lateral side of your fourth finger.</p>
			<p>Step 3. After completing your exhalation, rotate your hands until they are facing in with your fingers pointing up, and lower them slowly in front of your body. When your hands pass your shanzhong point, point your fingers down, while continuing to lower your hands down until they are at your sides. Inhale and exhale naturally. Bring your mind back to your qiaoyin point on the Gall Bladder Channel on your fourth toe. Repeat Steps 1 to 3, 6 times.</p>
