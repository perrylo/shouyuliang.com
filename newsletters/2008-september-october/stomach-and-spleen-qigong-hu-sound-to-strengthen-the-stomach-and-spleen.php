			<h4>Intention:</h4>
			<p>Use Drawing 1-13 as a reference. During inhalation, use your mind to lead the qi from your yinbai point on your big toe, the beginning of the Spleen Channel, to dadu, to gongsun, to sanyijiao, to xuehai, to jimen, along the inside of your leg, and up to your spleen.</p>
			<p>When exhaling, use your mind to lead the impurities from your spleen out, passing your zhourong point towards your daboa point. When exhaling make a Hu sound; while drawing your stomach in and holding up your huiyin area, squeeze out the impurities from your spleen.</p>
			<h4>Key Points:</h4>
			<p>This technique is also effective without using your intention, provided that you hold up your huiyin area and draw in your abdomen to squeeze out the impurities from your spleen.</p>
