			<h4>Movement:</h4>
			<p>Stand naturally upright. Inhale and bring your hands next to your shanzhong point, with your palms facing up and fingers pointing at each other (Figure 1-51). Exhale and rotate your right palm out and up, and rotate your left palm in and down; then extend both hands up and down (Figure 1-52).</p>
			<p>Then inhale and bring both hands down facing each other at your shanzhong point. Next rotate your left palm out and up, and rotate your right palm in and down. Then extend both palms up and down and exhale as you did on the other side. Repeat a total of 6 inhalations and exhalations.</p>
