							<p><i>Describing a man of fortitude who is not afraid of difficulties.</i></p>
							<p>THE Foolish Old Man of North Mountain was almost ninety. His house faced two mountains, the Taihang and the Wangwu, which obstructed his way. One day, he called all his family together and said to them, "Let's work together, remove the two mountains and open a road. How do you like it?" Everybody agreed. Only his wife had some doubts. She said, "With the few of you, you can hardly level a small mound. How can you remove two great mountains? Besides, where are you going to put the rocks and earth?" "We'll move the rocks and earth to the shore of the Bohai Sea," the others said.</p>
							<p>The following day, the Foolish Old Man and his family began to dig.</p>
							<p>Another grey-beard, known as the Wise Old Man, saw them working so hard and said derisively, "How silly you are! At your age, you can hardly pull out a few blades of grass on the mountains. How can you dig up the mountains?"</p>
							<p>The Foolish Old Man uttered a deep sigh and said, "How can you be so helplessly foolish. It's true that I'm old and cannot live very long. But when I die, my sons will carry on. When they die, there will be my grandsons, and then their sons and grandsons and on to infinity. High as they are, the mountains cannot grow any higher. Why is it that we cannot flatten them?" The Wise Old Man was not able to answer him.</p>
