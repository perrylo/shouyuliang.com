			<ol type="a">
				<li>Gray throws a right round house kick to White's left. White bends his left arm so his elbow points out. White's right arm leans against his upper left arm and the right palm faces outward to block Gray's kick. This is called Palm and Elbow Crossing Method (Figure 4-12).</li>
