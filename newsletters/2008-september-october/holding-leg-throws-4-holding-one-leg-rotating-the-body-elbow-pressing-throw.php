				<li>With his left foot, White steps behind the right leg. White turns quickly to the left and uses his right elbow to press against Gray's thigh to make Gray twist and fall. (Figure 4-14)</li>
			</ol>
			<p><b>Key Points</b></p>
			<ul style="list-style:disc">
				<li>When stepping behind your right foot, keep his right leg tightly locked and pull it towards the rear left corner. Your body weight should be more on the right leg to form a Bow Stance to keep good balance.</li>
				<li>Your right arm or elbow should press down on your opponent's leg to keep him off balance before the throw.</li>
				<li>When turning to the left, your body weight should shift from right to left as quickly and smoothly as possible.</li>
