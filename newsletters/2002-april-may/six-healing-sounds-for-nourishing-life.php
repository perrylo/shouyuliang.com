          <h1 class="contentheading">Six Healing Sounds for Nourishing Life</h1>
          <h2>By Grandmaster Shou-Yu Liang and Master Wen-Ching Wu</h2>
          <br><br>
          <p>The practice of the Six Healing Sounds for Nourishing Life has a long history.  As early as the Qin Dynasty (221-207 B.C.) there has been a record of healing sound practices.  During the Sui Dynasty (590 - 618 A.D.), a Taintai Buddhist high priest, Zhi Zhuan, pointed out the healing potential of the Six Healing sounds.  The Six healing Sounds were recorded as <i>Chui, Hu, Xi, Ke, Xu, and Si.</i></p>
          <p>In the Tang Dynasty (618-906 A.D.) a medical expert and doctor of TCM, Sun Si-Miao, wrote in the Songof Hygiene about the Six Healing Sounds and can be paraphrased as follows:</p>
          <blockquote>The Liver and Spring are classified as Wood elements; the Xu sound in the Spring will brighten the eyes and relieve liver stagnation.  The Heart and Summer are classified as Fire Elements; the Ke sound in the Summer will relieve fire in the heart. The Lungs and Fall are classified as Metal elements; the Si sound in the Fall will nourish the lungs. The Kidneys and Winter are classified as Water elements; the Chui sound in the Winter will keep the kidneys at ease.  The xi sound will regulate the Triple Burner and eliminate annoying heat.  The Hu sound during the four seasons will assists the assimilation of food by the spleen.  It is not necessary to make any noise when you practice.  The benefit is greater than miraculous pills.</blockquote>
          <p>Grandmaster Liang's mother, Ms. Huang Zhe-Xi, was very sickly in her youth.  She went to Huang Mountain to learn the Six Healing Sounds.  Later, she also studied from the well-known Qigong master, Master Ma Li-Tang. From then on, her health got better and better and she was filled with energy.</p>
          <p>The practice of the Six healing sounds is very flexible.  You can practice all six sounds in order oronlypracticespecific sounds to treat specific conditions in the body.  You can also practice the sounds according to the seasons of the year.  If you practice all six healing sounds, you should practice them in this order: Xu, Ke, Hu, Si, Chui, and Xi.</p>
