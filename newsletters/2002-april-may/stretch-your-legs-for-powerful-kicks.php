	        <h1 class="contentheading">Stretch Your Legs for Powerful Kicks</h1>
	        <h2>By Grandmaster Shou-Yu Liang and Master Wen-Ching Wu</h2>
	        <br><br>
	        <p>Leg stretches are for developing the flexibility, agility, and power of the legs.</p>
			<img src="../../images/newsletter/v2n3/a6p1.jpg" border="0" alt="Stretch Your Legs for PowerfulKicks">
			<h4>1. Front Pressing Leg Stretch (zhengnyatui)</h4>
			<p>Stand in front of a supporting structure about the height of your waist.  Stand with your feet together and toes pointing forward.  Place one foot on the support with your toes pointing upward.  Keep your legs straight and hips square facing the supporting structure.</p>
			<p><b>Drill 1:</b> Place your palm over your knee (Figure 1-23).  Gently and rhythmically lean forward, thenbackup. Gradually increase the depth of the lean.  Do as many times as you like, then change legs and do the other side.  Gradually over time, increase your flexibility until your forehead, nose, and chin are able to touch the tip of the toes (Figure 1-24).</p>
			<p><b>Drill 2:</b> After you have done the stretch, bend your knee and hold your knee up with your arms in frontof your chest for about 10 seconds.  Next, extend your leg straight and hold it above the supporting structure for about 10seconds.  This training will strengthen your legs and improve your ability to control your legs.</p>
			<br clear="all">
			<img src="../../images/newsletter/v2n3/a6p3.jpg" border="0" alt="Stretch Your Legs for PowerfulKicks">
			<h4>2. Side Pressing Leg Stretch (ceyatui)</h4>
			<p><b>Drill:</b> Stand sideways in front of a supporting structure about the height of your waist.  Stand with the support to your side.  Place one foot on the support with toes pointing upward.  Keep the other foot on the floor with your toes turned out slightly.  Gently and rhythmically lean sideways, then up, with your upper body.  Gradually increase the depth of the lean.</p>
			<p>Do this stretch as many times as you like, then change legs and do the other side.  Keep both legs straight during the stretch.  Gradually over time, increase your flexibility until your upper body is parallel with the stretched leg (Figure 1-25).</p>
			<p align="center"><img src="../../images/newsletter/v2n3/a6p2.jpg" border="0" alt="Stretch Your Legs for PowerfulKicks"></p>
			<h4>3. Backward Pressing Leg Stretch (houyatui)</h4>
			<p><b>Drill:</b> Stand facing away from the supporting structure with your feet together. Lift one leg up from behind you and lace your foot on the support.  Gently and rhythmically lean back, then up, with your upper body. Keep both legs straight and hips square during the stretch.  Gradually increase the depth of the lean.  Do this stretch as many times as you like, then change legs and do the other side.  Gradually over time, increase your flexibility by stretching further back (Figure 1-26).</p>
			<h4>4. Half-Squat Stretch (pubu yatui)</h4>
			<p><b>Drill:</b> Stand with your feet wide apart.  Bend your right knee and squat down as low as youcan,while keeping your left leg straight.  Keep both feet flat on the floor and toes pointing forward.  Then use your hands to hold on to the outer edge of your feet (Figure 1-27).</p>
			<p>Hold this posture for about 30 seconds, then change sides by bending your left knee and straighteningyourright leg.  You may also alternate from side to side without holding the posture for 30 seconds.</p>

			<h4>5. Leg Lifts</h4>
			<p><b>Front Lift Stretch (zhengbantui):</b> Bend your left knee and lift it up with your right hand (Figure1-28).  Then straighten your left leg by extending your left foot forward (Figure 1-29).  As an alternative you may have an assistant help hold you leg up as high as your flexibility will allow (Figure 1-30).  Hold the stretch for about 30 seconds, then change legs.</p>
			<p align="center"><img src="../../images/newsletter/v2n3/a6p4.jpg" border="0" alt="Stretch Your Legs for PowerfulKicks"></p>
			<p><b>Side Lift Stretch (cebantui):</b> Hold on to the back of your right leg with your right hand. Liftit up sideways as high as you can (Figure 1-31).  As an alternative, you may have an assistant help hold your leg up, as high as your flexibility will allow (Figure 1-32).  Hold the stretch for about 30 seconds, then change legs.</p>
			<p><b>Back Lift Stretch (houbantui):</b> Stand facing a supporting structure with your feet pointingforward.Place your hands on the support and have an assistant lift one of your legs up from behind you, as high as your flexibility will allow (Figure 1-33).  Keep both legs straight and hips flat during the stretch.</p>
			<p align="center"><img src="../../images/newsletter/v2n3/a6p5.jpg" border="0" alt="Stretch Your Legs for PowerfulKicks"></p>
			<h4>6. Splits</h4>
			<p><b>Regular Split Stretch (qianhoupicha):</b> Place your hands on the floor.  Place your left leg in frontand yourright leg behind.  Start by straighten your front leg, then gradually straighten your back leg.  When you are able to do a complete split, keep your front foot pointing up and the inside edge of your back foot facing down, and extend your arms to your sides (Figure 1-34).  Hold this stretch for about 30 seconds, then change sides.  As your flexibility gets better, you may also lean forward and backward while holding this stretch.</p>
			<p>Straddle Split Stretch (zhengpicha): Sit on the floor with your feet as wide apart as you can andleanforward.Gradually increase your flexibility until you are able to keep your legs 180 degrees apart.  Then stretch your arms to your sides and put your feet flat on the floor (Figure 1-35).</p>
