			<h3>Part 1. Ke Sound To Ease the Heart Fire</h3>
			<p>The traditional Ke Sound Qigong is capable of balancing heart fire. It is especially effective in treating and healing heart diseases.</p>
			<h4>Movement and Intention:</h4>
			<p>Step 1. Use Drawing 1-12 as a reference. Inhale naturally and hold your breath. Raise both arms up from your sides, palms face down. Relax your shoulders; keep your wrists and elbows, naturally bent and relaxed (Figure 1-36).</p>
			<p>When your arms are even with your shoulders, rotate your palms until they are facing in and circle them towards your chest. Then rotate your palms until the fingers of both hands are pointing at each other, without touching, and palms face down (Figure 1-37).</p>
			<p>While holding your breath, use your mind to lead qi up from your yinbai point on the Spleen Channel, passing through your shangqiu, your sanyinjiao, and your yinlingquan points. Then lead the qi up the inside of your thighs and into your abdomen. Continue up into your Heart Channel.</p>
			<p>Step 2. Exhale and vocalize the sound Ke, with your mouth half open, tongue touching the bottom of your mouth, and exerting a slight pressure with your jaw. At the same time, lower both palms down until your abdomen level. Hold up your anus area and draw your abdomen in.</p>
			<p>During exhalation, lead qi from your Heart Channel through your shaohai point, to your shenmen point, and out through your shaochong point at the tip of your little finger. Exhale out all the impurities in your Heart Channel.</p>
			<p>Repeat Steps 1 and 2, 6 times. This completes a set. Do three sets.</p>
