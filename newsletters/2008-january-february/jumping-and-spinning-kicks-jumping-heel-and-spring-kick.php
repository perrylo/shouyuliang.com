			<h3>Jump Flying Kick</h3>
			<p>This is a long range kick with the top of the foot. It is also referred to as Jump Slap Toe Kick. When the kicking foot is hit on the top by the opposite hand, it is referred to as teng kong xie feijiao, meaning angled Jump Flying Kick. To increase the challenge, the speed, and the difficulty of this kick, try to kick and land with the same leg.</p>
			<p>Drill: Stand on your right leg with your left knee up, and your arms extended forward and back, left arm in front (Figure 2-72). Do a Hit Step, jibu, by stepping forward and down with your left, then push off with your left foot and hit your back foot behind your front foot. At the same time, bring your arms towards your chest, then extend them forward and back (Figure 2-73).</p>
			<p>Land on your right foot, lift your left knee up as high as you can, and slap the back of your right hand with the front of your left hand over your head (Figure 2-74). Continue the momentum, kick up with the top of your right foot and hit down on the top of your foot with your right hand. At the same time, hook your left hand back (Figure 2-75). Continue down the training area, then change legs and do the other side.</p>
			<h3>Jump Spring Kick</h3>
			<p>This is basically the same as a Spring Kick, except that it is done with the addition of a skip forward or a jump forward. It can also be done with the nonkicking leg bent at the knee. Typically, the first leg lift or kick helps generate the lift for the second kick.</p>
			<p>Drill: Start with your feet together and fists at your sides. Spring Kick with your left leg and punch with your right fist. Continue the lift of your left kick and kick with your right leg. At the same time, punch with your left fist. As you kick with your right leg, simultaneously bring your left knee up as high as you can and your right hand back to your waist (Figure 2-76). Continue down the training area, then change legs and do the other side.</p>
			<h3>Jump Heel Kick</h3>
			<p>This kick is done the same as Jump Spring Kick. The only difference is that the kicking surface is the heel and not the top of the foot (Figure 2-77).</p>
