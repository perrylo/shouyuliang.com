			<div class="small" style="float:right;width:272px;margin-left:15px;"><img src="../../images/newsletter/v8n1/baguazhang-bow-turn-posture-stepping-pattern.jpg" width="272" height="400" border="0" alt="Bagua Turning-Spinning Qigong - Bore and Turn Posture"><br /><b>Bagua Turning-Spinning Qigong - Bore and Turn Posture</b></div>
			<p><b>Verbal Secrets: Arcing and swinging walking in the mud along the edge of a circle.</b><br />Arcing and swaying steppings (Kou Bai Bu) are two of the most basic walking methods in Baguazhang.  In order to walk firmly and smoothly along the edge of a circle, in addition to using the arcing and swaying steppings, you must also image that you are walking on muddy ground. Refer to Figure 3-36 for the stepping pattern.</p>
			<p>Movements:</p>
			<p>From Figure 3-35, inhale; then step forward with your left foot and rotate your palms to your id u in the Rotate Palm Posture; exhale (Figures 3-37 and 3-38).</p>
			<p>With your palms fixed in place, turn your left foot out slightly and step in an arc with your right foot, foot turned in, (Figure 3-39). Step with your right foot in an arc, foot turned in (Figure 3-40). Next bring your right foot next to your left, foot turned in. Both feet are pigeon toed (Figure 3-41).</p>
			<div class="small" align="center"><img src="../../images/newsletter/v8n1/baguazhang-qigong-bow-turning-posture-1.jpg" width="623" height="610" border="0" alt="Bagua Turning-Spinning Qigong - Twist Turning Posture"><br /><b>Bagua Turning-Spinning Qigong - Twist Turning Posture</b></div>
			<p><b>Right Overhand Boring:</b><br/>
			Turn your body until your arms are in front of your chest and begin to lowering your left palm (Figure 3-42). Lower your left palm and execute a an overhand Chuan with your right palm. At the same time step forward with your left foot (Figure 3-43). Step in an arc with your right foot while extending and rotating your palms to your right (Figure 3-44). With your arms fixed in place, walk in a circular path for 5 more steps. On the fifth step bring your feet next to each other, both feet turned in (Figures 3-45 to 3-49).</p>
			<div class="small" align="center"><img src="../../images/newsletter/v8n1/baguazhang-qigong-bow-turning-posture-2.jpg" width="622" height="609" border="0" alt="Bagua Turning-Spinning Qigong - Twist Turning Posture"><br /><b>Bagua Turning-Spinning Qigong - Twist Turning Posture</b></div>
			<p><b>Left Overhand Boring:</b><br/>
			Turn your body until your arms are in front of your chest, and begin to lower your right palm (Figure 3-50). Continue to lower your right palm and execute an overhand Chuan with your left palm. At the same time step forward with your right foot (Figure 3-51). Step in an arc with your left foot while extending and rotating your palms to your left (Figure 3-52). With your arms fixed in place walk two steps in an arc (Figures 3-53 and 3-54).</p>
			<div class="small" align="center"><img src="../../images/newsletter/v8n1/baguazhang-qigong-bow-turning-posture-3.jpg" width="634" height="611" border="0" alt="Bagua Turning-Spinning Qigong - Twist Turning Posture"><br /><b>Bagua Turning-Spinning Qigong - Twist Turning Posture</b></div>
			<p>At this point, you should be almost back to the starting location as in Figure 3-38. Next walk three steps in an arc as in Figures 3-39, 3-40, and 3-41. This will bring you to a ready position to do another right overhand Chuan. Repeat the right and the left overhand boring 7 more times.</p>
			<p>To end this posture, after completing your last left overhand boring, follow the movements described in the next posture.</p>
			<p>Key Notes:</p>
			<p>When boring and turning, the movements must be smooth and continuous, without any pauses. Once the movements are smooth, you may wish to add a few more "sliding" steps.</p>
