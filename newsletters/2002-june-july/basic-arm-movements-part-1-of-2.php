			<p>With this first part of a two-part introduction, we will explain basic arm movements found in many Wushu applications.</p>
			<h3>Fist</h3>
			<img src="../../images/newsletter/v2n4/a6p1.jpg" alt="Basic wushu arm movments - the fist" align="right">
			<p><b>Posture:</b> A fist is accomplished by coiling all the fingers in towards the center of the palm with the thumb tightly pressed on the top of the index and the middle fingers.  The different sections of the fist are described with different names.  They are the <i>face, back, heart, eye, and wheel</i> of the fist (Drawing 1-1)</p>
			<br clear="all">
			<p><b>Drill 1:</b> Alternate hands punching while in Standing Stance with feet apart  (<i>kailibu chongquan</i>).  Start with you your fists at your waist.  When you punch, rotate your arm forward until the <i>heart</i> of the fist is facing down (Figures 1-63 and 1-64).</p>
			<p align="center"><img src="../../images/newsletter/v2n4/a6p2.jpg" alt="Basic wushu arm movments - the fist, drill 1:standing punching"></p>
			<p><b>Drill 2:</b> Alternate punching forward while in a Horse Stance (<i>mabu chongquan</i>).  Start with your fists at your waist.  When you punch, rotate your arm forward until the <i>heart</i> of the fist is facing down (Figures 1-65 and 1-66).</p>
			<p align="center"><img src="../../images/newsletter/v2n4/a6p3.jpg" alt="Basic wushu arm movments - the fist, drill 2:horse stance forward punching"></p>
			<p><b>Drill 3:</b> Alternate punching sideways while in a Horse Stance (<i>mabu hengchongquan</i>). Start with your fists at your waist.  Punch sideways.  When you punch, rotate your arm forward until the <i>heart</i> of the fist is facing down (Figure 1-67).</p>
			<p align="center"><img src="../../images/newsletter/v2n4/a6p4.jpg" alt="Basic wushu arm movments - the fist, drill 3:horse stance side punching"></p>
			<p><b>Drill 4:</b> Alternate punching while in a Bow Stance (<i>gongbu chongquan</i>).  Start with your fists atyourwaist.  Punch with your left fist.  Then pull your left fist back as you simultaneously punch forward with your right fist.  When you punch rotate your arm in until the <i>heart</i> of the fist is facing down (Figures 1-68 and 1-69).</p>
			<p align="center"><img src="../../images/newsletter/v2n4/a6p5.jpg" alt="Basic wushu arm movments - the fist, drill 4:bow stance punching"></p>
			<p><b>Drill 5:</b>  Alternate casting your fist up while in a Standing Stance with feet apart (<i>kailibu liangquan</i>).  Start with your fists at your waist.  Extend your right arm to your side, then up.  At the same time, turn your head to look to your left.  When you extend your arm up, rotate your fist until the <i>heart</i> of the fist is facing up (Figures 1-70 and 1-71).</p>
