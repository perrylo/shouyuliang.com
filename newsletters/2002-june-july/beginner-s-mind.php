			<p>Sometimes the question arises, "Why do you keep taking the same class over again?" If I ask that question of myself, my reasons are: 1) I like it! 2) To keep learning, and 3) To train myself in keeping a <i>Beginner's Mind</i>.</p>
			<p>There is always more to learn from Sifu!  There are always greater depths to uncover and delve into.  You can't catch everything the first or second time around. And I enjoy the practice. </p>
			<p>I generally laugh to myself and say to myself, "How can you learn it just once through?"  How can you become bored after only once or twice through the teaching?  I don't understand?  Do you not try to improve each and every time?</p>
			<p>I treasure Sifu's input and revelations.  Not everything is revealed or understood at first try.  You try to keep a Beginners Mind each and every time you learn.  If it's your first time through a new 'learning' you have an authentic beginners mind and beginner's experience!</p>
			<p>If it's your second or multiple time through the same 'learning' then you try to have a beginners mind and accumulated experience!  You try to improve bit by bit.  Always striving to make progress, always learning from the teaching, progressing to the next level.</p>
			<p>As an instructor, students expect me to have answers, and I try to accommodate them as best as I can!  Sometimes I refer them to Sifu, so that they can get a <i>best</i> answer and so that I can also get a new input into my accumulated experience.  When I help Sifu in his classes I give what I can and answer questions as best I can.  When I follow along and learn a teaching for the multiple time, I try my best to keep quiet.  I try to keep a beginner's mind and accumulate experience!  <i>Shutting up</i> can be very hard and rewarding.</p>
			<p>Also, there are times when answering a student's question may not be in his best interest because he's really not at the development point that the answer would do him the most good.  He hasn't accumulated enough experience where a question with a beginner's mind would help him progress and gather insights.  Sifu has proven this point to me many times and I appreciate it immensely.  Just when you think you're gotten somewhere you get introduced to the next level - Welcome!  Again, silence can be very humbling and rewarding.</p>
			<p>In the long run, what you are learning is <i>How to learn</i>. How to progress on your own! How to be sustained by the teaching and how to always to keep learning from it! And paradoxically, how to learn from others, and how to help others learn!  Beginner's mind, accumulated experience!</p>
