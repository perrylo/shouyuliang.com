			<h3>Morality of Deed: Humility <i>(Qian Xu)</i></h3>
			<p>Humility comes from controlling your feelings of pride.  In China it is said: "Satisfaction (i.e.pride) loses, humility earns benefits."  When you are satisfied with yourself, you will not think deeply, and you will not be willing to learn.  However, if you remain humble, you will always be looking for ways to better yourself, and you will keep on learning.  Remember, there is no limit to knowledge.  It does not matter how deep you have reached, there is always a deeper level.  Confucius said, "If three people walk by, there must be one of them who can be my teacher."  There is always someone who is more talented or more knowledgeable than you in some field.  The Chinese say: "There is always a man beyond the man, there is sky above the sky."  Since this is so, how can you be proud of yourself?</p>
			<p>I remember a story that my White Crane master told me when I was seventeen years old.  Once therewas a bambooshoot that had just popped up out of the ground.  It looked at the sky and smiled, and said to itself, "Someone told me that the sky is so high that it cannot be reached.  I don't believe that's true."  The sprout was young and felt strong.  It believed that if it kept growing, one day it could reach the sky.  So it kept growing and growing.  Ten years passed, twenty years passed.  Again it looked at the sky.  The sky was still very high, and it was still far beyond its reach.  Finally, it realized something, and started to bow down.  The more it grew the lower it bowed.  My teacher asked me to always remember that "The taller the bamboo grows, the lower it bows."</p>
			<p>There was another story a friend told me.  Once upon a time, a student came to see a Zen master. He said,"Honorable master, I have studied for many years, and I have learned so much of the martial arts and Zen theory already that I have reached a very high level.  I heard that you are a great master, and I have therefore come to see if you can teach me anything more."</p>
			<p>The master didn't reply.  Instead, he picked up a teacup and placed it in front of the student.  He then picked up the teapot and poured until the tea reached the rim of the cup, and then he kept on pouring until the tea overflowed onto the table.  The student stared at the master in total confusion and said, "No, no, Master!  The cup is overflowing!"</p>
			<p>The master stopped pouring, looked at him and smiled.  He said, "Young man, this is you.  I am sorry thatI cannotaccept you as a student.  Like this cup, your mind is filled up and I cannot teach you any more.  If you want to learn, you must first empty your cup."</p>
			<p>In order to be humble, you must first rid yourself of false dignity.  This is especially true in front ofa master.  A person who is really wise knows when and how to bend, and always keeps his cup empty.</p>
