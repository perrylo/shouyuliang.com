			<p><b>3. Both Knees to Your Chest and Turn:</b><br />
			Drill: Stand in a high Horse Stance. Turn your upper body to your right with your arms also extending to your right. Swing your arms to your left as you turn your body to your left, and jump up and turn 360 degrees with your knees up. Land lightly in a Horse Stance. Repeat 10 to 20 times (Figure 2-2). Then do this drill on the other side.</p>
			<p><b>4. One Knee to Your Chest:</b><br />
			Drill: Jump and bring one knee up to your chest while keeping the other leg straight. Keep doing the same leg or alternate legs (Figure 2-3). Repeat each leg, 10 to 20 times.</p>
			<p align="center"><img src="../../images/newsletter/v6n3/a6p2.jpg" width="506" height="400" alt="Jumping Drills for wushu"></p>
			<p><b>5. Jump and Reach Up:</b><br />
			Drill: Jump up as high as you can. As you reach up, stretch your body from your hands down to your toes and keep your body straight. Repeat 10 to 20 times.</p>
			<p><b>6. Jump and Touch Toes:</b><br />
			Drill: Stand with your feet shoulder width apart. Bend your knees slightly, and lift your legs up at an angle to your sides. At the same time, slap your palms on top of your feet (Figure 2-4). Land lightly. Repeat 10 to 20 times.</p>
