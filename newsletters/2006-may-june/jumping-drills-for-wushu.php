			<p><b>1. Light Jumps:</b><br />
			Drill: Stand with your feet about 6 inches apart. Keep your knees straight, but not locked. Use only your feet, bounce up and down as high as you can, 10 to 20 times.</p>
			<p><b>2. Both Knees to Your Chest:</b><br />
			Drill: Stand in a high Horse Stance. Swing your arms as you jump up. When you jump, bring your knees up together as high as you can. When you land, separate your legs and land lightly in a Horse Stance (Figure 2-1). Repeat 10 to 20 times.</p>
