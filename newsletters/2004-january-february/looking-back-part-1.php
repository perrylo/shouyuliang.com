	        <br><br>
			<p>When I look back on the past year I can honestly say that I was happy with my progress as a Martial Artist. Can you?</p>
			<p>In 2003 I continued my training as a member of the Canadian National Team for Sanshou. I worked on improving my stamina and strength and focused on my diet. I did an herbal cleanse and then supplemented with creatine and protein powder to increase my weight as I had decided to move up a weight class. What a mistake that turned out to be! I'll get to that later. My re-focus on good nutrition and diet got me thinking again about how important it is as a martial artist to pay attention to your body and what fuels it. My fighting continued to improve this year especially from a mental standpoint. I also began teaching a new class with Master Liang's approval on Thursday nights at 7pm. This class was a way for me to pass on my personal views on strength training, fitness, grappling and sparring, including traditional. I would like to thank Tjong for helping teach this when I went to the World Championships. And to my regular students primarily Albert, Dan and Bart who made the effort to push themselves and improve. There are also those who worked hard in this class who I would like to see return namely Winnie, Michael, Simon, Norman. You guys have shown improvement and you have the potential to climb higher. I also continued teaching at UBC on Monday nights and I was very happy with how the year turned out. As a beginner level class I feel the students walked away with a new appreciation for the combat side of Wushu.</p>
			<p>The World Championships were approaching fast around this time and I intensified my training. I won'tgiveawaytoo much of my training here, I'll save that for my book. Around this time I also submitted an article on Sanshou Linking Combinations to Kung Fu/Wushu magazine. Anyone who has taken my Thursday class will recognize these. Look for this article to come out this April. The World Championships in Macao were an interesting and exciting experience. It helped to push me as I realized I needed to work a lot harder. I don't mean just as a fighter though. It was interesting for me to see that most of the fighters were simply that, fighters. I felt good that I was more well rounded. I also noticed that the men in my division were bigger and stronger than me. I felt that my skill level was probably better overall though. I was at the bottom of the division and looked very small next to everyone else. It was a bad idea to move up a division. I couldn't keep the weight on as I lost 8 pounds in the hot and humid air of Macao. I ended up losing in the Quarterfinals to the 3-time World Champion from Iran. For my first taste of World Competition I was ok with that. I took away some lessons from Macao. I needed to fight in a weight I was comfortable with, I needed to increase my strength, I needed to train harder, I needed to focus on some weaker areas. But, I went to the World Championships, the Olympics for my sport, and I felt like I belonged. I was not outclassed, I held my own, and next time I would do better.</p>
