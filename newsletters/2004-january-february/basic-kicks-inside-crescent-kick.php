			<p>After the impact, continue the movement of your right leg towards your left and touch down lightly with the ball of your foot (Figure 2-18).</p>
			<p>You may repeat the kick in place 10 to 20 times by moving your kicking foot back to the starting position. You may also alternate kicking with one leg then the next across the training area, by stepping forward with the foot that has just completed the kick, then kick with the back leg. Alternating from side to side.</p>
