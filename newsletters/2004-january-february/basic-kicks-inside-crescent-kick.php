			<p>An Inside Crescent Kick is a circular kick with the sole of the foot. It is accomplished by swinging your kicking leg in from the outside, up and down to the opposite side.</p>
			<p>Drill: From the starting position, take a small step forward with your left foot, turn your foot out about 30 degrees, and shift your weight onto your left leg (Figure 2-15). Swing your right leg from behind, to your right and up, in front of your body with your leg straight. At the same time, slap your left hand towards the oncoming foot, contacting the bottom of your right foot (Figures 2-16 and 2-17).</p>
