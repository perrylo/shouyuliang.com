			<p>Wushu to me means more than just a sport. It is more than beautiful forms that delight the senses. It is more than a feeling of responsibility. I do not have much time to dedicate to my Wushu training right now because I am swamped with so many things (work, school, etc.) At first I was depressed at myself. I'm not training hard enough.  I'm not dedicated. I would be angry at myself. But as time goes by I am slowly coming to realize that what's most important is that I keep training on my own when I am cannot attend Wushu class, and that I always come back.</p>
			<p>I am not progressing as fast as I was before because I cannot dedicate the amount of training that I need to do. Though it takes longer for me to notice improvement, I can start to feel changes: I can hold my leg a little higher, I can punch a little better, I can stretch a little farther. Now that am learning to stop beating up my mind when I cannot attend class or I am late, I can focus on strengthening my mind. I can keep training to perfection the positive changes I've noticed and take my body to the next level. I can see more clearly the areas that need more attention so that my body is well balanced. I can let my mind help me instead of hinder me.</p>
			<p>I am beginning to feel that I am learning more about my body, and my mind. For example how my mind gives up easily sometimes, or how my mind and body has not fully understood Wushu. It does not feel like a part of me yet, I am too stiff and too distanced still. This is what it is about for me right now. I must stop complaining, whining, and being so hard on myself in a negative way and start looking deeper within myself and training my mind and body. I have to be strong myself and be able to train alone and with my Sifu. This is a better meaning of dedication than just showing up to every class and not working hard. No matter what, Wushu is a part of me. My Sifu, teachers, fellow students are a part of me. They are my family, I will grow old and they will still be my family, alongside my father and my siblings.</p>
