			<p>According to Traditional Chinese Medicine (TCM), the face reflects the condition of the Shen (spirit) or Heart of the body. When a person is very healthy, that is, the person has a strong Shen, his or her face will have a healthy glow.</p>
			<p>You may have heard of the phrase she has a pregnancy glow. This is because when a woman is pregnant and a life is growing inside her, there is an exuberance of Shen (full of life-force). Her skin may be smoother and hair fuller. Hence, a healthy glow appears on her face.</p>
			<p>On the other hand, a person who isn't feeling well will often manifest a greenish hue on the face; a sick face. A person with hepatitis or has recently had hepatitis will often have a yellowish hue on the face. Colors, according to TCM, also has their corresponding organs. For instance, green corresponds to Liver, red to Heart, black to Kidney, and so forth. Furthermore, different parts of the face has their corresponding organs. For instance, the lips reflects the Spleen and Stomach, the eyes the Liver, and so forth.</p>
			<h4>Microcosm</h4>
			<p><b>What are microcosm?</b><br />
			A microcosm refers to the theory that a part of the body, such as a foot, can reflect the whole body. Hence, treatments given to the foot can affect the whole body. Alternatively, aches in certain parts of the foot may suggest a problem in the corresponding organs. You may have heard of hand and foot reflexology. Our hands and feet are not the only places that reflect the organs of our whole body.</p>
			<p>One extremely valuable microcosm in the body is the tongue. Doctors of TCM always use tongue diagnosis to determine the condition of their patients. From the tongue diagnosis, they would know which organs are affected, and decide which herbs and/or acupuncture points to use. However, they would not normally treat the tongue. Instead, herbal remedy and body acupuncture treatment would be used to treat the problem.</p>
			<p>Microcosms are very useful for diagnosis. Other examples are:</p>
			<ul>
				<li>ear (auricular acupuncture)</li>
				<li>face</li>
				<li>hara (area around the naval)</li>
			</ul>
