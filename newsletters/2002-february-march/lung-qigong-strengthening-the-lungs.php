			<h4>Movement and Intention:</h4>
			<p><b>Step 1</b><br />
			Sit comfortable in a chair.  Gently press your index and middle fingers of your right hand on the pulse areaof your left wrist (Figure 1-9 or 1-10).  Gently press your tongue to the roof of your mouth and relax your chest.  Breath in deeply though your nose until your abdomen is raised, then hold your breath and count your pulse for 20 to 40 times (the number of times depends on your condition).</p>
			<p><img src="../../images/newsletter/v2n2/a2p1.jpg" alt="Lung Qigong">
			Then lower your tongue and make round shape with your mouth and exhale.  As you exhale through your mouth, draw your stomach in, and focus on or say the word song.  After exhaling all you can, inhale again and reapeat the exercise.  Each time, train only 3 to 5 breathing cycles.  Do this exercise once in the morning and once in the evening. After three months of training, go on to the next exercise.</p>
			<p><b>Step 2</b><br />
			After training Step 1 for a period of time, you should feel that your breathing pattern is getting smoother and that you are able to do ordinary exercise or work without a shortness of breath.  If so, then you can start training Step 2.</p>
			<p>From the same sitting and had position as in Step 1, inhale until your abdomen is raised, then inhalea littlemore by raising your chest (abdomen and chest breathing).  After inhaling to the maximum, hold your breath and count your pulse. After a period of training, you will be able to hold your breath for 100 to 140 pulses or more.</p>
			<p>When you can no longer hold your breath, open your mouth wide and make a soft ha sound as youexhale all the impurities from your mouth.  After  exhaling all you can, repeat the exercise.  Do three breathing cycles each time you train.  Train once in the morning and once in the evening.</p>
			<p><b>Key Points:</b></p>
			<ol type="A">
			  <li>People with cardiovascular problems, and people that are weak and old, should not practiceholding the breath right away.  These individuals should first practice deep inhalations and slow exhalation breathing.  Deep inhalation and slow exhalation is accomplished by first calming your mind and bring in your attention to your dantian area.  Then breath in and out slowly.  Find out the rate of your breathing.  An average person breathes about 18 to 20 cycles per minute.</li>
			  <li>Train 3 to 5 minutes each time.  With the practice of deep inhalations and slow exhalations, you can gradually decrease the rate of your breathing down to 6 to 12 cycles per minute.  When you can reduce your breathing rate to between 6 to 12 cycles per minute and feel comfortable doing it, then gradually include short periods of holding your breath.  In about 3 months, you should be ready to do Step 1.</Li>
