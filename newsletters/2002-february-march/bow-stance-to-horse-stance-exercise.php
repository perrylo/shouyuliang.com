	        <h2>By Tjong Tjhie</h2>
	        <br><br>
			<p>This basic Wushu drill is useful for developing coordination, balance and leg power.  Specifically, it helps improve your bow stance and horse stance, and should be practiced before your regular Wushu training.  For those just beginning their Wushu training, higher stances at shorter durations are recommended.  Ideally, advanced students should complete 10 repetitions of this exercise while holding each stance for at least 10 seconds.</p>
			<p><b>Starting out:</b><br />
			Begin in the <i>ready</i> position, feet together fists at your hip.</p>
			<p><b>Step 1: Left bow stance</b><br />
			Make a blocking motion with your left hand as you take a step forward with your left foot into a bowstance. Yourright hand punches out as you step into the bow stance while your left hand retracts to your hip and forms a fist.  The right hand should come directly from your hip and finish at shoulder height.  Your upper body should be straight with your eyes focused beyond your fist.  The bow stance should be such that both feet are flat on the ground with the back leg straight.  For advanced students, the front thigh should be parallel tothe floor.  Your front knee should be positioned over a between your heel and toes.  Make sure that you are balanced before moving on to the next step.</p>
			<p><b>Step 2: Left horse stance</b><br />
			Moving forward with your right foot, take a step past your left foot and settle into a horse stance facing left.At the same time your right hand moves into a blocking motion in front of your body.  Your left fist should remain at your hip during this entire motion.  The right arm is now bent approximately 90 degrees with the right hand clenched into a fist at eye level.   Make sure that you are balanced before moving on to the next step.</p>
			<p><b>Step 3: Right bow stance</b><br />
			Turn your body towards your right side to face the front while straightening your left leg to form abow stance. As you settle into the bow stance, your left hand punches out while your right hand retracts to your hip.</p>
			<p><b>Step 4: Right horse stance</b><br />
			Moving forward with your left foot take a step past your right foot and settle into a horse stance facing right.At the same time, your left hand moves into a blocking motion in front of your body.  Your right fist should remain at your hip during this entire motion.  The left arm is now bent approximately 90 degrees with the hand formed into a fist at eye level.  Again, make sure you are balanced before moving on.</p>
			<p><b>Step 5: Left bow stance</b><br />
			Turn your body towards your left to face the front while straightening your right leg to form a bow stance. As you settle into the bow stance, your right hand punches out while your left hand retracts to your hip.</p>
			<p>To continue, repeat steps 2 to 5.</p>
