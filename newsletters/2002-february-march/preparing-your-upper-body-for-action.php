	        <h2>By Grandmaster Shou-Yu Liang and Dr. Jwing-Ming Yang</h2>
	        <br><br>
			<p>Shoulder exercises are used for improving the shoulders' flexibility, increasing the range of motion,developing strength; and improving the arms' agility, extension, and rotation ability.</p>
			<p align="center"><img src="../../images/newsletter/v2n2/a4p1.jpg" alt="Stretch the Shoulders"</p>
			<h3>1. Stretch the Shoulders (<i>yajian</i>)</h3>
			<p><b>Drill 1:</b> Grab a hold of something stable that is about your waist height with both hands. Place your feed about shoulder width apart and bend your upper body forward.  Keep both of your arms and your legs straight (Figure 1-1)</p>
			<p><b>Drill 2:</b> Two people may also do the stretch by holding onto each other's arms (Figure 1-2).  When stretching the shoulders, rhythmically bounce your shoulders up and down.  Focus on the downward movement of the shoulders, and gradually increase the depth of the downward pressure over time.</p>
			<p align="center"><img src="../../images/newsletter/v2n2/a4p2.jpg" alt="Swing the Arms"</p>
			<h3>2. Swing the Arms (<i>lunbi</i>)</h3>
			<p><b>One Arm Backward Swing:</b> Stand in a left Bow Stance and place your left hand on top of yourleftknee, and extend your right arm over your head (Figure 1-3).  Keep your upper body straight while swinging your right arm in a vertical circle (Figures 1-4 and 1-5).  After you have done this about 20 times, change sides, and swing the other arm.</p>
			<p>One Arm Forward Swing: Same starting posture as in the Backward Swing.  Swing your arm in a vertical circle forward.  After you have done this about 20 times, change sides, and swing the other arm.</p>
			<p align="center"><img src="../../images/newsletter/v2n2/a4p3.jpg" alt="Swing the Arms"</p>
			<p><b>Both Arms Backward Swing:</b> Stand straight with your feet about one shoulder width apart.  Raiseone arm up over you head and the other next to your side pointing down (Figure 1-6).  Keep your arms pointing in the opposite directions and swing the arms in a vertical circle backwards (Figures 1-7 and 1-8)</p>
			<p><b>Both Arms Forward Swing: </b>Same starting posture as in the Backward Swing, except swing yourarmsin avertical circle forwards.</p>
			<p><b>Both Arms Forward and Backward Simultaneous Swing:</b> Extend both arms over your head (Figure1-9).  Loweryour arms in opposite directions, down then back up, while keeping the arms in a vertical plane (Figures 1-10 and 1-11).</p>
			<p align="center"><img src="../../images/newsletter/v2n2/a4p5.jpg" alt="Swing the Arms"</p>
			<p><b>Both Arms Cross Swing:</b> Stand with your feet about one should width apart.  Extend your right arm straight to your right and place your left hand next to your right upper arm (Figure 1-12).  Swing your arms up and across your head to the other side by extending your left arm and placing your right hand next to your left upper arm (Figure 1-13). Alternate from side to side.</p>
			<p>During the arm swings, relax the shoulders and keep the swinging arm straight.  Gradually increase the speed of the swing.  Do each swing about 20 times or until your fingers are slightly numb after each exercise.</p>
			<p align="center"><img src="../../images/newsletter/v2n2/a4p4.jpg" alt="Stretch the Back and Waise"</p>
			<h3>3. Stretch the Back and Waist:</h3>
			<p>The waist is the connector of the upper and lower body.  There is a saying in Wushu, <i>"If one trains the martial arts without training the waist, their accomplishment will never be high."</i>  In the training of Wushu, the waist is the key to the proper expression of the movement.</p>
			<p><b>Forward Back Stretch (<i>quanfuyao</i>):</b>  Stand with your feet together and interlock yourfingers.Stretch your arms up and then bend from your waist and push your palms down towards the floor as low as you can (Figure 1-14).  Then release the fingers and grab a hold of your heels with your hands, and bring your chest as close to your thighs as you can (Figure 1-15).  Hold this position for about 30 seconds before standing up.</p>
			<p>Instead of holding your heals, you may also place your palms on either side of your feet for about 30seconds each side (Figure 1-16).</p>
			<p><b>Bridge Bend (<i>xiayao</i>):</b> Stand with your feed shoulder width apart, and stretch your amrs overyourhead.  Lower your arms slowly behind your body to form a bridge or a back bend (Figure 1-17).  Hold this position for about 30 seconds.</p>
			<p>Do this stretch gradually so you don't injure your back.  An assistant may be used to help hold your legsin place to prevent your feet from slipping under you (Figure 1-18).</p>
			<p align="center"><img src="../../images/newsletter/v2n2/a4p6.jpg" alt="Stretch the Back and Waise"</p>
			<p><b>Swinging the Waist (<i>shuaiyao</i>):</b> Stand with you feet shoulder width apart and stretchyourarmsoveryour head.  Use the waist and hips as the pivot, swing your upper body forward and backward.  Allowing the upper body to wave forward and back (Figures 1-19 and 1-20).</p>
			<p><b>Circling the Waist (<i>shuayao</i>):</b>  Stand with your feet shoulder width apart, bend yourupper body from your waist and hips, and stretch your arms.  Use your waist and hips as the pivot, circle your upper body and your arms horizontally (Figures 1-21 and 1-22).  Gradually speed up the circles.  Do 10 to 20 times in each direction.</p>
			<p>After this exercise, you may wish to squat down or bend from your waist and keep your head down for awhile toprevent dizziness.</p>
