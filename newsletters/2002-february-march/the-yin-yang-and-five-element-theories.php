			<p>The basic theory behind the five elements can be summed up by two normal and three adverse interactions. Mutual Nourishment (interpromoting) and Mutual Restraint (interacting), are normal cyclic patterns.  Mutual Over-Restraint (encroachment), Reverse-Restraint (violating), and Mutual Burdening are the adverse conditions.  By understanding the cyclic patterns in the human organs (elements) and the possible adverse conditions, the illness of one organ can also indicate problems in the corresponding organs.</p>
			<p>Mutual Nourishment refers to the cyclic enhancement or interpromoting pattern of the Five Elements.  In this normal cycle, each element gives and receives nourishment in the cyclic pattern.  Mutual Restraint refers to the cyclic neutralizing of the elements in order to keep each other in check and balance.  Mutual Nourishment and Restraint cycles are not independent cycles.  They interact with each other and are closely related.  Drawing 1-3 is a typical representation of the normal cyclic interactions of the Five Elements.</p>
			<p align="center"><img src="../../images/newsletter/v2n2/a1p1.gif" alt="Mutual Nourishment Cycle and Mutual Restraint Cycle, Yin-Yang"></p>
			<p>Everything in the great cosmos contains many linkages.  Any object that has any type of interaction with another object, and within itself, has an opposing, yet interdependent nature.  This opposing, yet interdependent nature, is due to the interaction of <i>yin</i> and <i>yang</i>.  The Yin-Yang philosophy is a means to understand and grasp the lays of nature.  It is the empirical reasoning for the production, development, and perishing of all things in the great cosmos.</p>
			<p>Drawing 1-4 is the most commonly used symbol to represent the yin-yang philosophy.  This symbol is known as <i>Taiji</i> - the grand ultimate.  The big enclosed circle symbolizes the <i>whole</i> universe.  The curvature within the circle symbolizes the opposing, yet interdependent nature of yin and yang.  The black (yin) and the white (yang) tear drop shapes symbolize the decreasing and increasing of yin and yang, as well as, thetransformation of yin and yang. At the highest concentration of yang there is black dot, and at the highest concentration of yin, there is white dot.  The dots symbolize that yin and yang are not absolute.  They are not only interdependent, by there are subdivisions of yin and yang within yin and within yang.</p>
			<p>The Yin-Yang Theory is primarily used to describe the opposing, interdependent, waxing and waning, and transformational nature of all things in the cosmos.  The Five Element Theory is primarily used to explain characteristics, classifications, and the law of mutual nourishment and mutual restraint.  In the practical applications of Yin-Yang and the Five Elements, they are used in conjunction with one another.</p>
			<p>Human physiology, and the causes and development of diseases are also within the Yin-Yang and Five Element Theories.  In the Spring-Autumn and Warring Kingdom periods in China (8th-3rd Century B.C.), the Yin-Yang and Five Element philosophy was appended into Chinese medicine to describe human physiology, illness, herbal characteristics, and the relationship between humans and nature.  This effectively guided the diagnostic and healing processes and became an important component of Chinese medicine.</p>
			<p>In traditional Chinese medicine, yin-yang also refers to the various antitheses in the human bodywhich occur in the anatomy, physiology, pathology, diagnosis and treatment.  For example, the material aspect of the body is yin, and the functional aspect of the body is yang.  The five elements are used in traditional Chinese medicine to expound on the unity of the human body and the environment, and the physiological and pathological relationship between the human internal organs.</p>
