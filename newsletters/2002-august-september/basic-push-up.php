	        <h2>By Ayron Howey</h2>
	        <br><br>
			<p>The following exercises will help anyone who is interested in improving their strength or power. Pushups are a great upper body workout focusing on the arms and chest but also benefiting the back and shoulders. Pushups are also a simple exercise but have many variations - I will hope to introduce you to a few. The key to remember is that quality is the most important, quantity will come with time and practice. So take your time and be patient focusing on your body position. Pushups will help with punching power, executing throws, forms, weapon work, basically anything that requires arm movement. The benefits of pushups are that a pushup forces the muscles of the arm, chest, back and shoulders in co-ordination with each other. This is best for martial applications because the strength that is developed is total body as opposed to say, arm curls, which simply enhance one muscle group. It may look nice, but without muscles trained to work together it is not a strength that is practical in martial applications. That being said, we will begin with the basic pushup.</p>
			<p>On a flat floor/surface, kneel down onto all fours, before removing the support from your knees, check to make sure that your hands are arranged properly - fingers are together and palms are on the floor, palms are facing straight forward. Your hands should be positioned underneath your shoulders, or shoulder-width apart.  Lift your body so that you are now balanced on 4 points, your left and right toes, and your left and right hands.  For beginners, your feet should be positioned shoulder width apart, and for advanced the feet should be together.  You can later experiment with varying distances in-between.</p>
			<img src="../../images/newsletter/v2n5/a6p1.jpg" alt="Basic Push-Up - Starting position on knees">
			<img src="../../images/newsletter/v2n5/a6p2.jpg" alt="Basic Push-Up - Starting position hands and toes ">
			<p>Now that you are in a raised position, there are 2 ways to do the basic pushup. The first is to lower yourself down to the floor, focusing on the arms, and touch your nose to the floor. This keeps the neck and spine in a straight line and is recommended for the beginner and for those with any neck problems. The second, is to lower yourself, but this time looking straight ahead, touching your sternum or chest to the floor. This puts a greater emphasis on the chest. I would recommend doing both variations in a rotating basis for better development of the muscles.</p>
			<br clear="all">
			<img src="../../images/newsletter/v2n5/a6p3.jpg" alt="Basic Push-Up">
			<img src="../../images/newsletter/v2n5/a6p4.jpg" alt="Basic Push-Up">
			<p>The minimum amount of repetitions should be ten.  Intermediate range would be 30-50 and advanced 50-100. For pushups, you need to give the muscles a chance to rest between sets, 30 seconds is enough with no more than 2 minutes rest. Three sets should be what you strive for as a beginner, for example, 3 sets of 10 looking straight ahead or touching your nose. 5 sets would be for an intermediate level, and for the advanced, the limit should be until you can no longer left your self off the floor.</p>
			<p>For other variations without changing hand position or movement, try lowering yourself down and pushing up to a count of 3 each. Example, down 1-2-3, and up 1-2-3. Increase this to 5 counts and then to 10 counts.</p>
			<p>This is the basic pushup, practice hard, the next few variations will get harder, but will in turn make you stronger. Pushups strengthen the body and will help to improve a martial artist practitioner, whether their focus is on fighting, weapons, forms, or tai chi. Improving strength, improves your health and with an exercise so simple and easy to do, there's no reason why everyone can't do these, even if it's just 3 sets of 10, but hopefully it will be 5 sets of 100. I will continue next time with more pushup variations and will also delve into Sit-ups and there many variations for strengthening the stomach and abs.</p>
