	        <p>With this second part of a two-part introduction, we will explain basic arm movements found in many Wushu applications.</p>
			<h3>Palm and Hook</h3>
			<p><img src="../../images/newsletter/v2n5/a5p1.jpg" align="right" alt="Wushu Arm Movements, Palm and Hook">
			<b>Posture:</b>  A typical palm hand posture is accomplished by bending the thumb and tucking it tightlynextto the edge of the palm, while keeping the other four fingers straight and tightly together.  The different sections of the palm are described with different names.  They are the <I>back, fingers, outer edge, heard,</I> and <I>root</I> of the palm. (Drawing 1-2).  A typical hook hand posture is accomplished by squeezing the fingers together and bending the wrist.  The different sections of the hook are described with different names.  They are the <I>root</I> and the <I>tip</I> of the hook (Drawing 1-3).</p>
			<p><b>Drill 1:</b> Alternate casting your palm up while in a Standing Stance with feet apart (<I>kailibuliangzhang</I>). Start with your fist at your waist.  Extend your right arm to your side, then up.  At the same time, turn your head to look to your left.  When you extend your arm up, open your hand and rotate your palm to face up (Figures 1-72 and 1-73).</p>
			<p align="center"><img src="../../images/newsletter/v2n5/a5p2.jpg" alt="Wushu Arm Movements, Palm and Hook Drill 1"></p>
			<p><b>Drill 2:</b> Alternate palm strikes while in a Standing Stance with feet apart (<I>kailibu tuizhang</I>). Start with your fists at your waist.  Open your right hand and push forward with your palm (Figure 1-74).</p>
			<p align="center"><img src="../../images/newsletter/v2n5/a5p3.jpg" alt="Wushu Arm Movements, Palm and Hook Drill 2"></p>
			<p><b>Drill 3:</b>  Alternate spearing and hooking while in a Standing Stance with feet together (<I>libu chuanshou liangzhang</I>).  Start with your arms next to your sides (Figure 1-75).  Extend your right hand up at an angle to your right (Figure 1-76).  Spear your left hand up along your right arm.  At the same time, bring your right hand down and move your left arm to your left (Figure 1-77).  Cast your right hand down, then up, over your head, while hooking your left hand down behind you (Figures 1-78 and 1-78a).</p>
