			<ol type="1">
				<li>Circle/Roll your eyes 9 times clockwise, then 9 times anti-clockwise.</li>
				<li>Look Up then Down 9 times; then, Look Down then Up 9 times.</li>
				<li>Sideways, Look Left to Right 9 times; then, Right to Left 9 times.</li>
				<li>Diagonal, Look from the bottom right corner to top left corner of your eye, 9 times; then, diagonally from bottom left corner to top right corner of your eye, 9 times.</li>
				<li>For support, place each of your thumbs on its respective temple, then using the side knuckles of your index fingers, Massage your Eyebrows 9 times, start from nose bridge and finish when your index knuckles meet the thumbs, for one repetition.</li>
				<li>Carefully also Massage under your eyes - your cheekbones 9 times, using the side knuckles of your index fingers, start with one knuckle adjacent to each side of the nose and work outwards, for one repetition.  <i>BE CAREFUL NOT TO BREAK YOUR SKIN.  USE A LITTLE CREAM IF YOU HAVE VERY SENSITIVE SKIN.</i></li>
				<li>Close your eyes and using the tips/finger-pads of your index, middle and ring fingers, Massage your eyeballs circularly, 9 times clockwise, then 9 times anti-clockwise, gently apply pressure inward while rotating.</li>
				<li>Press and massage the following acupuncture/acupressure points: (press firmly into the point hollow and massage using a circular motion, using both your index fingers simultaneously.)</li>
				<li>Finish by bringing qi and Warmth to your hands by rubbing them together, then cover each eye with it's respective palm and send your qi into your eyes - hold for at least one minute.  You can close your eyes or keep them open while they are covered.</li>
			</ol>

