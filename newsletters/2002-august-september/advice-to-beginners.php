	        <h2>By Dan Marks</h2>
	        <br><br>
	        <p>I mulled this over in my mind and came up with a list of pointers for budding martial artists.</p>
			<p><b>1) Keep at it.</b> If the class seems impossibly hard, always remember that nothing is over until you give up.  Keep trying.  Eventually you will make some progress and improve.  Talk to a person who makes it look easy; they have probably been practicing for a long time.</p>
			<p><b>2) Have fun.</b>  Many people join martial arts for recreation; they want to enjoy themselves.  That's good.  I think practice should be serious and committed, but you should still find pleasure in what you're doing no matter how skilled you are.</p>
			<p><b>3) Practice outside of class.</b>  If you just like following the instructor, don't trouble yourself with outside practice.  If, however, your goal is to improve, then you'll have to put in some work.  Pay special attention in class and learn.  That's what class time is for.  You learn some movements - you receive some correction.  Practice what you've learned until it is permanently seared into your head.  (Note, this takes a while.  Don't worry - stay patient).<p>
			<p><b>4) Be realistic in your expectations.</b>  Don't show up to class hoping to impress everyone with all your natural talents.  (By the way, the people who impress me the most are the quiet, hard-workers who always come to class).  Always keep in mind the example of Grandmaster Liang. His abilities are amazing though he is humble. We should all strive to be that way.</p>
			<p><b>5) Be gentle with yourself.</b>  So what, you're not perfect; accept it and move on.  Does your body hurt when you practice?  Stop, and take a break.  With time and training, you capabilities will increase with your body and spirit.  Imagine you are inflating a balloon.  You don't blow into it until it pops; no, you inflate the balloon to capacity and then stop.</p>
