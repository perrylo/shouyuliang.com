			<p>The kicking surface of this kick is usually the heel.  It can also be a kick with the ball of the foot or the back of the heel during the downward movement of the kick.</p>
			<p>Stand with your feet together.  Extend your arms out to your sides with your arms straight, palms facing to your sides, and fingers pointing up.  Take a small step forward with your left foot (figure 2-5).  Shift your weight onto your left leg.  Keep your body square facing forward and kick your right leg up as high as you can without leaning.  Kick as close as possible to your forehead (figure 2-6).</p>
