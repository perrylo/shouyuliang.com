			<p>Like many other different martial art styles, successful strategies for attack and defense will generally follow the style's basic principles and rules. Without understanding the style's principles and rules, you will not achieve a high level of skill. One of the basic principles of San Shou Kuai Jiao is taking advantage of your opponent's body posture and applying the appropriate techniques at the right time to make your opponent fall. When applying a San Shou Kuai Jiao technique, you must follow your opponent's body postures and techniques, and know how to borrow your opponent's power and use it against him. For example; when your opponent's body is extend forward, you should not try to throw him backward, and vice versa.</p>
			<p>There are two basic elements in a successful throw. First, the opportunity to quickly throw your opponent depends primarily upon his body posture. Second, you must use the right San Shou Kuai Jiao technique at the right time. The throw will not be successful if you miss either of the two elements mentioned above.</p>
			<p>San Shou Kuai Mao techniques are concealed by or mixed with hand and leg techniques. The techniques can be defensive or offensive. When used defensively, you must lure an opponent to come in and attack first, or intercept your opponent's attack at the right moment, then apply the appropriate technique. When used offensively, you need to be able to create throwing opportunities by attacking first. Kicks, punches, and fakes will force an opponent to concentrate on defense while you look for the right opportunity to throw him.</p>
			<p>Opportunity is an important factor in a fight. But in order to take advantage of an opportunity, timing and decision are important. You need to know how to capitalize on the opportunity when it comes. During fighting, you and your opponent will move and change fighting strategies constantly. Therefore, body postures also change all the time to adjust to new situations. The change in body posture will happen in the blink of an eye. However, there is always a chance that the opponent will make a mistake. When he does, you need to make a quick decision and apply the appropriate technique. If your timing is off or you are indecisive, the opportunity will disappear. And if you pursue a disappearing chance, not only will your efforts be in vain, you may also put yourself in a dangerous position.</p>
			<p>In order to be proficient in the art of San Shou Kuai Jiao, not only must you understand the basic principles, but you also need to have many other skills. You need to have the right mindset when facing an opponent; your techniques must be alive, fast and powerful; and you need know how and when to capitalize on opportunities and apply techniques in an ingenious way. Of course, the use of strategy in a fight cannot be ignored.</p>
