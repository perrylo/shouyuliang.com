			<p>In Chinese martial arts society, virtually every style has its own Qigong training. Qigong practice enables a practitioner to manifest his techniques with more power and efficiency. Most Qigong practices that have evolved focus on their progenitive style's nature and characteristics. For example, for the Eagle Claw style, Qigong that can strengthen finger power (i.e., claw strength) is stressed. White Crane Style Qigong emphasizes the whipping power of the arms (i.e., wings). This principal also holds true in Baguazhang. Baguazhang Qigong specializes in training the body's turning, limb rotation and spinning, and the circular movements of stepping. In this section, we will introduce the well known Baguazhang Qigong set, Bagua Turning-Spinning Gong (Bagua Zhuan Xuan Gong).</p>
			<p>The Bagua Turning-Spinning Gong set consists of eight patterns of movements, including both stationary and stepping. It integrates the internal), Qi, and Jin (martial power) with the external tendons, bones, and muscles. The movements utilize the entire body, which is a very important training to build up a solid foundation for your Baguazhang training. Because of this, this Qigong set especially stimulates the joints and muscles with turning and spinning movements. It also exercises the areas of the body that are not normally exercised, so that the entire body is exercised. In addition, the turning and spinning movements stimulate the acupuncture cavities by compressing and relaxing the muscles around the cavities. This stimulation loosens stagnant energy and allows the Qi circulating in the channels to flow smoothly throughout the body.</p>
			<p>As you learn this set of Qigong exercises, practice one posture at a time until your movements are smooth. Then go on to the next posture and practice it until it is also smooth. Only after you are able to perform each posture smoothly should you combine them and train them as a complete set of Qigong exercises.</p>
