	        <br><br>
			<p>Looking for a high-protein, low-fat alternative to meat that can actually reduce your cholesterol level, ease menopause and help prevent osteoporosis and even cancer?</p>
			<p>Soy protein is a "complete" protein and just as nutritious as animal protein. In those countries where soy is the main source of protein, rates of heart disease and certain cancers( particularly breast, cervical, prostate and colon) are relatively low. Soy also protects against osteoporosis and some symptoms of menopause.</p>
			<p>Let's take a look at some ways that you can substitute soy for animal products.</p>
			<p><b>Tofu</b> - made from soy milk. It can be blended, stir-fried, steamed, grilled and baked. Crumble firm tofu into the cheese for lasagna, or add it to chili or spaghetti sauce.<p>
			<p><b>Miso</b> - soybean paste, often put in soups.</p>
			<p><b>Tempeh</b> - made from fermented soybeans, can be made into a spread or pressed into a cake and marinated, grilled or baked.</p>
			<p><b>Soy milk</b> - is rich, cream milk of whole soybeans. Pour over breakfast cereal or mix half-and-half with dairy milk. Add soy milk to coffee in place of milk or cream. Choose a low-fat product fortified with calcium.</p>
			<p><b>Soy flours</b> - can be used to thicken gravies and cream sauces, to make homemade soy milk, or to add to baked goods for a protein boost. Using about 15% soy flour in a bread recipe procudes a dense, moist, nutty-flavored bread.</p>
			<p><b>Soy sprouts</b> - like alfalfa sprouts, can be added to salads and other dishes.</p>
			<p>Soy cheese is great in omelettes. Soy flour can be added to pancakes and waffles. Soy protien powders and soy based meal replacement drinks are convenient and tasty way to increase soy intake. Soy "burgers" are available at most heath food stores. Textured vegetable soy protien can be added to ground beef or turkey recipes. (10% soy in such dishes blocks the formation of cancer causing HCA's, hetero-cyclic-amines, in meat.)</p>
			<p>Try to eat some legumes every day. And make soy your most   common choice. The average Asain eats one and a half servings a day.</p>
