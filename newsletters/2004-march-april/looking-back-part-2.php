			<p>My Look at 2003 continues, from my perspective as a martial artist.</p>
			<p>Following the World Championships in Macao I set about to fix the weaknesses that were brought tolight asa resultof my experience. I also realized that this is why Master Liang pushes us to compete. The experience of world competition showed me new insights about myself.  I became revitalized in martial arts, I had new goals I wanted to attain and I became wiser. This was the fundamental benefit of the experience of the World Championships, it pushed me to become a better martial artist, and I am still pushing for this today. At the World's I watched other teams work out, warm up and do drills. I picked up a lot of new techniques to try. Watching the taolu I realized just how talented our school is, especially concerning Tai chi.</p>
			<p>My martial arts goals upon returning centered around 2 primary points; to become physically stronger and to improve my internal. To get physically stronger I approached my teacher and after some discussion I adjusted by weight training program significantly. Internally I would continue to learn qigong but would need to make a greater effort in Tai chi. I would also need to improve my hsing yi, which has excellent applications for fighting.</p>
			<p>Another aspect I would continue is my research into the diet of a martial artist. This research wouldlay the groundwork for the book that I am currently writing. It is coming along nicely in my opinion. This has increased my desire to write more often and I have created a schedule that allows me to do this. I took these new lessons that I have learned and brought them to the classes that I teach, and help teach. I teach Monday nights at the University of British Columbia and Thursday nights at the SYL Institute. Both classes are Sanshou but I teach more than just the modern competition style of sparring. I include a warm up, cardio; cool down, self-defense, stretching, form/taolu, shuai jiao and meditation/visualization exercises. My goal is to train martial artists in these classes, not just fighters.</p>
			<p>Human potential is so much more than just being a fighter that I feel it is important to decreasethe amount of ego involved and make better people out of those who take my classes. It may be an idealistic goal, but I don't care, I have to try. Now that is not to say that a good martial artist cannot be a good fighter. If someone has the potential to be a good, competitive fighter, I will train him or her very hard in this regard. I realized that as a teacher I need to be more disciplined, and stricter during classes. I learned that I need to train people individually, and I need to have patience. I also learned that these things need to apply to myself as a student. Remaining humble is a lesson a martial artist should never forget.</p>
			<p>Looking back on 2003 I was very happy with my year as a martial artist. I learned many things andI received the drive to become a better student, better teacher and a better person.</p>
