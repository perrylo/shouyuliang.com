	        <br><br>
			<p>Usually in our Beginner's (Health Maintenance) Qigong Class, Sifu will ask everyone to try a little exercise that illustrates the power of one's mind.  He talks us through it, taking a full 2 to 3 minutes to do.  The results are something you see on yourself, of a 'before and after' type of comparison.  Give it a try, have fun with it, and for the 2 to 3 minutes that you do it, remember to fully concentrate on the task at hand and command your body to follow the directives of your mind. Enjoy!</p>
			<p>First place your palms together in a prayer type position.  The bottom lines at the inside of your wrists touch first and then everything matches up.  Generally you should find both hands to be close to the same size, fingertips close to the same height.  Make note of any particular differences if you have them.  You will compare your hands in this same manner immediately after the exercise and hopefully you will see a change in one hand.</p>
			<p>Now pick a hand, any hand - the non-chosen hand you can forget about, leave it down by your side or in your lap, open or lightly closed - up to you, ignore it for the next 2 to 3 minutes.  Next, look at your chosen hand, stare at it as you command it grow.  Tell it expand, grow larger, grow stronger, thicken.  For the next 2 to 3 minutes keep talking to your hand in this fashion, willing it to grow, expand and get stronger.  Really, really concentrate and keep concentrating on this one ideal.  Allow your hand to freely stretch, bend, move, as the feeling hits it (so to speak).
			Keep this up for the full 2 to 3 minutes!</p>
			<p>Time's up.  Now, compare your hands in the exact same manner as you did before. Are you surprised/amazed by the results?  Did you successfully get the one hand bigger, or longer than the other?   Many people do.  An average difference of half-an-inch finger length is often noted - some people more, others less.  The power of your mind has done this.  After you've rested a bit you can try this exercise with the other hand and see if you get better results or not.  For some, one hand may be easier to "grow".</p>
			<p>You can work/play with this exercise often as a tool for training your mind. Feel free to be creative with it.</p>
