	        <h1 class="contentheading">Relaxation Techniques</h1>
	        <h2>By Grandmaster Shou-Yu Liang and Master Wen-Ching Wu</h2>
	        <br><br>
	        <p>The Basic Relaxation Technique (<I>Fangsong Gong</I>) is especially effective for helping people with <I>deficient-yin accompanied with excess-fire</I>, and people with high blood pressure.  It is also helpful for relaxing nervous tension from the pressure of work and to regain a vitality of spirit.  Fangsong Gong will also set a sound foundation for your other qigong training.</p>
			<p>We will be using exhalations to assist our mental and physical relaxation.  During inhalation, the body is in a <I>condensing</I> mode.  This makes it difficult for the body to relax.  During exhalation, the body is in an <I>expanding</I> mode.  This makes it easier for the body to relax.  Also, physiologically speaking, during an inhalation, the intercostal muscles and diaphragm must contract to raise the ribs.  During an exhalation, the intercostal muscles and the diaphragm naturally relax and return to the uncontracted position, making it easier for the body to relax.</p>
			<h3>Preparation:</h3>
			<p>Posture - Sit comfortably on a chair with your hands placed naturally on your knees or with your hands overlapping and thumbs touching each other.  Men should have their left hand on top and women should have their right hand on top.</p>
			<h3>Visualization:</h3>
			<p><b>Step 1.</b> With your eyes closed, sit for about three minutes.  While exhaling direct the mind to relax the entire body, one section at a time.  Begin from your head (eyes, ears, nose, mouth, ...) and pay special attention to your eye bridge area (<I>Yintang</I>).  Relax your hands, your chest, your abdomen, your thighs, and down to the top of your feet.</p>
			<p>Then relax the back of your head, relax your back, relax your waist, relax your hips, relax the back of your thighs, and relax the bottom of your feet.  Repeat the exercise a few more times until you feel light, relaxed, and comfortable.</p>
			<p><b>Step2.</b>Next, simply think of the word <I>song</I>,  or pronounce the word <I>song</I> softly as you exhale and spread <I>song</I> all over your body.  Feel your body and evergy channels, immersing and melting into a tranquil comfortable state.</p>
			<p>The word <I>song</I>, literally means loose.  It is pronounced, first with your teeth gently touching each other and with your tongue pressing gently on your teeth as you release the air in your lungs from your mouth, making a <I>sssss</I>...sound.  Then separate your teeth and allow the air to exit from both your mouth and nose making a <I>ong</I>...sound that resonates like a bell.</p>
			<p><b>Key Points:</b></p>
			<ol type="A">
				<li>If you should find that some areas are not easy to relax, don't be overly concerned, go on to the next area.  With practice you will gradually be able to relax your entire body.</li>
				<li>If you should feel that some areas of your body are tingling, itching, are warm, or your muscles contract slightly, or you see flashing lights during the training, don't worry.  These are natural qi reactions called qigan -- qi sensations.</li>
				<li>Usually after this qigong training you will have a peaceful, relaxing sleep.  Some people, however,whentheyfirst begin their training, are energized and can't fall asleep.  If this should happen to you, lay down on your bed and do this Relaxation Technique.  This should help you fall asleep.</li>
				<li>If your eye lids should twitch and they are unable to stop moving, you should open your eyes for a while, then close your eyes halfway and continue the training.</li>
				<li>If you should feel any discomfort during training, check your posture and make any necessary adjustments until you feel comfortable.  Make sure that you are not leaning, your waist is relaxed, your shoulders are down, and your hands are placed comfortably in place.</li>
			</ol>
