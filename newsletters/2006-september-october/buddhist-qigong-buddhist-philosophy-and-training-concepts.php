			<p>Tantric Buddhist cultivation involves the cultivation of the Esoteric Abilities of the Body, Speech, and Mind, to reach nirvana. Because their practices are very profound, this sect of Buddhism gradually became known as Mizong in China. Mizong literally means the Secret Doctrine. The implication, however, is not due to the secretive nature of the practice, but due to the extensive and profound nature of the practice.</p>
