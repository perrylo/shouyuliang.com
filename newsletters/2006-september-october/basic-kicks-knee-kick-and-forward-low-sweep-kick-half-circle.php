			<h3>Knee Kick</h3>
			<p>This is a kick with the top of your knee while pulling your arms in the opposite direction of your kick.</p>
			<p><b>Drill:</b> Start with your left leg forward and arms extended in front of your body (Figure 2-43). Pull both arms back and lift your right knee up and forward as high as you can (Figure 2-44). Bring your right foot back down to the starting position and extend your arms forward. Repeat the kick several times. Then do the kick with the other knee.</p>
			<h3>Forward Low Sweep Kick-half circle</h3>
			<p>This is a kick with the front of your leg and foot, by pivoting on your front foot while sweeping your back leg forward. It is done with the body low to the ground and with the kicking foot touching the floor during the kick.</p>
			<p><b>Drill:</b> Start with your left leg forward and extend your left arm up (Figure 2-45). Push off with your right foot and pivot on the ball of your left foot. At the same time, sweep your right leg forward in a half circle (Figure 2-46). Shift your weight forward to your right leg and extend your right arm up. Then push off with your left foot, pivot on the ball of your right foot, and sweep your left leg forward in a half circle. Alternate from side to side across the training area.</p>
