	        <h2>By Ayron Howey</h2>
	        <br><br>
			<p>The last newsletter I wrote about where I believe Sanshou fits into the martial arts world and I hope to make this an underlying theme throughout upcoming issues. I would like to differentiate between the two distinct methods of practicing Sanshou.  Sanshou means free fighting.  To clarify, this means any type of non-choreographed sparring or applications. The two distinct types are what I will refer to as Traditional Sanshou and Modern (or Competition) Sanshou.</p>
			<p>Traditional Sanshou is performed without the standard equipment you would see in modern competitions, no gloves, headgears, shin pads etc. You do not use just closed fists but can now deliver palm strikes, finger jabs, elbows, knee strikes, grabs and joint locks as well as allow the flavour of your Wushu to be more prominent. This is a freer form of Sanshou and has advantages that Modern does not. Allowing one less restriction in attacks and defense allows a student to understand their forms or lessons more easily. Direct application in fighting opens your mind to the intricacies of the art. On the other hand, due to a lack of gear or safety equipment, you cannot use as much power as you would in modern Sanshou, and need to exercise more controlled attacks to prevent serious injury.</p>
			<p>Modern/Competition Sanshou is full contact with all the necessary gear. Boxing gloves (note that open-finger gloves are NOT part of Sanshou), boxing/Sanshou headgear (note that karate point fighting head gear is NOT part of Sanshou), shin pads/instep pads, mouth guard, chest protector (note that tae kwon do chest guards are NOT part of Sanshou) and cup, this is the standard gear and should be worn in training as well. The Modern style follows well established rules such as the fighting of rounds, the size of ring (10m by 10m) the restriction of continuous head punches and more, all designed to allow students from all Chinese Wushu, regardless of their particular styles, to meet and compete against each other on equal ground. It provides a uniform method of competition that shows the beauty and skills of Chinese Wushu. Being a full contact sport elevates it above the point sparring you see in other disciplines, which is not full contact and to me doesn't show any of the traditional flavour of the arts they represent. Being full contact show that Chinese Wushu is powerful and explosive and is combative retaining the "martial" in the term "martial arts". Chinese Wushu, or Martial Arts is not only beautiful but also effective, and I think that Sanshou demonstrates this, both Modern and Traditional.</p>
			<p>In closing I would encourage the readers to write in and respond to my article. Pros and Cons or things you would just like to add. I'll post interesting and relative responses and answer any questions that I can.</p>
