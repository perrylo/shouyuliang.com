			<h3>Intensive Iron Shirt Qigong</h3>
			<p>Iron Shirt and Golden Bell Cover are qigong training that build up the human body's ability to withstand an attack.  A Chinese proverb states, "First learn how to take hits, then learn how to fight."  That is to say, before one gets involved in free sparring, one should first attain the ability to withstand physical strikes.  Iron Shirt training makes your body so strong that it is like you are wearing a shirt made of iron - iron shirt.  It is also like your body is covered by a golden bell  - golden bell cover.</p>
			<p>There are many different ways to develop the Iron Shirt ability.  Much of this training will takeat leastthree years to complete.  The Iron Shirt training in this section is an Intensive Iron Shirt Qigong.  If you practice twice a day, once in the morning and once at night, you will attain significant results from this training in thirty days.  Of course, if you only train once a day or once every few days, the length of time necessary will increase.</p>
			<p>In today's society, we are all very busy.  Not everyone can concentrate or has the time to train everyday.  From our once a week classroom instruction and training experience, it took three to five months for students to attain significant results.  In our Iron Shirt training program, we also include conditioning exercises for the palms, fingers, arms, etc.</p>
			<p>Even though it takes a short amount of time to complete the Intensive Iron shirt Qigong training,it stillneeds a long period of continuous training to solidify and maintain your attainment.  A Chinese proverb states, "Impatiently rushing produces no result."  This proverb implies that when one rushes to complete something, one tends to become bewildered and not be able to achieve one's objective.  In other words, what one should be concerned with, is the effectiveness of the training, not the length of time needed in the training.  If you simply complete the training in a short period of time and stop training, the Kung Fu you attained will not stay with you for very long.  People in training often say, "Live until old age, learn until old age."</p>
