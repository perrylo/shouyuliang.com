	        <h2>By Ayron Howey</h2>
	        <br><br>
			<p>The following combinations should be done in front of a mirror or wall and should be done continuously for two minutes, like an actual round. Movements should be relaxed and the body should not be tense. Power is exerted at the end of punches or kicks with a snap. Rhythm is key. Light footwork, strong stance and up on the toes driving with the balls of the foot. By conditioning the body, you in turn condition the mind, training your reflexes to react on instinct.</p>
			<ol type="1">
				<li>Jabs - bouncing lightly on the toes, every third bounce is forward ending in a jab with the forward hand. Do this for 1 minute then without pausing, switch stance so that the other hand now jabs. Complete at 2 minutes.</li>
				<li>Jab/Straight punch - bouncing lightly on the toes, every third bounce ending in a jab, then followed by a straight punch using the waist and rear foot to drive the rear punch forwards. Bounce backwards and continue for 2 minutes.</li>
				<li>Jab/Straight/Hook - bouncing lightly on the toes, every third bounce ending in a jab, then followed by a straight punch using the waist and rear foot to drive the rear punch forwards, then twist the waist and throw a hook punch with the same hand that threw the jab. Bounce backwards and continue for 2 minutes.</li>
				<li>Jab/Straight/Double Hook - bouncing lightly on the toes, every third bounce ending in a jab, then followed by a straight punch using the waist and rear foot to drive the rear punch forwards, then twist the waist and throw a hook punch with the same hand that threw the jab and then twist the other way throwing another hook (same hand that threw the straight punch). Bounce backwards and continue for 2 minutes.</li>
				<li>Single leg attack - bouncing lightly on the toes, on every third bounce lunge ahead with the forward foot as the back bends and legs crouch. Your hands should reach forward in a double grab. At the end of the lunge, return to stance, then bounce back and repeat for 2 minutes.( This simulates a single-leg takedown grappling technique.)</li>
			</ol>
			<p>Variations</p>
			<ol type="a">
				<li>At the end of lunge, instead of standing up, jump up, bringing legs up as high as you can with hands in defensive position.</li>
				<li>At the end of the lunge, stand up and as you bounce pack throw a jab.</li>
				<li>At the end of the lunge, stand up and as you bounce pack throw a jab, then a straight.</li>
