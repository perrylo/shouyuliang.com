			<h3>Morality of Deed: Trust (Xin Yong)</h3>
			<p>Trust includes being trustworthy, and also trusting yourself.  You must develop a personality which other people can trust.  For example, you should not make promises lightly, but if you have made a promise, you should fulfill it.  Trust is the key to friendship, and the best way of earning respect.  The trust of a friend is hard to gain, but easy to lose.  Self-trust is the root of confidence.  You must learn to build up your confidence and demonstrate it externally.  Only then can you earn the trust and respect of others.</p>
			<p>There is an ancient Chinese story about Emperor You of Zhou (781-771 BC).  When Emperor You attacked the kingdom of Bao, he won a beautiful lady named Bao Shi.  However, although she was beautiful, Bao Shi never smiled.  In order to make her smile, the Emperor gave her precious pearls and jewels to wear, and delicious things to eat.  He tried a thousand things but still Bao Shi wouldn't smile.  The Emperor was the monarch of the country and yet he couldn't win a smile from the beautiful lady.  It made him very unhappy.</p>
			<p>At that time, the country of Zhou had platforms for signal fires around its borders.  If an enemy attacked the capital, the fires were lit to signal the feudal lords that their emperor was in danger, and they would immediately send out troops to help.  The fires were not to be lit unless the situation was critical.  However, the emperor thought of a way to use them to please Bao Shi.  He ordered the signal fires lit.  The feudal lords thought that the capital city was in great danger, so a vast and mighty army of soldiers soon came running.</p>
			<p>When Bao Shi saw all the troops rushing crazily about in a nervous frenzy, she unconsciously let out a great laugh.  Emperor You was so happy that he smiled and smiled, and completely forgot about the lords, standing there staring blankly.  After a while the Emperor said, "It's nothing.  Everyone go home."</p>
			<p>Emperor You completely forgot about the importance of the signal fires, and went so far as to light them several times in order to win Bao Shi's smile.  The lords all knew that they had been made fools, and were furious.</p>
			<p>Later, Emperor You dismissed his empress, Lady Shen, in favor of his concubine Bao Shi.  Lady Shen's father was greatly angered, and united with a foreign tribe called the Quan Rong to attack Emperor You.  When Emperor You's situation grew urgent, he ordered the signal fires lit, summoning the feudal lords to save him and the capital.  Even as he died, the Emperor never understood that, because of the games he had played with the signal fires, not even one lord would come to save him.</p>
