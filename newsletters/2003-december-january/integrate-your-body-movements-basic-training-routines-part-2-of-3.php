			<h3>Five Stance Routine (Wu Bu Quan)</h3>
			<p><b>Preparation</b></p>
			<p><b>Step 1</b><br />
			Start with your feet together and your arms naturally down at your sides (Figure 1-105).  Bring yourfists upto your waist. At the same time, turn your head to look to your left (Figure 1-106).</p>
			<p><b>Punch while in a Bow Stance</b></p>
			<p><b>Step 2</b><br />
			Bend both knees and shift your weight onto your right leg as you step to your left with your left foot into a left Gong Bu (Bow Stance).  At the same time, circle block with your left arm and punch forward with your right first (Figure 1-107).</p>
			<p><img src="../../images/newsletter/v3n1/a6p1.jpg" border="0" width="500" height="260" alt="Integrate Your Body Movements - Basic Training Routines - Five Stance Routine"></p>
			<p><b>Punch While Spring Kicking Forward</b></p>
			<p><b>Step 3</b><br />
			Stand up completely on your left leg and Spring Kick (Toe/Top of the Foot Kick) forward with your right leg.At the same time, pull your right fist back to your waist and punch forward with your left fist (Figure 1-108).</p>
			<p><b>Block Up and Punch in Horse Stance</b></p>
			<p><b>Step 4</b><br />
			Step down with your right foot into a Ma Bu (Horse Stance) as you turn your body 90 degrees to your left.  At the same time, life your left arm up over your head and punch to your right side with your right fist (Figure 1-109).</p>
			<p><img src="../../images/newsletter/v3n1/a6p2.jpg" border="0" width="500" height="260" alt="Integrate Your Body Movements - Basic Training Routines - Five Stance Routine"></p>
			<p><b>Block Down and Punch while in a Resting Stance</b></p>
			<p><img src="../../images/newsletter/v3n1/a6p3.jpg" align="right" border="0" width="168" height="260" alt="Integrate YourBody Movements - Basic Training Routines - Five Stance Routine">
			<b>Step 5</b><br />
			Step to your right by crossing your left leg behind your right leg.  At the same time, block down infront ofyourheadwith your right arm and pull your left hand back to your waist in a fist (Figure 1-110).  Lower your body down into Xie Bu (Resting Stance).  At the same time, pull your right hand back to your waist in a fist, and punch forward with your left fist (Figure 1-111).</p>
			<p><b>Lift Knee, Half-Squat Stance Down, and Spear Palm</b></p>
			<p><b>Step 6</b><br />
			Turn your body 90 degrees to your left as you stand up, and open your left hand (Figure 1-112).  Shift all your weight onto your right leg and bring your left knee up as high as you can.  At the same time, pull your left hand down next to your right armpit and spear up with your right hand over your left hand (Figure 1-113).</p>
			<p><b>Step 7</b><br />
			Bend your right knee to lower your body and step to your left with your left foot into Pubu (Half-Squat Stance).At the same time, spear your left hand along your left leg until your toes (Figure 1-114).</p>
			<p><img src="../../images/newsletter/v3n1/a6p4.jpg" border="0" width="500" height="260" alt="Integrate Your Body Movements - Basic Training Routines - Five Stance Routine"></p>
			<p><b>Lift Palm While in Empty Stance</b></p>
			<p><b>Step 8</b><br />
			Shift your weight forward and lift your left arm up (Figure 1-115).  Turn your left foot out and step forward with your right foot into a right Xu Bu (Empty Stance).  At the same time, change your left hand into a hook as you pull it back to your back left corner; and scoop your right hand up until your fingers are pointing up (Figures 1-116 and 1-116a, back view).</p>
			<p><img src="../../images/newsletter/v3n1/a6p5.jpg" border="0" width="500" height="260" alt="Integrate Your Body Movements - Basic Training Routines - Five Stance Routine"></p>
			<p><b>Closing</b></p>
			<p><b>Step 9</b><br />
			Turn your body 90 degrees to your left and bring you left foot next to your right foot.  At the sametime, changebothhands into fists and bring them back to your waist (Figure 1-117).</p>
			<p><img src="../../images/newsletter/v3n1/a6p6.jpg" align="right" border="0" width="168" height="260" alt="Integrate YourBody Movements - Basic Training Routines - Five Stance Routine">
			<b>Step 10</b><br />
			Turn your head to look to your left and repeat Steps 2 through 9.  This will bring you back to the starting position.</p>
			<p>When you have returned to the starting position, you may repeat the exercise or do the mirror image. Thatis, instead of stepping to your left and punching with your right fist, step to your right and punch with your left fist.  Do the mirror image of Steps 2 through 9.  When you get to the end, do the mirror image again, which will bring you back to the starting position.  Keep alternating from side to side by doing the left side twice and the right side twice, as many times as you wish.</p>
