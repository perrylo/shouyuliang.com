	        <h1 class="contentheading">A Master's Booklist</h1>
	        <h2>By Kelly Maclean</h2>
	        <br><br>
			<p>Master Kelly Maclean is one of the top instructors at the Shou-Yu Liang Wushu Taiji Qigong Institute,and specializes in Taiji and internal styles.  She has been both grandchampion and master judge at many competitions, and has extensive experience with internal martial arts.  But what does a master refer to for training notes and information?  We asked Kelly, and here are her recommendations.</p>
			<p>"Of course, I would like to recommend any of Grandmaster Liang's books, but for Taiji enthusiasts, I would especially recommend his book Tai Chi Chuan on the 24 and 48 Forms. This book gives some history and theory, and is an excellent manual for anyone learning either of the two forms. Here are some other suggestions:</p>
			<p>
				<b><A HREF="http://www.amazon.com/exec/obidos/ASIN/0804813574/ref=nosim/theofficiasho-20" TARGET="_new"><img src="../../images/newsletter/v3n1/a3p1.jpg" align="right" border="1" alt="Get the Tao of Tai-Chi from Amazon.com">The Tao of Tai-Chi Chuan</a><br />
				<span class="small">
				By Jou, Tsung Hwa<br />
				Tai Chi Foundation, Taiwan, 1981<br />
				</span>
				</b>
				This book gives an excellent overview of what Taiji is, its history, the philosophy that informs it,and its theories, with a chapter on Push Hands.  This book is available through <A HREF="http://www.amazon.com/exec/obidos/ASIN/0804813574/ref=nosim/theofficiasho-20" TARGET="_new">Amazon.com</a>.
			</p>
			<p>
				<b><A HREF="http://www.amazon.com/exec/obidos/ASIN/0452267560/ref=nosim/theofficiasho-20" TARGET="_new"><img src="../../images/newsletter/v3n1/a3p2.jpg" align="right" border="1" alt="Get the Tao of Tai-Chi from Amazon.com">Mastery</a><br />
				<span class="small">
				By George Leonard<br />
				Plume, USA, 1991<br />
				</span>
				</b>
				Recommended to me by Sam Masich, I would consider this a "must read" by anyone who is setting out onthe pathof learning Taiji. Written by a master of Aikido, who also draws on his experience as a pilot, this book is about the process of mastery. Its principles may be applied to any learning process, any important life project, and even personal relationships.  This book is available through <A HREF="http://www.amazon.com/exec/obidos/ASIN/0452267560/ref=nosim/theofficiasho-20" TARGET="_new">Amazon.com</a>.
			</p>
			<p>
				<b><a href="http://www.amazon.com/gp/redirect.shtml?ie=UTF8&location=http%3A%2F%2Fwww.amazon.com%2FTai-Chi-Secrets-Ancient-Masters%2Fdp%2F188696971X%3Fie%3DUTF8%26s%3Dbooks%26qid%3D1188675777%26sr%3D8-1&tag=theofficiasho-20&linkCode=ur2&camp=1789&creative=9325">Tai Chi Secrets of the Ancient Masters</a><br />
				<span class="small">
				Translation and commentary by Dr. Yang Jwing Ming<br />
				YMAA Publication Center, Boston, 1999<br />
				</span>
				</b>
				This is a very helpful presentation and explanation of the famous "Tai Chi Classics," and is an important read for anyone interested in Taiji theory.  This book is available through ><a href="http://www.amazon.com/gp/redirect.shtml?ie=UTF8&location=http%3A%2F%2Fwww.amazon.com%2FTai-Chi-Secrets-Ancient-Masters%2Fdp%2F188696971X%3Fie%3DUTF8%26s%3Dbooks%26qid%3D1188675777%26sr%3D8-1&tag=theofficiasho-20&linkCode=ur2&camp=1789&creative=9325">Amazon.com</a>.
			</p>
			<p>
				<b><A HREF="http://www.amazon.com/exec/obidos/ASIN/1886969868/ref=nosim/theofficiasho-20" TARGET="_new"><img src="../../images/newsletter/v3n1/a3p3.jpg" align="right" border="1" alt="Get the Tao of Tai-Chi from Amazon.com">101 Reflections on Tai Chi Chuan</a><br />
				<span class="small">
				By Michael Gilman<br />
				YMAA Publication Center, Boston, 2000<br />
				</span>
				</b>
				An easy and pleasant read, this is a book offering thoughts and observations on Taiji, by a long time Taiji practitioner.  This book is available through <A HREF="http://www.amazon.com/exec/obidos/ASIN/1886969868/ref=nosim/theofficiasho-20" TARGET="_new">Amazon.com</a>.
			</p>
			<p>
				<b><A HREF="http://www.amazon.com/exec/obidos/ASIN/0912059044/ref=nosim/theofficiasho-20" TARGET="_new"><img src="../../images/newsletter/v3n1/a3p4.jpg" align="right" border="1" alt="Get the Tao of Tai-Chi from Amazon.com">T'ai Chi's Ancestors</a><br />
				<span class="small">
				By Douglas Wile<br />
				Sweet Ch'i Press, USA, 1999<br />
				</span>
				</b>
				This is a scholarly work on the two thousand year development of soft-style martial arts theory.  This book is available through <A HREF="http://www.amazon.com/exec/obidos/ASIN/0912059044/ref=nosim/theofficiasho-20" TARGET="_new">Amazon.com</a>.
			</p>
			<p>
				<b><A HREF="http://www.amazon.com/exec/obidos/ASIN/1556433182/ref=nosim/theofficiasho-20" TARGET="_new"><img src="../../images/newsletter/v3n1/a3p5.jpg" align="right" border="1" alt="Get the Tao of Tai-Chi from Amazon.com">Mastering Yang Style Taijiquan</a><br />
				<span class="small">
				By Fu Zhongwen, translated by Louis Swaim<br />
				North Atlantic Books, Berkeley, 1999<br />
				</span>
				</b>
				This book will be of particular interest to Yang style Taiji players. Written by a close student of YangCheng Fu, one of the most important figures in the history of Taiji, this book is an elucidation of the Tai Chi Classics, and a manual on the traditional Yang form.  This book is available through <A HREF="http://www.amazon.com/exec/obidos/ASIN/1556433182/ref=nosim/theofficiasho-20" TARGET="_new">Amazon.com</a>.
			</p>
			<p>And for Chen style enthusiasts:</p>
			<p>
				<b><A HREF="http://www.amazon.com/exec/obidos/ASIN/1556433778/ref=nosim/theofficiasho-20" TARGET="_new"><img src="../../images/newsletter/v3n1/a3p6.jpg" align="right" border="1" alt="Get the Tao of Tai-Chi from Amazon.com">Chen Style Taijiquan</a><br />
				<span class="small">
				By Davidine Siaw-Voon Sim and David Gaffney<br />
				North Atlantic Books, Berkeley, 2000<br />
				</span>
				</b>
				This book is an expose of Chen style Taiji, the oldest style known to this day. Especially interesting is a chapter on "Chen Family Legends."  This book is available through <A HREF="http://www.amazon.com/exec/obidos/ASIN/1556433778/ref=nosim/theofficiasho-20" TARGET="_new">Amazon.com</a>.
			</p>
