				<li>Our school offers a system of Xing Yi that includes traditional routines and applications as follows:</li>
				<li>Five Phases (Five Fists) which include Pi, Zhuan, Beng, Pao, Heng which correspond to the Five Phases of Metal, Water, Wood, Fire, and Earth.</li>
