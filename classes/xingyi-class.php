			<h2>What is Xingyi?</h2>
			<p><strong>Xingyi</strong> is an internal Wushu style known for its dynamic and explosive movements. Xing or shape, is derived from imitating the fighting movements of 12 different animals. Yi or mind, is generated internally from the heart. Therefore, Xingyi is an internal style which imitates the shapes of twelve animals and their inner characteristics when they fight. Xing Yi's theory and techniques are aggressive, yet the practitioner avoids emotional excitement. Xing Yi manifests great power, but it is a very refined power, and while the strategy is offensive, it is not disordered. Continued training in Xing Yi can make you spiritually brave, your spirit can be raised to a very powerful stage, yet you will remain calm, peaceful, and in control.</p>
			<p>This style consists of five fundamental movements: Pi(Split), Zuan(Drill), Beng(Expand), Pao(Cannon), Heng(Sideways shear). The five movements are matched up with Metal, Wood, Water, Fire, and Earth elements from the Five Element Theory. It is characterized by linear movements and uses offense as a way of defense. Our school offers a system of Xingyi including traditional fist and weapon routines and applications such as Five Phases, Twelve Shapes, Miscellaneous Strike Sequence, Linking Sequence, Secure Body Strike matching set, etc.</p>
			<h2>Why learn Xing Yi? Who should take this class?</h2>
			<ul>
				<li>It is highly advised that students should have some Wushu basic training before taking up Xing Yi classes.</li>
				<li>For people who wish to improve their focus, power, stamina, coordination, strength, balance, fitness level, endurance, and confidence.</li>
				<li>The basic forms are simple and easy to learn. There are hundreds of variations derived from the basic movements and their applications are countless.</li>
				<li>The fundamental theory and training principles are easy to understand.</li>
				<li>From a health point of view, the movements of Xing Yi are designed for strengthening the five important Yin organs: heart, lungs, kidneys, liver, and spleen.</li>
				<li>Xing Yi's theory and techniques are aggressive, yet the practitioner avoids emotional excitement. Xing Yi manifests great power, but it is a very refined power and while the strategy is offensive it is not disordered. Continued training in Xing Yi can make you spiritually brave; your spirit can be raised to a very powerful level, yet you will remain calm, peaceful, and in control.</li>
			</ul>
