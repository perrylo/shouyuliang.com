			<h2>What is the Wuji Xiao Yao System?</h2>
			<p><strong>Wuji</strong> literally means "no extremity". It is an ancient internal martial art form long veiled in mystery. A great number of people have long heard of; few, however, have ever seen it, and an even fewer have had a mastery over it. The entire form of Wuji depicts a vivid story that embodies profound Taoist and Buddhist philosophies; it is about how we cultivate our ‘original spirit’ following the course of the cosmos and harmonizing with the myriad of changes of the universe. Learnt originally from his grandfather in the 1940’s, Grandmaster Shouyu Liang is one of the rare few who has a mastery over the physical form and its many applications as well as having an in depth understanding of the theories and philosophies of this art. Grandmaster Liang had performed Wuji in China 30 years ago. In 1985, he had again performed this form at the First International Wushu Invitational Competition held in China. This precious art is now taught by Grandmaster Shouyu Liang at the SYL Wushu Taiji Qigong Institute.</p>
			<p><strong>Xiao Yao</strong> means to accord or comply with one's heart, to devote one's heart to doing something with no restraint. When applied to the martial arts, it means that when one's martial arts skill reaches a certain level, one enters into a state of freedom. Therefore, the Wuji Xiao Yao System does not restrain how each style or routine should be performed; instead, it emphasizes "dropping the rules and yet complying with the rules". As long as one understands how to utilize the principles and rules, one has free will in how to perform a routine according to his/her understanding. Grandmaster Liang never asks his students to completely imitate him when performing any routines. Instead he encourages them to find their own style hoping that they will surpass him one day. This is the nature of Wuji Xiao Yao System.</p>
			<p>Utilizing 60 years of training, competition, and judging experience, Grandmaster Shouyu Liang has integrated the essence of many different styles of external and internal martial arts into a unique system called the "Wuji Xiao Yao System". This system has gained enormous recognition and praise from a great number of experts in the martial arts world. Wuji Xiao Yao System is not only practical in its applications, but is also extremely beautiful to watch. It contains numerous routines which focus training in free sparring, striking, grappling, throwing, pressure point striking, and weapon applications. Furthermore, the system integrates the essence of soft and hard Qigong, it also emphasizes the cultivation of Qi, light, and spirit with the goal of reaching enlightenment.</p>
