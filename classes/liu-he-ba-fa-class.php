			<h2>What is Liu He Ba Fa?</h2>
			<p><strong>Liu He Ba Fa (Water Style)</strong> is one of the major internal style martial arts. It originally passed down from generation to generation only to a handful of gifted and chosen students with exemplary courage and moral character. Liu He Ba Fa literally means Six Harmonies and Eight Principles. It utilizes the power of Xingyi as its center, utilizes the stepping patterns of Bagua for its turning and spinning, and utilizes the neutralizing power of Taiji for its variations. Its movements are sometimes high, sometimes low, sometimes fast, sometimes slow. These movements resemble that of floating clouds, and flowing water that is sometimes calm, sometimes surging. Therefore, it is also known as Water Style. The movements of Liu He Ba Fa are constantly fluctuating with clearly defined forward, backward, upward, downward, and lateral motions. Each and every movement is lead by the mind(xinyi),. The mind initiates and the movements follow. It is therefore, also known as Xinyi Liu He Ba Fa. As an internal art, it not only serves as a system of self defence; it also benefits health, strengthens the body, dispels sickness and increases longevity.</p>
			<p>There are two Liu He Ba Fa routines: Liu He Ba Fa Fist routine and San Pan Shi Er Shi routines. The Liu He Ba Fa Fist consists of sixty-six postures including Qigong and martial applications. It is divided into two sections, and each section is practiced as a routine. The San Pan Shi Er Shi routine is a foundation training routine that includes advancing, retreating, rising and lowering movements which is designed to develop reaction skills for combat.</p>
